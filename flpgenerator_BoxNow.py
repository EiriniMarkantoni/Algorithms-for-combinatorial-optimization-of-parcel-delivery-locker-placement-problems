def select_clients(grid, points, N, CL, grid_size):
    """
    Επιλέγει τυχαία CL πελάτες από τα συνολικά σημεία που χρησιμοποιούμε στο πρόβλημα 
    
    Οι πελάτες επιλέγονται από τα σημεία που έχουν ήδη οριστεί ως FACILITY_POINT,
    αντιπροσωπεύοντας το γεγονός ότι πελάτες και εγκαταστάσεις βρίσκονται
    σε σημεία του ίδιου πλέγματος
    
    Args:
        grid: NumPy array με ετικέτες σημείων
        points: Λίστα Point objects με συντεταγμένες
        N: Αριθμός συνολικών σημείων που θα χρησιμοποιήσουμε στο πρόβλημα
        CL: Αριθμός πελατών προς επιλογή
        grid_size: Διάσταση πλέγματος
    
    Returns:
        NumPy array με τους δείκτες των επιλεγμένων πελατών
    """
    print("--Selecting clients... (step 3)")

    client_list = set()                   # Set για αποφυγή διπλοεπιλογών
    clients = numpy.zeros(CL, dtype=int)  # Array για αποθήκευση δεικτών πελατών
    clients_in_grid = 0                   # Μετρητής επιλεγμένων πελατών

    while clients_in_grid < CL:
        # Τυχαία επιλογή δείκτη από τα N σημεία
        cl_index = random.randint(0, N - 1)

        # Έλεγχος αν ο δείκτης δεν έχει ήδη επιλεγεί
        if cl_index not in client_list:
            client_list.add(cl_index)

            # Ενημέρωση grid με ετικέτα πελάτη (οπτική ένδειξη)
            x = points[cl_index].x
            y = points[cl_index].y
            grid[x][y] = CLIENT_POINT

            # Αποθήκευση θέσης ως γραμμικός δείκτης (x * grid_size + y)
            clients[clients_in_grid] = x * grid_size + y

            clients_in_grid += 1

    return clients
    
    
