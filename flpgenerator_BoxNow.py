def compute_euclidean(point_coords):
    """
    Υπολογίζει τις ευκλείδειες αποστάσεις μεταξύ όλων των ζευγών σημείων
    Args:
        point_coords: NumPy array με συντεταγμένες όλων των σημείων (N x 2)
    
    Returns:
        NumPy array με τις ευκλείδειες αποστάσεις για όλα τα ζεύγη σημείων (N x N)
    """

    global max_euclidean
    print("--Computing Euclidean distances... (step 2.1)")
    
    # Χρήση NumPy broadcasting για αποδοτικό υπολογισμό όλων των αποστάσεων
    diff_x = point_coords[:, 0].reshape(-1, 1) - point_coords[:, 0] # Υπολογίζει και δημιουργεί έναν πίνακα με τις διαφορές στις x-συντεταγμένες για όλα τα ζεύγη σημείων
    diff_y = point_coords[:, 1].reshape(-1, 1) - point_coords[:, 1] # Υπολογίζει και δημιουργεί έναν πίνακα με τις διαφορές στις y-συντεταγμένες για όλα τα ζεύγη σημείων
    # Υπολογισμός τετραγωνικών αποστάσεων
    squared_distances = diff_x**2 + diff_y**2

    # Τετραγωνική ρίζα για τον τελικό υπολογισμό ευκλείδειων αποστάσεων
    euclidean_distances = numpy.sqrt(squared_distances)

    return euclidean_distances


def compute_shortestpath(point_coords):
    """
    Υπολογίζει τις συντομότερες αποστάσεις διαδρομής (Manhattan distance) μεταξύ όλων των σημείων
    Args:
        point_coords: NumPy array με συντεταγμένες όλων των σημείων (N x 2)
    
    Returns:
        NumPy array με τις αποστάσεις Manhattan για όλα τα ζεύγη σημείων (N x N)
    """
    print("--Computing Shortest Path distances... (step 2.2)")
    
    # Υπολογισμός διαφορών συντεταγμένων χρησιμοποιώντας broadcasting
    diff_x = point_coords[:, 0].reshape(-1, 1) - point_coords[:, 0] 
    diff_y = point_coords[:, 1].reshape(-1, 1) - point_coords[:, 1]  

    # Απόσταση Manhattan: άθροισμα απόλυτων τιμών διαφορών (υπολογίζεται ως: d=∣x2−x1∣+∣y2−y1∣)
    sp_distances = numpy.abs(diff_x) + numpy.abs(diff_y) 

    return sp_distances


def sample_synthetic_distance():
    """
    Δημιουργεί τυχαία συνθετική απόσταση για χρήση στο πρόβλημα
    
    Η συνάρτηση αυτή χρησιμοποιείται ΓΙΑ ΔΥΟ ΔΙΑΦΟΡΕΤΙΚΟΥΣ ΣΚΟΠΟΥΣ:
    
    1. ΑΠΟΣΤΑΣΕΙΣ ΠΕΛΑΤΗ-ΕΓΚΑΤΑΣΤΑΣΗ:
       Τα εύρη αποστάσεων αντιστοιχούν σε διαφορετικά μέσα μεταφοράς σύμφωνα με την λίστα ranges
       Τα βάρη δίνουν μεγαλύτερη πιθανότητα σε πεζούς και οχήματα καύσης
       καθώς είναι τα πιο συνηθισμένα μέσα μεταφοράς για πρόσβαση σε lockers
    
    2. ΑΠΟΣΤΑΣΕΙΣ ΕΓΚΑΤΑΣΤΑΣΗ-ΕΓΚΑΤΑΣΤΑΣΗ:
       Οι ίδιες κλάσεις αποστάσεων χρησιμοποιούνται για την προσομοίωση
       των αποστάσεων μεταξύ υποψήφιων τοποθεσιών εγκαταστάσεων
    
    ΣΗΜΕΙΩΣΗ: Αντί να χρησιμοποιηθούν οι υπολογισμένες Ευκλείδειες ή Manhattan
    αποστάσεις από τις συντεταγμένες του πλέγματος, επιλέγεται η δειγματοληψία
    συνθετικών αποστάσεων για μεγαλύτερο ρεαλισμό και προσομοίωση πραγματικών
    συνθηκών οδικού δικτύου με διαφορετικά μέσα μεταφοράς
    
    Returns:
        float: Τυχαία συνθετική απόσταση σε km με 2 δεκαδικά ψηφία

    """
    # Ορισμός εύρους αποστάσεων για κάθε μέσο μεταφοράς (km)
    # Τα εύρη αυτά εκφράζουν ρεαλιστικά μέγιστα όρια μετακίνησης ανά μέσο
    # (π.χ. έως 0.5 km για πεζή μετακίνηση)
    ranges = [
        (0.1, 0.5), # Πόδια
        (0.6, 1.0), # Ποδήλατο
        (1.1, 2.0), # ΜΜΜ
        (2.1, 3.5), # Όχημα με κινητήρα εσωτερικής καύσης
        (3.6, 6.0)  # Ηλεκτρικά οχήματα
    ]
    
    # Βάρη πιθανότητας για κάθε εύρος
    weights = [2.5, 1, 1, 2, 1]  
    
    # Επιλογή τυχαίου εύρους βάσει βαρών
    chosen_range = random.choices(ranges, weights=weights, k=1)[0]
    # Επιλογή τυχαίας τιμής μέσα στο επιλεγμένο εύρος
    return round(random.uniform(*chosen_range), 2)


