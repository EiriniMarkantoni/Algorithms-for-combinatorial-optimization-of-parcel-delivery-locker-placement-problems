def assign_t_m(grid, points, locker_requirements):
    """
    Αναθέτει τυχαίο διαθέσιμο όγκο σε κάθε πιθανή τοποθεσία και καθορίζει
    τον μέγιστο τύπο εγκατάστασης που μπορεί να φιλοξενήσει
    
    Η διαδικασία περιλαμβάνει:
    1. Ανάθεση τυχαίου διαθέσιμου όγκου (0.06-0.2 m³) σε κάθε τοποθεσία
    2. Καθορισμός του μέγιστου τύπου θυρίδας που μπορεί να εγκατασταθεί
       βάσει του διαθέσιμου όγκου
    
    Args:
        grid: NumPy array με ετικέτες σημείων
        points: Λίστα Point objects
        locker_requirements: Dictionary με απαιτήσεις όγκου ανά τύπο
    
    Returns:
        Dictionary που αντιστοιχίζει (x, y) -> τύπο εγκατάστασης (string)
    """

    facility_volumes = {}  # Dictionary: (x, y) -> διαθέσιμος όγκος (m³)

    # -------------------------------------------------------------------------
    # Βήμα 1: Ανάθεση τυχαίου διαθέσιμου όγκου σε κάθε facility point
    # -------------------------------------------------------------------------
    for point in points:
        # Έλεγχος αν το σημείο είναι πιθανή τοποθεσία εγκατάστασης
        if grid[point.x][point.y] == FACILITY_POINT: 
            # Τυχαίος όγκος μεταξύ 0.06 και 0.2 m³
            # Το εύρος επιλέγεται ώστε να επιτρέπει όλους τους τύπους θυρίδων
            facility_volumes[(point.x, point.y)] = round(random.uniform(0.06,0.2), 4)  # Τυχαίος διαθέσιμος όγκος (m³)

    print("--Deciding facility types for each point... (step 4)")
    decision = {}  # Dictionary: (x, y) -> τύπος εγκατάστασης

    # -------------------------------------------------------------------------
    # Βήμα 2: Καθορισμός τύπου εγκατάστασης βάσει διαθέσιμου όγκου
    # -------------------------------------------------------------------------
    for point in points:
        if grid[point.x][point.y] == FACILITY_POINT:  
            # Ανάκτηση διαθέσιμου όγκου για αυτό το σημείο
            available_volume = facility_volumes.get((point.x, point.y), None)

            # Έλεγχος αν υπάρχει διαθέσιμος όγκος για την εγκατάσταση
            if available_volume:
                # Καθορισμός μέγιστου τύπου θυρίδας που χωράει
                # Έλεγχος από το μεγαλύτερο προς το μικρότερο
                if available_volume >= locker_requirements["large/type 2"]:
                    decision[(point.x, point.y)] = "large/type 2"  # Μεγάλη εγκατάσταση
                elif available_volume >= locker_requirements["medium/type 1"]:
                    decision[(point.x, point.y)] = "medium/type 1"  # Μεσαία εγκατάσταση
                elif available_volume >= locker_requirements["small/type 0"]:
                    decision[(point.x, point.y)] = "small/type 0"  # Μικρή εγκατάσταση
                else:
                    # Αν ο όγκος δεν επαρκεί ούτε για small locker
                    decision[(point.x, point.y)] = "none"  
            else:
                # Αν δεν υπάρχει διαθέσιμος όγκος, δεν μπορεί να εγκατασταθεί locker
                decision[(point.x, point.y)] = "none"  

    return decision



