def initialize_grid(grid, points, grid_size, N):
    """
    Αρχικοποιεί το πλέγμα με πιθανές τοποθεσίες εγκαταστάσεων
    
    Αν N = grid_size², τότε όλα τα σημεία του πλέγματος είναι πιθανές τοποθεσίες
    Αλλιώς, επιλέγονται τυχαία N μοναδικές τοποθεσίες
    
    Args:
        grid: NumPy array (grid_size x grid_size) για την αποθήκευση ετικετών
        points: Λίστα με Point objects για αποθήκευση συντεταγμένων
        grid_size: Διάσταση του πλέγματος
        N: Αριθμός συνολικών σημείων που θα χρησιμοποιήσουμε στο πρόβλημα
    """

    print("--Initializing grid... (step 1)")
    i = 0
    used_positions = set() # Set για έλεγχο μοναδικότητας θέσεων

    # Περίπτωση 1: Όλα τα σημεία του πλέγματος είναι πιθανές τοποθεσίες
    if (grid_size * grid_size) == N:
        for i in range(N):
            x = i // grid_size            # Υπολογισμός γραμμής
            y = i % grid_size             # Υπολογισμός στήλης
            points[i].x = x
            points[i].y = y
            grid[x][y] = FACILITY_POINT   # Σήμανση ως πιθανή τοποθεσία

    # Περίπτωση 2: Τυχαία επιλογή N σημείων
    else:
        while i < N:
            # Τυχαία επιλογή συντεταγμένων
            x = random.randint(0, grid_size - 1)
            y = random.randint(0, grid_size - 1)

            # Έλεγχος αν η θέση δεν έχει ήδη επιλεγεί
            if (x, y) not in used_positions:
                points[i].x = x
                points[i].y = y
                grid[x][y] = FACILITY_POINT
                used_positions.add((x, y)) # Προσθήκη στο set χρησιμοποιημένων θέσεων
                i += 1


