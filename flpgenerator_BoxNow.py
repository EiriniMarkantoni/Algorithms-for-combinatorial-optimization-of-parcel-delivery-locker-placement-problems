# Ποσοστά ζήτησης ανά περιοχή
# Υψηλότερα ποσοστά υποδηλώνουν περισσότερους πελάτες και μεγαλύτερη ζήτηση
pct = [
    0.1548, 0.1451, 0.1417, 0.1409, 0.1206, 0.1098, 0.0972, 0.0899
]


# Παράμετρος λ για την εκθετική κατανομή της ζήτησης
# Μικρότερη τιμή λ → περισσότερες μεγάλες τιμές ζήτησης
# Μεγαλύτερη τιμή λ → περισσότερες μικρές τιμές ζήτησης
lambda_tol = 0.023


def calculate_demand_for_customers(CL, areas_limits, lambda_tol, pct):
    """
    Υπολογίζει τη ζήτηση για κάθε πελάτη χρησιμοποιώντας εκθετική κατανομή
    
    Η διαδικασία περιλαμβάνει:
    1. Τυχαία ανάθεση κάθε πελάτη σε μία από τις 8 γεωγραφικές περιοχές
    2. Δημιουργία ζήτησης από εκθετική κατανομή (πολλές μικρές, λίγες μεγάλες τιμές)
    3. Κλιμάκωση ζήτησης με το ποσοστό της περιοχής
    4. Περιορισμός μέγιστης ζήτησης στα 15 δέματα
    5. Μετατροπή σε ακέραιο αριθμό δεμάτων (τουλάχιστον 1)
    
    Args:
        CL: Αριθμός πελατών
        areas_limits: Λίστα με τα όρια των γεωγραφικών περιοχών
        lambda_tol: Παράμετρος εκθετικής κατανομής
        pct: Ποσοστά ζήτησης ανά περιοχή
    
    Returns:
        tuple: (customers, total_demand) όπου
            - customers: λίστα με tuples (περιοχή, ζήτηση, αριθμός_δεμάτων)
            - total_demand: συνολικός αριθμός δεμάτων όλων των πελατών
    """
    print("--Calculate demand for customers... (step 5)")
    customers = []    # Λίστα για αποθήκευση πληροφοριών πελατών
    for _ in range(CL):
        # Τυχαία τιμή [0, 1) για επιλογή γεωγραφικής περιοχής
        z = random.random()  
        # Εύρεση της περιοχής στην οποία ανήκει η τιμή z
        for i, (low, high) in enumerate(areas_limits):
            if low <= z < high:
                # Δημιουργία ζήτησης μέχρι να βρεθεί αποδεκτή τιμή
                while True:
                    # Εκθετική κατανομή: P(X ≤ x) = 1 - e^(-λx)
                    # Δίνει περισσότερες μικρές τιμές, λίγες μεγάλες
                    demand = numpy.random.exponential(1 / lambda_tol) * pct[i]
                    # Αποδοχή μόνο αν η ζήτηση δεν υπερβαίνει τα 15 δέματα
                    if demand <= 15:
                        # Μετατροπή σε ακέραιο (τουλάχιστον 1 δέμα)
                        num_parcels = max(1, round(demand))  
                        # Αποθήκευση: (αριθμός περιοχής, συνεχής ζήτηση, ακέραια δέματα)
                        customers.append((i + 1, demand, num_parcels))  
                        break  # Αν η ζήτηση είναι μεγαλύτερη από 15, απορρίπτεται και επαναλαμβάνεται η διαδικασία
    
    return customers



