# Αυτή η ενότητα περιλαμβάνει 4 ευρετικές μεθόδους κατασκευής (construction heuristics) που δημιουργούν αρχικές εφικτές λύσεις 
# για το πρόβλημα εγκατάστασης εγκαταστάσεων
# Οι ευρετικές χρησιμεύουν ως αρχική λύση στους μεθευρετικούς αλγορίθμους
#
# ΣΤΡΑΤΗΓΙΚΗ: Κάθε ευρετική χρησιμοποιεί διαφορετικό κριτήριο για να επιλέξει τις θέσεις:
# H1: Απόσταση (proximity to customers)
# H2: Κόστος (budget-aware)
# H3: Εφικτότητα (capacity-focused)
# H4: Τυχαιοποίηση (GRASP για διαφοροποίηση)
#
# ΟΛΑ τα heuristics ακολουθούν την ίδια δομή:
# 1. Επιλογή P θέσεων (open_sites)
# 2. Ανάθεση πελατών σε θέσεις (assign) με greedy + repair
# 3. Επιλογή ελάχιστου τύπου εγκατάστασης ανά θέση (open_types)
# 4. Έλεγχος προϋπολογισμού (Cmax)
# 5. Επιστροφή πλήρους λύσης ή empty_sol() αν αποτυχία


