# Αυτή η ενότητα περιέχει μεθευρετικούς αλγορίθμους που ΒΕΛΤΙΩΝΟΥΝ υπάρχουσες εφικτές λύσεις
# Δεν κατασκευάζουν λύσεις από το μηδέν

def decode_repair_from_open_sites(open_sites, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
    transport_mode, combined_cost_per_km, max_tries_swap=200):
    """
    DECODER + REPAIR: Μετατρέπει μια επιλογή θέσεων σε πλήρη εφικτή λύση
    
    ΣΚΟΠΟΣ:
    Χρησιμοποιείται από τις μεθευρετικές SA, ILS που τροποποιούν το open_sites
    Παίρνει ΜΟΝΟ τις θέσεις και "αποκωδικοποιεί" (decodes) σε πλήρη λύση:
    - Ανάθεση πελατών σε θέσεις (greedy + swap repair)
    - Επιλογή τύπων εγκαταστάσεων (minimal type)
    - Έλεγχος εφικτότητας (budget)
    
    ΧΡΗΣΗ:
    1. Simulated Annealing: Swap θέσεις → Decode → Evaluate
    2. ILS: Perturb θέσεις → Decode → Refine με VND
    3. Οποιαδήποτε μέθοδος που αλλάζει μόνο το open_sites
    
    ΛΟΓΙΚΗ "DECODE":
    Το "decode" σημαίνει: Από μερική λύση (θέσεις) → Πλήρη λύση (θέσεις + τύποι + αναθέσεις)
    
    ΛΟΓΙΚΗ "REPAIR":
    Αν η greedy ανάθεση αποτύχει → "Επιδιορθώνει" με swap repair
    
    ΑΛΓΟΡΙΘΜΟΣ:
    1. Sanitization: Έλεγχος open_sites (P θέσεις, ανοίξιμες)
    2. Greedy Assignment: Δοκιμή με max capacities
    3. Repair (αν χρειάζεται): Swap repair για εφικτότητα
    4. Type Selection: Minimal type ανά θέση
    5. Budget Check: total_cost ≤ Cmax
    
    Παράμετροι:
        open_sites: Λίστα με P indices θέσεων προς άνοιγμα
        P, N, E, CL: Αριθμοί εγκαταστάσεων, θέσεων, τύπων, πελατών
        cl_sp_distances: Πίνακας αποστάσεων [CL × N]
        max_distance: Μέγιστη απόσταση ανά πελάτη [CL]
        demand: Ζήτηση πελατών [CL]
        facility_types: Μέγιστος τύπος ανά θέση [N]
        capacities: Χωρητικότητα ανά τύπο [E]
        cost_types: Κόστος ανά τύπο [E]
        Cmax: Μέγιστος προϋπολογισμός
        transport_mode: Μέσο μεταφοράς ανά πελάτη [CL]
        combined_cost_per_km: Κόστος/km ανά μέσο [5]
        max_tries_swap: Μέγιστες προσπάθειες swap repair (προεπιλογή 200)
    
    Επιστρέφει:
        dict: {
            "feasible": True/False,
            "distance_obj": float,
            "total_cost": float,
            "open_sites": list[int],
            "open_types": dict {θέση → τύπος},
            "assign": array[CL],
            "xvec": array (full solution vector)
        }
        
        Αν αποτυχία, επιστρέφει _empty_sol()
    """

    # ========================================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ ΔΕΔΟΜΕΝΩΝ
    # ========================================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    dem = np.asarray(demand, float)

    # Boolean πίνακας εφικτότητας: dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
    dist_feasible = (cl_sp <= maxd[:, None])

    # ========================================================================
    # ΒΗΜΑ 1: SANITIZATION - Έλεγχος και καθαρισμός open_sites
    # ========================================================================
    # Η μεθευρετική μπορεί να έχει δημιουργήσει διπλότυπα
    # dict.fromkeys() trick: Αφαίρεση duplicates διατηρώντας σειρά
    open_sites = list(dict.fromkeys([int(i) for i in open_sites]))
    if len(open_sites) != P:  # ΕΛΕΓΧΟΣ: Ακριβώς P μοναδικές θέσεις
        return _empty_sol()  

    # Έλεγχος αν κάθε θέση επιτρέπει τουλάχιστον έναν τύπο
    for i in open_sites:
        # Βρες επιτρεπόμενους τύπους για αυτή τη θέση
        if len(list(_allowed_types(i, facility_types, E))) == 0:
            return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 2: GREEDY ASSIGNMENT - Δοκιμή με μέγιστες χωρητικότητες
    # ========================================================================
    # Δίνουμε στο greedy assignment τη μέγιστη δυνατή ελευθερία υποθέτοντας ότι κάθε θέση έχει τον ΜΕΓΙΣΤΟ επιτρεπόμενο τύπο (max capacity).
    max_cap = np.zeros(N, dtype=float)            # Αρχικοποίηση: όλες οι θέσεις με capacity=0
    for i in open_sites:
        jmax = min(int(facility_types[i]), E-1)   # Βρες τον μέγιστο επιτρεπόμενο τύπο για αυτή τη θέση
        if jmax < 0:
            return _empty_sol()
        max_cap[i] = float(capacities[jmax])      # Θέσε τη διαθέσιμη capacity = capacity του μέγιστου τύπου

    # ΔΟΚΙΜΗ GREEDY ASSIGNMENT. Scarcity-first greedy: Δύσκολοι πελάτες πρώτα
    assign, ok = _assign_greedy(open_sites, max_cap, dist_feasible, cl_sp, dem)

    # ========================================================================
    # ΒΗΜΑ 3: REPAIR (αν χρειάζεται) - Swap Repair για εφικτότητα
    # ========================================================================
    # ΤΙ ΚΑΝΕΙ:
    #      1. Εντοπίζει "δύσκολο" πελάτη (uncovered ή με λίγες επιλογές)
    #      2. Βρίσκει θέσεις που τον καλύπτουν
    #      3. Swap: Αφαιρεί "χειρότερη" ανοιχτή θέση, προσθέτει νέα
    #      4. Ελέγχει coverage και capacity
    #      5. Επαναλαμβάνει μέχρι επιτυχία ή max_tries
    if not ok:
        open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, dem, facility_types, capacities, E, P, max_tries=max_tries_swap)
        if not ok:
            return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 4: ΥΠΟΛΟΓΙΣΜΟΣ ΦΟΡΤΙΟΥ & ΕΠΙΛΟΓΗ MINIMAL TYPES
    # ========================================================================
    # Υπολογίζουμε το πραγματικό φορτίο κάθε θέσης και επιλέγουμε τον ΕΛΑΧΙΣΤΟ τύπο που το χωράει (για οικονομία)
    load = {i: 0.0 for i in open_sites}  # Αρχικοποίηση: μηδενικό φορτίο
    for k in range(CL):                  # Για κάθε πελάτη
        i = int(assign[k])               # Σε ποια θέση ανατέθηκε
        load[i] += float(dem[k])         # Πρόσθεσε τη ζήτηση του στο φορτίο της θέσης

    # Επιλογή μικρότερου τύπου ανά θέση. Για κάθε θέση, βρες τον μικρότερο τύπο με capacity ≥ load
    open_types = {}      # Dictionary: θέση → τύπος
    for i in open_sites:
        j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
        if j is None:
            return _empty_sol()
        open_types[int(i)] = int(j)

    # ========================================================================
    # ΒΗΜΑ 5: ΑΞΙΟΛΟΓΗΣΗ & ΕΛΕΓΧΟΣ BUDGET
    # ========================================================================
    # Υπολογισμός distance objective, υπολογισμός total cost (installation + transport), έλεγχος budget constraint
    dist_obj, total_cost = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

    if float(total_cost) > float(Cmax) + 1e-9:
        return _empty_sol()

    # ========================================================================
    # ΕΠΙΣΤΡΟΦΗ ΕΦΙΚΤΗΣ ΛΥΣΗΣ
    # ========================================================================
    # Επιτυχία! Δημιουργήθηκε πλήρης εφικτή λύση από το open_sites
    return {
        "feasible": True,
        "distance_obj": float(dist_obj),
        "total_cost": float(total_cost),
        "open_sites": list(open_sites),
        "open_types": open_types,
        "assign": np.array(assign, dtype=int),
        "xvec": _build_xvec(open_types, assign, N, E, CL),
    }


# ============================================================================================================================
# ΜΕΘΕΥΡΕΤΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ VND (Variable Neighborhood Descent)
# ============================================================================================================================
def meta_vnd_assignments(sol_init, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
        combined_cost_per_km, max_iters=200, top_scan_per_client=100, first_improvement=True, seed=0):
    """
    VND (Variable Neighborhood Descent): Βελτίωση λύσης με reassignment πελατών
    
    ΣΚΟΠΟΣ:
    Βελτιώνει μια υπάρχουσα λύση αναθέτοντας ξανά πελάτες σε πλησιέστερες θέσεις, λαμβάνοντας υπόψη capacity constraints και budget
    
    ΙΔΕΑ:
    "Αν ένας πελάτης μπορεί να μετακινηθεί σε πλησιέστερη θέση χωρίς να παραβιαστούν
     τα constraints, κάνε το για να μειωθεί η τιμή της αντικειμενικής συνάρτησης"
    
    NEIGHBORHOOD:
    Για κάθε πελάτη k: Δοκίμασε να τον μετακινήσεις από τρέχουσα θέση i_old σε
    πλησιέστερη θέση i_new (αν εφικτό με capacity & budget)
    
    ΑΛΓΟΡΙΘΜΟΣ:
    1. Για κάθε πελάτη (σε τυχαία σειρά):
       a) Εξέτασε τις top-K πλησιέστερες θέσεις (K=top_scan_per_client)
       b) Για κάθε υποψήφια θέση i_new:
          - Έλεγχος: Βελτιώνει την απόσταση; (d_new < d_old)
          - Έλεγχος: Εφικτό capacity; (νέα φορτία χωράνε)
          - Έλεγχος: Εφικτό budget; (νέο total_cost ≤ Cmax)
          - Αν ΝΑΙ σε όλα → Υποψήφια βελτίωση
       c) Επιλογή ΚΑΛΥΤΕΡΗΣ βελτίωσης (ή πρώτης αν first_improvement=True)
    2. Αν βρέθηκε βελτίωση → Εφαρμογή και επανάληψη
    3. Αν όχι → ΤΕΡΜΑΤΙΣΜΟΣ (local optimum)
    
    FIRST IMPROVEMENT vs BEST IMPROVEMENT:
    - first_improvement=True: Σταμάτα μόλις βρεις ΜΙΑ βελτίωση (πιο γρήγορο)
    - first_improvement=False: Εξέτασε ΟΛΟΥΣ τους πελάτες, επίλεξε καλύτερη (πιο αργό)
    
    ΠΡΟΕΠΕΞΕΡΓΑΣΙΑ:
    Για επιτάχυνση, προϋπολογίζει για κάθε πελάτη τις εφικτές θέσεις
    ταξινομημένες κατά απόσταση → Σταματά early όταν d_new ≥ d_old
    
    Παράμετροι:
        sol_init: Αρχική λύση (από heuristics H1-H4)
        P, N, E, CL: Αριθμοί εγκαταστάσεων, θέσεων, τύπων, πελατών
        cl_sp_distances: Πίνακας αποστάσεων [CL × N]
        max_distance: Μέγιστη απόσταση ανά πελάτη [CL]
        demand: Ζήτηση πελατών [CL]
        facility_types: Μέγιστος τύπος ανά θέση [N]
        capacities: Χωρητικότητα ανά τύπο [E]
        cost_types: Κόστος ανά τύπο [E]
        Cmax: Μέγιστος προϋπολογισμός
        transport_mode: Μέσο μεταφοράς ανά πελάτη [CL]
        combined_cost_per_km: Κόστος/km ανά μέσο [5]
        max_iters: Μέγιστες επαναλήψεις VND (προεπιλογή 200)
        top_scan_per_client: Πόσες πλησιέστερες θέσεις να εξετάσει (προεπιλογή 100)
        first_improvement: True=σταμάτα στην πρώτη βελτίωση (προεπιλογή True)
        seed: Random seed για την τυχαία σειρά πελατών (προεπιλογή 0)
    
    Επιστρέφει:
        dict: Βελτιωμένη λύση με ίδια δομή με sol_init
              Αν sol_init είναι infeasible, επιστρέφει _empty_sol()
    """

    # Γεννήτρια τυχαίων αριθμών για τυχαία σειρά πελατών
    rng = random.Random(seed)

    # Εκκίνηση μόνο αν υπάρχει εφικτή αρχική λύση από τους ευρετικούς
    if (sol_init is None) or (not sol_init.get("feasible", False)):
        return _empty_sol()

    # ========================================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ ΔΕΔΟΜΕΝΩΝ
    # ========================================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    dem = np.asarray(demand, float)
    
    # ΕΞΑΓΩΓΗ ΑΡΧΙΚΗΣ ΛΥΣΗΣ
    # Ανοιχτές θέσεις (μετατροπή από list σε set για γρηγορότερο έλεγχο)
    open_sites = [int(i) for i in sol_init["open_sites"]]
    open_set = set(open_sites)  # Για γρήγορο "i in open_set"

    # Ανάθεση πελατών (copy για να μην τροποποιήσουμε την αρχική)
    assign = np.array(sol_init["assign"], dtype=int).copy()

    # ΠΡΟΫΠΟΛΟΓΙΣΜΟΣ ΚΟΣΤΟΥΣ ΜΕΤΑΦΟΡΑΣ ΑΝΑ ΠΕΛΑΤΗ
    # Για κάθε πελάτη k, αποθήκευσε το cost_per_km του μέσου μεταφοράς του. Αυτό επιταχύνει τους υπολογισμούς (αποφεύγει επαναλαμβανόμενα lookups)
    mode_cost = np.array([float(combined_cost_per_km[int(transport_mode[k])]) for k in range(CL)], dtype=float)

    # ========================================================================
    # ΠΡΟΕΠΕΞΕΡΓΑΣΙΑ: Ταξινομημένες λίστες εφικτών θέσεων ανά πελάτη
    # ========================================================================
    # Για κάθε πελάτη k, δημιουργούμε μια λίστα εφικτών θέσεων ΤΑΞΙΝΟΜΗΜΕΝΩΝ κατά απόσταση (πλησιέστερες πρώτες)
    # Έτσι πετυχαίνουμε:
    #   - Επιτάχυνση: Μπορούμε να σταματήσουμε early όταν d_new ≥ d_old
    #   - Εξοικονόμηση: Δεν χρειάζεται να ταξινομούμε σε κάθε iteration
    # ΔΟΜΗ: sorted_sites[k] = [i1, i2, i3, ...] όπου distance(k,i1) ≤ distance(k,i2) ≤ ...
    sorted_sites = []
    for k in range(CL):   # Για κάθε πελάτη
        row = cl_sp[k]    # Αποστάσεις από πελάτη k σε όλες τις θέσεις [N]
        md = maxd[k]      # Μέγιστη επιτρεπόμενη απόσταση για πελάτη k

        # ΦΙΛΤΡΑΡΙΣΜΑ: Κράτα μόνο εφικτές θέσεις (distance ≤ max_distance)
        # Δημιουργία λίστας (distance, index) tuples για εφικτές θέσεις. Χρήση μικρής ανοχής (1e-12) για αριθμητικά σφάλματα
        cand = [(row[i], i) for i in range(N) if row[i] <= md + 1e-12]
        # ΤΑΞΙΝΟΜΗΣΗ: Κατά απόσταση (αύξουσα - πλησιέστερες πρώτες)
        cand.sort(key=lambda t: t[0])
        # ΑΠΟΘΗΚΕΥΣΗ: Μόνο τα indices (χωρίς τις αποστάσεις)
        sorted_sites.append([i for _, i in cand])
        """
        Τώρα: sorted_sites[k][0] = πλησιέστερη εφικτή θέση για πελάτη k
              sorted_sites[k][1] = 2η πλησιέστερη, κλπ.
        """

    # ========================================================================
    # ΑΡΧΙΚΟΠΟΙΗΣΗ ΤΡΕΧΟΥΣΑΣ ΛΥΣΗΣ
    # ========================================================================
    # Υπολογισμός φορτίου και τύπων από την αρχική ανάθεση (Όπως στο decode_repair)

    # Υπολογισμός φορτίου ανά θέση
    load = {i: 0.0 for i in open_sites}  # Αρχικοποίηση
    for k in range(CL):
        load[int(assign[k])] += float(dem[k])

    # Επιλογή μικρότερου τύπου
    open_types = {}
    for i in open_sites:
        j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
        if j is None:
            return sol_init
        open_types[i] = j

    # Αξιολόγηση αρχικής λύσης
    cur_dist, _ = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

    # ========================================================================
    # ΒΟΗΘΗΤΙΚΕΣ ΣΥΝΑΡΤΗΣΕΙΣ ΓΙΑ ΥΠΟΛΟΓΙΣΜΟ ΚΟΣΤΟΥΣ
    # ========================================================================
    # Για επιτάχυνση, υπολογίζουμε τα components του κόστους ξεχωριστά και κάνουμε σταδιακές (incremental) ενημερώσεις αντί να καλούμε το _evaluate() κάθε φορά
    
    def install_cost(types_dict):
        """Υπολογίζει το installation cost από dict τύπων"""
        return sum(float(cost_types[int(j)]) for j in types_dict.values())

    def move_cost(assign_vec):
        """Υπολογίζει το transport cost από array ανάθεσης"""
        tot = 0.0
        for k in range(CL):
            i = int(assign_vec[k])  # Θέση που ανατέθηκε ο πελάτης k
            # Κόστος = απόσταση × cost_per_km για το μέσο του πελάτη
            tot += float(cl_sp[k][i]) * float(mode_cost[k])
        return tot

    # Υπολογισμός αρχικού κόστους
    cur_transport = move_cost(assign)        # Transport cost
    cur_install = install_cost(open_types)   # Installation cost
    cur_cost = cur_install + cur_transport   # Συνολικό κόστος


    # ============================================================================
    # ΚΥΡΙΟΣ ΒΡΟΧΟΣ VND: Επαναληπτική βελτίωση
    # ============================================================================
    # Επαναλαμβάνουμε μέχρι να μην βρεθεί βελτίωση ή να τελειώσουν οι επαναλήψεις

    it = 0            # Μετρητής επαναλήψεων (iterations)
    improved = True   # Flag: Βρέθηκε βελτίωση στην προηγούμενη iteration;

    while improved and it < max_iters:
        it += 1
        improved = False  # Υπόθεση: Δεν θα βρούμε βελτίωση σε αυτή την iteration

        # Εξετάζουμε τους πελάτες σε ΤΥΧΑΙΑ σειρά 
        client_order = list(range(CL))
        rng.shuffle(client_order)  # Τυχαία ανακάτωμα

        # Αναζήτηση βελτίωσης
        best_delta = 0.0     # Καλύτερη βελτίωση σε αυτή την iteration (αρχικά 0)
        best_move = None     # Καλύτερη κίνηση (None = καμία βρέθηκε)

        # ================================================================
        # ΕΞΕΤΑΣΗ ΚΑΘΕ ΠΕΛΑΤΗ
        # ================================================================
        for k in client_order:             # Για κάθε πελάτη (τυχαία σειρά)
            # ΤΡΕΧΟΥΣΑ ΚΑΤΑΣΤΑΣΗ ΠΕΛΑΤΗ k
            i_old = int(assign[k])         # Τρέχουσα θέση ανάθεσης
            d_old = float(cl_sp[k][i_old]) # Τρέχουσα απόσταση
            dk = float(dem[k])             # Ζήτηση πελάτη

            # Εξέταση εναλλακτικών θέσεων
            scanned = 0   # Μετρητής: Πόσες θέσεις εξετάσαμε
            # Σάρωση εφικτών θέσεων για πελάτη k (ήδη ταξινομημένες κατά απόσταση)
            for i_new in sorted_sites[k]: 
                # ================================================================
                # ΕΛΕΓΧΟΣ 1: Παράλειψη τρέχουσας θέσης
                # ================================================================
                if i_new == i_old:
                    continue   # Δεν μπορούμε να "μετακινηθούμε" στην ίδια θέση

                # ================================================================
                # ΕΛΕΓΧΟΣ 2: Η θέση είναι ανοιχτή;
                # ================================================================
                if i_new not in open_set:
                    continue   # Μπορούμε να αναθέσουμε μόνο σε ανοιχτές θέσεις

                # =================================================================
                # ΕΛΕΓΧΟΣ 3: Βελτιώνει την απόσταση;
                # =================================================================
                d_new = float(cl_sp[k][i_new])  # Απόσταση στη νέα θέση
                # Αν d_new ≥ d_old → ΧΕΙΡΟΤΕΡΗ ή ΙΔΙΑ απόσταση
                # Επειδή οι θέσεις είναι ταξινομημένες, ΟΙ ΕΠΟΜΕΝΕΣ θα είναι ακόμα χειρότερες
                if d_new >= d_old - 1e-12:  # Ανοχή για αριθμητικά σφάλματα
                    break                   # ΣΤΑΜΑΤΑ τη σάρωση (early termination)

                # ==================================================================
                # ΕΛΕΓΧΟΣ 4: Ορισμός scan limit
                # ==================================================================
                # Εξέτασε μόνο τις top-K πλησιέστερες θέσεις για επιτάχυνση
                scanned += 1
                if scanned > top_scan_per_client:
                    break

                # Υπολογισμός νέων φορτίων (Hypothetical)
                # Τι θα συμβεί αν μετακινήσουμε τον πελάτη k από i_old σε i_new;
                # Νέο φορτίο i_old: load[i_old] - demand[k]  (αφαιρείται ο πελάτης)
                load_old_new = load[i_old] - dk

                # Νέο φορτίο i_new: load[i_new] + demand[k]  (προστίθεται ο πελάτης)
                load_new_new = load[i_new] + dk

                # ΕΛΕΓΧΟΣ 5: Feasibility - Αρνητικό φορτίο;
                # Το νέο φορτίο του i_old δεν πρέπει να είναι αρνητικό
                # (Αυτό δεν πρέπει να συμβεί, αλλά ελέγχουμε για safety)
                if load_old_new < -1e-12:
                    continue

                # ================================================================
                # ΕΛΕΓΧΟΣ 6: Capacity Feasibility
                # =================================================================
                # Βρες τους νέους μικρότερους τύπους που χωράνε τα νέα φορτία
                j_old_new = _choose_min_type_for_load(i_old, load_old_new, facility_types, capacities, E)
                j_new_new = _choose_min_type_for_load(i_new, load_new_new, facility_types, capacities, E)
                # Αν κάποιο φορτίο δεν χωράει σε κανέναν τύπο → Infeasible
                if (j_old_new is None) or (j_new_new is None):
                    continue  # Παράλειψη αυτής της κίνησης

                # Υπολογισμός αλλαγής στο κόστος (Incremental)
                # INSTALLATION COST DELTA: Αλλαγή = (νέο κόστος i_old + νέο κόστος i_new) - (παλιό κόστος i_old + παλιό κόστος i_new)
                old_j_old = open_types[i_old]   # Τρέχων τύπος i_old
                old_j_new = open_types[i_new]   # Τρέχων τύπος i_new

                delta_install = float(cost_types[j_old_new]) - float(cost_types[old_j_old]) \
                              + float(cost_types[j_new_new]) - float(cost_types[old_j_new])

                # TRANSPORT COST DELTA: Αλλαγή = (d_new - d_old) × cost_per_km[mode[k]] (Μόνο ο πελάτης k επηρεάζεται)
                delta_transport = (d_new - d_old) * float(mode_cost[k])

                # ===================================================================
                # ΕΛΕΓΧΟΣ 7: Budget Constraint
                # ====================================================================
                # Νέο συνολικό κόστος = τρέχον + delta
                new_total_cost = (cur_install + delta_install) + (cur_transport + delta_transport)
                # Αν παραβιάζεται το budget → Infeasible
                if new_total_cost > float(Cmax) + 1e-9:
                    continue  # Παράλειψη αυτής της κίνησης
                
                # ====================================================================
                # ΕΛΕΓΧΟΣ 8: Καλύτερη βελτίωση;
                # ====================================================================
                # Distance delta (αρνητικό = βελτίωση)
                delta_dist = d_new - d_old

                # Αν αυτή η κίνηση είναι καλύτερη από την best_delta
                if delta_dist < best_delta - 1e-12:
                    # Ενημέρωση καλύτερης κίνησης
                    best_delta = delta_dist
                    best_move = (k, i_new, j_old_new, j_new_new, delta_install, delta_transport)
                    # Αν first_improvement = True
                    if first_improvement:
                        break   # ΣΤΑΜΑΤΑ τη σάρωση θέσεων για αυτόν τον πελάτη

            # Αν βρέθηκε βελτίωση και first_improvement=True → ΤΕΛΟΣ iteration
            if first_improvement and best_move is not None:
                break  # ΣΤΑΜΑΤΑ την εξέταση άλλων πελατών

        # ================================================================
        # ΤΕΛΟΣ ΕΞΕΤΑΣΗΣ ΠΕΛΑΤΩΝ
        # ================================================================

        # ΕΛΕΓΧΟΣ: Βρέθηκε βελτίωση;
        if best_move is None:
            # ΚΑΜΙΑ βελτίωση δεν βρέθηκε → LOCAL OPTIMUM
            break  # ΤΕΡΜΑΤΙΣΜΟΣ VND (έξοδος από while loop)

        # Εφαρμογή καλύτερης κίνησης
        # Unpack της best_move
        k, i_new, j_old_new, j_new_new, delta_install, delta_transport = best_move
        i_old = int(assign[k])   # Η τρέχουσα θέση πριν τη μετακίνηση
        dk = float(dem[k])       # Ζήτηση πελάτη

        # ===================================================
        # ΕΝΗΜΕΡΩΣΗ 1: Assignment
        # ===================================================
        assign[k] = int(i_new)  # Μετακίνηση πελάτη k από i_old → i_new

        # ====================================================
        # ΕΝΗΜΕΡΩΣΗ 2: Load
        # ====================================================
        load[i_old] -= dk  # Μείωση φορτίου i_old
        load[i_new] += dk  # Αύξηση φορτίου i_new

        # ==================================================
        # ΕΝΗΜΕΡΩΣΗ 3: Types
        # ===================================================
        open_types[i_old] = int(j_old_new)  # Νέος τύπος για i_old (μικρότερο φορτίο → μικρότερος τύπος)
        open_types[i_new] = int(j_new_new)  # Νέος τύπος για i_new (μεγαλύτερο φορτίο → μεγαλύτερος τύπος)
        
        # ====================================================
        # ΕΝΗΜΕΡΩΣΗ 4: Objective & Cost (Incremental)
        # ====================================================
        cur_dist += best_delta                      # Νέα συνολική απόσταση
        cur_install += float(delta_install)         # Νέο installation cost
        cur_transport += float(delta_transport)     # Νέο transport cost
        cur_cost = cur_install + cur_transport      # Νέο συνολικό κόστος
        
        # FLAG: Βελτίωση βρέθηκε → Συνέχισε στην επόμενη iteration
        improved = True
    
    # =====================================================================
    # ΤΕΛΟΣ ΚΥΡΙΟΥ ΒΡΟΧΟΥ VND
    # =====================================================================
    # Αιτίες τερματισμού:
    # 1. improved=False: Έφτασε σε local optimum (καμία βελτίωση)
    # 2. it >= max_iters: Έφτασε στο όριο iterations

    # =====================================================================
    # ΕΠΙΣΤΡΟΦΗ ΒΕΛΤΙΩΜΕΝΗΣ ΛΥΣΗΣ
    # =====================================================================
    return {
        "feasible": True,
        "distance_obj": float(cur_dist),
        "total_cost": float(cur_cost),
        "open_sites": list(open_sites),
        "open_types": dict(open_types),
        "assign": np.array(assign, dtype=int),
        "xvec": _build_xvec(open_types, assign, N, E, CL),
    }


# ============================================================================================================================
# ΜΕΘΕΥΡΕΤΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ Simulated Annealing & Iterated Local Search
# ============================================================================================================================
def meta_simulated_annealing_site_swap(sol_init, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
    transport_mode, combined_cost_per_km, iters=2000, T0=1.0, alpha=0.995, candidate_pool=200,seed=0, progress_every=0  # 0=off, else print every X evaluations
):
    """
    SIMULATED ANNEALING (SA): Στοχαστικό site‑swap με αποδοχή βασισμένη στη θερμοκρασία
    Escape από local optima (δέχεται χειρότερες λύσεις)
    Εμπνευσμένο από τη φυσική διαδικασία ανόπτησης μετάλλων (annealing):
    - Υψηλή θερμοκρασία → Περισσότερη κινητικότητα → Εξερεύνηση (Exploration)
    - Χαμηλή θερμοκρασία → Λιγότερη κινητικότητα → Εκμετάλλευση (Exploitation)
    
    ΣΤΡΑΤΗΓΙΚΗ:
    Σε κάθε iteration:
    1. PERTURBATION: Swap μίας θέσης (remove + add)
    2. DECODE: Δημιουργία πλήρους λύσης από νέο open_sites
    3. EVALUATION: Υπολογισμός Δobjective
    4. ACCEPTANCE: Αποδοχή με πιθανότητα που εξαρτάται από T
       - Αν βελτίωση (Δ ≤ 0) → Πάντα αποδοχή 
       - Αν επιδείνωση (Δ > 0) → Αποδοχή με πιθανότητα exp(-Δ/T)
    5. COOLING: Μείωση θερμοκρασίας T ← T × alpha
    
    Παράμετροι:
        sol_init: Αρχική λύση (από heuristics H1-H4)
        P, N, E, CL: Αριθμοί εγκαταστάσεων, θέσεων, τύπων, πελατών
        cl_sp_distances: Αποστάσεις [CL × N]
        max_distance, demand, facility_types, capacities, cost_types, Cmax, 
        transport_mode, combined_cost_per_km: (Κοινές παράμετροι)
        iters: Μέγιστες iterations 
        T0: Αρχική θερμοκρασία
            - Μεγαλύτερο → Περισσότερη exploration
            - Μικρότερο → Λιγότερη exploration
        alpha: Cooling rate
            - Κοντά στο 1 (π.χ. 0.999) → Αργή ψύξη, περισσότερη exploration
            - Μακριά από το 1 (π.χ. 0.95) → Γρήγορη ψύξη, λιγότερη exploration
        candidate_pool: Μέγεθος pool για επιλογή νέας θέσης 
            - Μικρότερο → Ταχύτερο αλλά λιγότερο diverse
            - Μεγαλύτερο → Αργότερο αλλά περισσότερο diverse
        seed: Random seed 
        progress_every: Εκτύπωση προόδου κάθε X evaluations (0=off)
    
    Επιστρέφει:
        dict: Η ΚΑΛΥΤΕΡΗ λύση που βρέθηκε σε όλες τις iterations
    """

    # Γεννήτρια τυχαίων αριθμών
    rng = random.Random(seed)

    # Εκκίνηση μόνο αν υπάρχει εφικτή αρχική λύση από τους ευρετικούς
    if (sol_init is None) or (not sol_init.get("feasible", False)):
        return _empty_sol()

    # Αρχικοποίηση τρέχουσας και καλύτερης λύσης
    cur = sol_init   # Τρέχουσα λύση (αλλάζει σε κάθε iteration)
    best = sol_init  # Καλύτερη λύση ever (ενημερώνεται μόνο με βελτίωση)

    # Εξαγωγή ανοιχτών θέσεων
    cur_sites = list(cur["open_sites"])  # List για random.choice
    cur_set = set(cur_sites)             # Set για γρήγορο έλεγχο

    # ========================================================================
    # ΠΡΟΫΠΟΛΟΓΙΣΜΟΣ: ΑΝΟΙΞΙΜΕΣ ΘΕΣΕΙΣ
    # ========================================================================
    # Δημιουργία του συνόλου ΟΛΩΝ των θέσεων που επιτρέπουν εγκαταστάσεις. Αυτό γίνεται μία φορά στην αρχή για επιτάχυνση
    # Το κάνουμε γιατί:
    #    - Σε κάθε iteration, χρειαζόμαστε το σύνολο των "κλειστών" θέσεων: closed = openable - open
    #    - Χωρίς προϋπολογισμό, θα ελέγχαμε ΟΛΕΣ τις N θέσεις σε κάθε iteration (αργό!)
    openable_set = set(
        i for i in range(N)
        if len(list(_allowed_types(i, facility_types, E))) > 0
    )

    # ========================================================================
    # ΑΡΧΙΚΟΠΟΙΗΣΗ TEMPERATURE & STATISTICS
    # ========================================================================
    """
    TEMPERATURE PARAMETERS:
    - T0 = 1.0: Αρχική θερμοκρασία
      → Όσο μεγαλύτερο, τόσο περισσότερη exploration (δέχεται χειρότερες)
    
    - alpha = 0.995: Cooling rate
      → Πόσο γρήγορα ψύχεται η θερμοκρασία
      → T(iter) = T0 × alpha^iter
    
    - iters = 2000: Μέγιστες iterations
      → Περισσότερες iterations = καλύτερη σύγκλιση αλλά πιο αργό
    
    TEMPERATURE SCHEDULE:
    T(iter) = T0 × alpha^iter
    """
 
    T = float(T0)    # Τρέχουσα θερμοκρασία (ξεκινά από T0)
    # Statistics για tracking
    evals = 0         # Πόσες feasible neighbor λύσεις αξιολογήθηκαν
    accepts = 0       # Πόσες λύσεις έγιναν αποδεκτές
    improvements = 0  # Πόσες φορές βελτιώθηκε το best

    # ========================================================================
    # ΚΥΡΙΟΣ ΒΡΟΧΟΣ: SIMULATED ANNEALING ITERATIONS
    # ========================================================================
    for _ in range(int(iters)):
        
        # ====================================================================
        # ΒΗΜΑ 1: ΔΗΜΙΟΥΡΓΙΑ ΓΕΙΤΟΝΙΚΗΣ ΛΥΣΗΣ (NEIGHBORHOOD MOVE)
        # ====================================================================
        # ΚΙΝΗΣΗ: Site swap (remove + add)
        # - Αφαίρεση: Μία τυχαία ανοιχτή θέση
        # - Προσθήκη: Μία τυχαία κλειστή θέση

        # Υπολογισμός κλειστών θέσεων
        # closed = openable θέσεις που δεν είναι στο cur_set. Χρήση set difference για ταχύτητα
        closed_set = openable_set - cur_set
        if not closed_set:
            break

        # Επιλογή θέσης προς αφαίρεση
        rem = rng.choice(cur_sites)  # Τυχαία ανοιχτή θέση προς κλείσιμο

        # Επιλογή θέσης προς προσθήκη
        # ΣΤΡΑΤΗΓΙΚΗ CANDIDATE POOL: Αν πολλές κλειστές θέσεις (>candidate_pool), δειγματοληψία για επιτάχυνση χωρίς απώλεια ποιότητας
        closed = list(closed_set)  # Μετατροπή σε λίστα ώστε να γίνει δειγματοληψία
        # Επιλογή pool: Αν λίγες κλειστές → Όλες,   αλλιώς → Sample
        pool = closed if len(closed) <= candidate_pool else rng.sample(closed, candidate_pool)
        # Τυχαία επιλογή από το pool
        add = rng.choice(pool)

        # Κατασκευή νέου open_sites
        # Αφαίρεση rem, προσθήκη add. List comprehension: [όλες εκτός rem] + [add]
        neigh_sites = [i for i in cur_sites if i != rem] + [add]
        # Sanitization: Αφαίρεση πιθανών duplicates (safety)
        neigh_sites = list(dict.fromkeys(neigh_sites))
        if len(neigh_sites) != P:
            continue
        
        # ====================================================================
        # ΒΗΜΑ 2: DECODE - Δημιουργία πλήρους λύσης από neigh_sites
        # ====================================================================
        # DECODE + REPAIR:
        # 1. Greedy assignment με max capacities
        # 2. Swap repair αν αποτυχία
        # 3. Minimal type selection
        # 4. Budget check
        neigh = decode_repair_from_open_sites(neigh_sites, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
            transport_mode, combined_cost_per_km)
        # ΕΛΕΓΧΟΣ: Το decode πέτυχε;
        # Αν όχι → Η κίνηση οδήγησε σε infeasible λύση
        if not neigh.get("feasible", False):
            continue  # Παράλειψη: Δοκίμασε άλλη κίνηση

        # ======================================================================
        # ΒΗΜΑ 3: COOLING - Μείωση θερμοκρασίας (ΜΟΝΟ μετά από feasible decode)
        # ======================================================================
        # Η θερμοκρασία μειώνεται μόνο μετά από ΕΠΙΤΥΧΗΜΕΝΗ αξιολόγηση, όχι σε κάθε iteration του for loop

        evals += 1    # Μετρητής επιτυχημένων αξιολογήσεων

        # ====================================================================
        # ΒΗΜΑ 4: EVALUATION - Υπολογισμός Διαφοράς objective
        # ====================================================================
        # METROPOLIS CRITERION:
        #   - Δ < 0: Βελτίωση (neighbor καλύτερη)
        #   - Δ > 0: Επιδείνωση (neighbor χειρότερη)
        #   - Δ = 0: Ίδια ποιότητα

        delta = float(neigh["distance_obj"]) - float(cur["distance_obj"])

        # ====================================================================
        # ΒΗΜΑ 5: ACCEPTANCE DECISION - Αποδοχή ή απόρριψη;
        # ====================================================================
        # ΚΡΙΤΗΡΙΟ ΑΠΟΔΟΧΗΣ (Metropolis):
        # - Δ ≤ 0:  Πάντα αποδοχή (βελτίωση ή ίδια ποιότητα)
        # - αλλίως: Αποδοχή με πιθανότητα P = exp(-Δ/T)
        # Υψηλή T → Μεγάλο exp(-Δ/T) → Πιθανότερη αποδοχή χειρότερης
        # Χαμηλή T → Μικρό exp(-Δ/T) → Σπάνια αποδοχή χειρότερης

        if (delta <= 0.0) or (rng.random() < math.exp(-delta / max(T, 1e-12))):
            # ΑΠΟΔΟΧΗ: Ενημέρωση τρέχουσας λύσης
            cur = neigh                          # O γείτονας γίνεται η νέα τρέχουσα
            cur_sites = list(cur["open_sites"])  # Ενημέρωση θέσεων
            cur_set = set(cur_sites)             # Ενημέρωση set (για closed_set)
            accepts += 1                         # Μετρητής αποδοχών

            # ΕΛΕΓΧΟΣ: Βελτίωση του best;
            # Το best ενημερώνεται μόνο αν η ΤΡΕΧΟΥΣΑ είναι ΚΑΛΥΤΕΡΗ από το best
            # Σημαντικό: Μπορεί να έχουμε αποδεχτεί χειρότερη από την cur, αλλά το best παραμένει η καλύτερη ever
            if float(cur["distance_obj"]) < float(best["distance_obj"]) - 1e-12:
                best = cur           # ΝΕΟ BEST!
                improvements += 1    # Μετρητής βελτιώσεων best

        # ====================================================================
        # ΒΗΜΑ 6: COOLING - Μείωση θερμοκρασίας
        # ====================================================================
        T *= alpha  # Μείωση θερμοκρασίας κατά παράγοντα alpha

        # ====================================================================
        # ΒΗΜΑ 7 (ΠΡΟΑΙΡΕΤΙΚΟ): PROGRESS TRACKING
        # ====================================================================
        # Εκτύπωση στατιστικών κάθε progress_every αξιολογήσεις. Χρήσιμο για debugging και παρακολούθηση σύγκλισης
        # evals: Πόσες feasible λύσεις αξιολογήθηκαν,    accepts: Πόσες έγιναν αποδεκτές (acceptance rate = accepts/evals)
        # best: Καλύτερη objective ever,                 T: Τρέχουσα θερμοκρασία
        if progress_every and (evals % int(progress_every) == 0):
            print(f"[SA] evals={evals} accepts={accepts} best={best['distance_obj']:.4f} T={T:.4g}")
    
    # Επιστροφή καλύτερης λύσης
    return best


