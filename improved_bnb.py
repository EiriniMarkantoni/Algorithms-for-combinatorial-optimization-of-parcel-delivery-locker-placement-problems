# Εκτέλεση ΟΛΩΝ των Ευρετικών και Επιλογή Καλύτερης
# ============================================================================================================================
def run_all_heuristics(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
    combined_cost_per_km, verbose=False):
    """
    Τρέχει και τους 4 ευρετικούς αλγορίθμους και επιστρέφει την ΚΑΛΥΤΕΡΗ feasible λύση (μικρότερη συνολική απόσταση)
    Αξιοποιούμε τα πλεονεκτήματα κάθε μεθόδου με αυτόν τον τρόπο (τα τρέχουμε όλα για να αξιοποιήσουμε τα δυνατά σημεία του καθενός)
    Χρησιμοποιούμε την καλύτερη λύση που θα προκύψει ως αρχική λύση για την εκκίνηση των μεθερευτικών αλγορίθμων
    
    Παράμετροι:
        (Ίδιες με τους ευρετικούς) +
        verbose: Αν True, εκτυπώνει πρόοδο και αποτελέσματα (προεπιλογή False)
    
    Επιστρέφει:
        dict: {
            "feasible": True/False,
            "distance_obj": float (objective value),
            "total_cost": float (≤ Cmax αν feasible),
            "open_sites": list[int],
            "open_types": dict {θέση → τύπος},
            "assign": array[CL],
            "xvec": array (full solution vector),
            "heuristic_used": str (ποια ευρετική έδωσε αυτή τη λύση)
        }
    """
    
    if verbose:
        print(f"\n[HEURISTICS] Running all 4 heuristics to find best solution...")
    
    # Λίστα για αποθήκευση όλων των feasible solutions
    feasible_solutions = []
    
    # ========================================================================
    # ΕΚΤΕΛΕΣΗ ΕΥΡΕΤΙΚΟΥ H1: Distance Greedy
    # ========================================================================
    if verbose:
        print(f"  [1/4] H1: Distance Greedy...", end=" ")
    
    sol_h1 = heuristic_distance_greedy(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, 
            transport_mode, combined_cost_per_km)
    
    # Έλεγχος feasibility και αποθήκευση
    if sol_h1["feasible"]:
        # Προσθήκη metadata: ποια ευρετική έδωσε αυτή τη λύση
        sol_h1["heuristic_used"] = "H1_distance_greedy"
        feasible_solutions.append(sol_h1)
        if verbose:
            print(f"feasible, obj={sol_h1['distance_obj']:.4f}")
    else:
        if verbose:
            print(f"infeasible")
    
    # ========================================================================
    # ΕΚΤΕΛΕΣΗ ΕΥΡΕΤΙΚΟΥ H2: Cost-Aware
    # ========================================================================
    if verbose:
        print(f"  [2/4] H2: Cost-Aware...", end=" ")
    
    sol_h2 = heuristic_cost_aware(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
             transport_mode, combined_cost_per_km)
    
    # Έλεγχος feasibility και αποθήκευση
    if sol_h2["feasible"]:
        # Προσθήκη metadata: ποια ευρετική έδωσε αυτή τη λύση
        sol_h2["heuristic_used"] = "H2_cost_aware"
        feasible_solutions.append(sol_h2)
        if verbose:
            print(f"feasible, obj={sol_h2['distance_obj']:.4f}")
    else:
        if verbose:
            print(f"infeasible")
    
    # ========================================================================
    # ΕΚΤΕΛΕΣΗ ΕΥΡΕΤΙΚOY H3: Feasibility-First
    # ========================================================================
    if verbose:
        print(f"  [3/4] H3: Feasibility-First...", end=" ")
    
    sol_h3 = heuristic_feasibility_first(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
             transport_mode, combined_cost_per_km)
    
    # Έλεγχος feasibility και αποθήκευση
    if sol_h3["feasible"]:
        # Προσθήκη metadata: ποια ευρετική έδωσε αυτή τη λύση
        sol_h3["heuristic_used"] = "H3_feasibility_first"
        feasible_solutions.append(sol_h3)
        if verbose:
            print(f"feasible, obj={sol_h3['distance_obj']:.4f}")
    else:
        if verbose:
            print(f"infeasible")
    
    # ========================================================================
    # ΕΚΤΕΛΕΣΗ ΕΥΡΕΤΙΚOY H4: GRASP Multistart
    # ========================================================================
    if verbose:
        print(f"  [4/4] H4: GRASP (60 iters)...", end=" ")
    
    sol_h4 = heuristic_grasp_multistart(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
             transport_mode, combined_cost_per_km, iters=60, rcl_size=max(30, 5*P), seed=123)
    
    # Έλεγχος feasibility και αποθήκευση
    if sol_h4["feasible"]:
        # Προσθήκη metadata: ποια ευρετική έδωσε αυτή τη λύση
        sol_h4["heuristic_used"] = "H4_grasp"
        feasible_solutions.append(sol_h4)
        if verbose:
            print(f"feasible, obj={sol_h4['distance_obj']:.4f}")
    else:
        if verbose:
            print(f"infeasible")
    
    # ========================================================================
    # ΕΠΙΛΟΓΗ ΚΑΛΥΤΕΡΗΣ ΛΥΣΗΣ
    # ========================================================================
    # ΣΕΝΑΡΙΟ 1: Κανένα feasible solution
    if not feasible_solutions:
        # Όλοι οι ευρετικοί απέτυχαν. Αυτό σημαίνει το instance είναι πολύ δύσκολο!
        if verbose:
            print(f"\n  ALL HEURISTICS FAILED! No feasible solution found.")
        
        # Επιστροφή empty solution (infeasible)
        return {
            "feasible": False,
            "distance_obj": np.inf,
            "total_cost": np.inf,
            "open_sites": None,
            "open_types": None,
            "assign": None,
            "xvec": None,
            "heuristic_used": "NONE"
        }
    
    # ΣΕΝΑΡΙΟ 2: Τουλάχιστον ένα feasible solution
    # Ταξινόμηση κατά distance objective (ΑΥΞΟΥΣΑ - καλύτερο πρώτο)
    feasible_solutions.sort(key=lambda s: s["distance_obj"])
    
    # Επιλογή της καλύτερης λύσης (πρώτη στη ταξινομημένη λίστα)
    best_solution = feasible_solutions[0]
    
    # LOGGING (αν verbose = True)
    if verbose:
        print(f"\n  BEST: {best_solution['heuristic_used']} with obj={best_solution['distance_obj']:.4f}")
        
        # Αν υπάρχουν πολλαπλά feasible, υπολογίζουμε το gap
        # ΕΡΜΗΝΕΙΑ GAP:
        #    Gap < 5%: Όλες οι ευρετικές βρίσκουν παρόμοιες λύσεις (εύκολο instance)
        #    Gap > 20%: Μεγάλη διασπορά (δύσκολο instance, importance of multistart)
    
        if len(feasible_solutions) > 1:
            worst_obj = feasible_solutions[-1]["distance_obj"]
            gap = (worst_obj - best_solution["distance_obj"]) / best_solution["distance_obj"] * 100
            print(f"     {len(feasible_solutions)}/4 heuristics succeeded")
            print(f"     Gap (worst-best): {gap:.2f}%")
    
    # Eπιστροφή της καλύτερης λύσης
    return best_solution


