def prune_infeasible_y_by_distance(ub, cl_sp_distances, max_distance, CL, N, E):
    """
    Προ-επεξεργασία που μηδενίζει τα άνω όρια των y μεταβλητών που παραβιάζουν
    τους περιορισμούς απόστασης. Αυτό γίνεται ΜΙΑ ΦΟΡΑ
    πριν ξεκινήσει το Branch and Bound, εξοικονομώντας σημαντικό υπολογιστικό χρόνο
    
    Για κάθε πελάτη k και θέση i:
    Αν distance(k, i) > max_distance[k] -> θέτει ub[y[k,i]] = 0
    
    Αυτό μειώνει τον χώρο αναζήτησης και επιταχύνει τη σύγκλιση, καθώς
    αποφεύγονται αναθέσεις που είναι εκ προοιμίου μη εφικτές
    
    Παράμετροι:
        ub: πίνακας άνω ορίων όλων των μεταβλητών
        cl_sp_distances: πίνακας αποστάσεων [CL x N] (πελάτης -> θέση)
        max_distance: μέγιστη επιτρεπόμενη απόσταση που μπορεί να διανύσει κάθε πελάτης ανάλογα με το μέσο μεταφοράς του [CL]
        CL: αριθμός πελατών
        N: αριθμός υποψήφιων θέσεων
        E: αριθμός τύπων εγκαταστάσεων
    """

    base = N * E                              # Οι y μεταβλητές ξεκινούν μετά τις x μεταβλητές
    for k in range(CL):
        row_base = base + k * N               # Αρχή γραμμής για πελάτη k
        maxd = max_distance[k]                # Μέγιστη απόσταση για αυτόν τον πελάτη
        for i in range(N):
            if cl_sp_distances[k][i] > maxd:  # Αν η απόσταση υπερβαίνει το όριο, μηδένισε το άνω όριο
                ub[row_base + i] = 0.0 


