# ΕΥΡΕΤΙΚOΣ ΑΛΓΟΡΙΘΜΟΣ H4: GRASP Multistart with Randomization
# ============================================================================================================================
def heuristic_grasp_multistart(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, combined_cost_per_km,
    iters=30, rcl_size=20, seed=0):
    """
    Η H4 τρέχει πολλές επαναλήψεις (multistart) με τυχαία επιλογή θέσεων από μια "Restricted Candidate List" (RCL)
    GRASP = Greedy Randomized Adaptive Search Procedure
      - Greedy: Χρησιμοποιεί attractiveness score
      - Randomized: Τυχαία επιλογή από top candidates (RCL)
      - Adaptive: Κάθε επανάληψη ανεξάρτητη
      - Search: Πολλές επαναλήψεις για διερεύνηση χώρου λύσεων

    ΣΤΡΑΤΗΓΙΚΗ:
      1. Υπολογίζει attractiveness score για κάθε θέση (βασισμένο σε proximity)
      2. Ταξινομεί θέσεις κατά attractiveness
      3. Για κάθε επανάληψη:
         a) Επιλέγει ΤΥΧΑΙΑ P θέσεις από το RCL (top θέσεις)
         b) Ανάθεση με swap repair
         c) Type selection
         d) Αξιολόγηση
     4. Κρατά το ΚΑΛΥΤΕΡΟ feasible solution από όλες τις επαναλήψεις

    ΔΙΑΦΟΡΑ ΑΠΟ H1/H2/H3:
    - Πολλαπλές εκκινήσεις (multistart) αντί για μία μόνο προσπάθεια
    - Τυχαιότητα ώστε κάθε εκτέλεση να δίνει διαφορετική αρχική λύση, αντί για πλήρως ντετερμινιστική διαδικασία
    - Επιλογή της καλύτερης λύσης από πολλές αντί για αποδοχή της πρώτης 

    ΠΛΕΟΝΕΚΤΗΜΑΤΑ: Κάθε επανάληψη παράγει διαφορετική λύση, εξερευνά μεγάλο μέρος του χώρου λύσεων, δεν εξαρτάται από μία greedy επιλογή
    ΜΕΙΟΝΕΚΤΗΜΑΤΑ: ΧΡΟΝΟΒΟΡΟ - Πολλές επαναλήψεις (συνήθως 30-100), ΤΥΧΑΙΟΤΗΤΑ - Αποτέλεσμα που εξαρτάται από seed

    Παράμετροι:
        (Κοινές με H1/H2/H3) +
        iters: Αριθμός GRASP iterations 
        rcl_size: Μέγεθος RCL - πόσες top θέσεις θεωρούνται 
        seed: Random seed για αναπαραγωγιμότητα
    
    Επιστρέφει:
        dict: Η ΚΑΛΥΤΕΡΗ λύση από όλες τις επαναλήψεις
    """

    # Γεννήτρια τυχαίων αριθμών με συγκεκριμένο seed (για αναπαραγωγιμότητα)
    rng = np.random.default_rng(seed)

    # ====================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ: Μετατροπή δεδομένων σε numpy arrays
    # ====================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    demand = np.asarray(demand, float)

    # ===============================================
    # ΥΠΟΛΟΓΙΣΜΟΣ ΕΦΙΚΤΟΤΗΤΑΣ ΑΠΟΣΤΑΣΕΩΝ
    # ===============================================
    # Δημιουργία boolean πίνακα: dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
    # Broadcasting: cl_sp[CL×N] <= maxd[CL×1] → [CL×N] boolean array
    dist_feasible = cl_sp <= maxd[:, None]

    # ========================================================================
    # ΠΡΟΥΠΟΛΟΓΙΣΜΟΣ: ATTRACTIVENESS RANKING (εκτελείται 1 φορά)
    # ========================================================================
    # Ταξινόμηση όλων των θέσεων κατά "attractiveness" για να δημιουργήσουμε το RCL
    # ΛΟΓΙΚΗ: Υψηλό score → Θέση κοντά σε ΠΟΛΛΟΥΣ πελάτες, Χαμηλό score → Θέση μακριά ή με λίγους πελάτες
    eps = 1e-6                                     # Μικρή σταθερά για αποφυγή διαίρεσης με μηδέν
    base_score = np.full(N, -np.inf, dtype=float)  # Αρχικοποίηση με -inf

    for i in range(N):
        # Βρες ποιοι πελάτες καλύπτονται από αυτή τη θέση
        cover = dist_feasible[:, i]  # Boolean mask [CL]
        # Αν κανένας πελάτης δεν καλύπτεται → παράλειψη (score = -inf)
        if np.any(cover):
            # Υπολογισμός attractiveness = Σ(1/distance) για covered πελάτες
            base_score[i] = float(np.sum(1.0 / (cl_sp[cover, i] + eps)))

    # ======================================================================
    # ΔΗΜΙΟΥΡΓΙΑ RCL: Ταξινόμηση θέσεων κατά attractiveness (φθίνουσα)
    # =====================================================================
    # αρνητικό πρόσημο → φθίνουσα σειρά (best first)
    cand_order = np.argsort(-base_score)  # Indices θέσεων, ταξινομημένες

    # ========================================================================
    # ΑΡΧΙΚΟΠΟΙΗΣΗ BEST SOLUTION
    # ========================================================================
    # Ξεκινά με infeasible solution - θα ενημερωθεί με την πρώτη feasible
    best = _empty_sol()

    # ========================================================================
    # ΚΥΡΙΟΣ ΒΡΟΧΟΣ: GRASP ITERATIONS
    # ========================================================================
    # Εκτελεί `iters` ανεξάρτητες επαναλήψεις κατασκευής λύσης
    for _ in range(int(iters)):  # Για κάθε GRASP iteration

        # ====================================================================
        # ΒΗΜΑ 1 : ΕΠΙΛΟΓΗ P ΘΕΣΕΩΝ
        # ====================================================================
        # Παίρνουμε τις top-rcl_size θέσεις από το pre-computed ranking
        top = cand_order[:min(int(rcl_size), N)]

        # RCL = Restricted Candidate List
        # Περιέχει τις "καλύτερες" θέσεις (υψηλό attractiveness)
        # Αλλά ΟΧΙ μόνο τη μία καλύτερη (αυτό θα ήταν pure greedy)

        chosen = set()    # Θέσεις που έχουν επιλεγεί
        open_sites = []   # Λίστα ανοιχτών θέσεων

        # Επαναλαμβάνουμε μέχρι να επιλέξουμε P έγκυρες θέσεις
        while len(open_sites) < P:
            # Τυχαία επιλογή μίας θέσης από το RCL
            i = int(rng.choice(top))
            # ΕΛΕΓΧΟΣ 1: Ήδη επιλεγμένη;
            if i in chosen:
                continue   # Προσπάθησε πάλι (επιλογή άλλης)
            # ΕΛΕΓΧΟΣ 2: Επιτρέπει τουλάχιστον έναν τύπο εγκατάστασης;
            if len(list(_allowed_types(i, facility_types, E))) == 0:
                continue   # Άκυρη θέση, προσπάθησε άλλη

            # ΕΠΙΤΥΧΙΑ: Η θέση είναι έγκυρη
            chosen.add(i)           # Σημείωσέ την ως επιλεγμένη
            open_sites.append(i)    # Πρόσθεσέ την στη λίστα

        # ====================================================================
        # ΒΗΜΑ 2: ΑΝΑΘΕΣΗ ΠΕΛΑΤΩΝ (assign)
        # ====================================================================
        # (ΙΔΙΑ ΛΟΓΙΚΗ ΜΕ H2/H3 - Swap repair)
        open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, demand, facility_types, capacities, E, P)
        if not ok:
            continue
        
        # ====================================================================
        # ΒΗΜΑ 3: ΕΠΙΛΟΓΗ ΕΛΑΧΙΣΤΟΥ ΤΥΠΟΥ (open_types)
        # ====================================================================
        # Υπολογισμός φορτίου ανά θέση. Τώρα που έχουμε αναθέσει τους πελάτες, υπολογίζουμε πόσο φορτίο (ζήτηση) έχει κάθε ανοιχτή θέση
        load = {i: 0.0 for i in open_sites}  # Αρχικοποίηση: μηδενικό φορτίο
        for k in range(CL):                  # Για κάθε πελάτη
            load[int(assign[k])] += float(demand[k])

        # Για κάθε θέση, επιλέγουμε τον ΜΙΚΡΟΤΕΡΟ τύπο που μπορεί να χειριστεί το φορτίο της. Αυτό ελαχιστοποιεί το installation cost
        open_types = {}
        ok_types = True  # Flag για capacity feasibility
        
        for i in open_sites:
            j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
            
            # Αν καμία εγκατάσταση δεν μπορεί να χειριστεί το φορτίο → ΑΠΟΤΥΧΙΑ
            if j is None:
                ok_types = False
                break  # Σταμάτα την επιλογή τύπων
            
            open_types[int(i)] = int(j)
        
        # Αν capacity infeasibility → συνέχισε στην επόμενη iteration
        if not ok_types:
            continue

        # ====================================================================
        # ΒΗΜΑ 4: ΕΛΕΓΧΟΣ ΠΡΟΥΠΟΛΟΓΙΣΜΟΥ (Cmax)
        # ====================================================================
        # Υπολογισμός των δύο μεγεθών: συνολική απόσταση (αντικειμενική συνάρτηση), total_cost (installation + transport cost)
        dist_obj, total_cost = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

        # Η λύση πρέπει να σεβαστεί το budget constraint: total_cost ≤ Cmax. Χρησιμοποιούμε μικρή ανοχή (1e-9) για αριθμητικά σφάλματα
        if total_cost > float(Cmax) + 1e-9:
            continue
        
        # ====================================================================
        # ΒΗΜΑ 5: ΕΝΗΜΕΡΩΣΗ BEST SOLUTION
        # ===============================================================
        # Αυτή η iteration παρήγαγε μια feasible λύση
        # Ελέγχουμε αν είναι καλύτερη από την τρέχουσα best
        # ΚΡΙΤΗΡΙΟ ΣΥΓΚΡΙΣΗΣ:
        # Αν best είναι infeasible → Οποιαδήποτε feasible είναι καλύτερη
        # Αν best είναι feasible → Σύγκριση distance objective (μικρότερο = καλύτερο)
        if (not best["feasible"]) or (dist_obj < best["distance_obj"] - 1e-9):
            best = {
                "feasible": True,
                "distance_obj": float(dist_obj),
                "total_cost": float(total_cost),
                "open_sites": list(open_sites),
                "open_types": open_types,
                "assign": assign,
                "xvec": _build_xvec(open_types, assign, N, E, CL),
            }

    return best

