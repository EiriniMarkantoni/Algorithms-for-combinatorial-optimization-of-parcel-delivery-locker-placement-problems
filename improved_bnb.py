# ΜΕΘΕΥΡΕΤΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ ILS (Iterated Local Search)
# ===================================================================================================================================
def meta_ils_perturb_vnd(sol_init, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
        combined_cost_per_km, outer_iters=50, perturb_swaps=2, vnd_iters=200, seed=0, patience=10, progress=False):
    """
    ILS (Iterated Local Search): Διατάραξη → Κύκλοι VND για διαφυγή από τοπικά βέλτιστα
    
    ΙΔΕΑ:
    Συνδυασμός PERTURBATION (διαταραχή) + LOCAL SEARCH (VND):
    - Perturbation: Διατάραξη της λύσης για escape από local optimum
    - Local Search: VND για βελτίωση της διαταραγμένης λύσης
    - Iteration: Επανάληψη του κύκλου
    
    ΣΤΡΑΤΗΓΙΚΗ:
    1. Ξεκινά από sol_init
    2. Για κάθε εξωτερική επανάληψη (outer iteration):
       a) PERTURB: Κάνε perturb_swaps τυχαία site swaps (διατάραξη της λύσης)
       b) DECODE: Δημιουργία πλήρους λύσης από τα διαταραγμένα sites
       c) VND: Local search με reassignment moves
       d) ACCEPT: Η βελτιωμένη λύση γίνεται η νέα τρέχουσα λύση
       e) UPDATE BEST: Αν καλύτερη από best → ενημέρωση
    3. EARLY STOPPING: Τερματισμός αν περάσουν patience επαναλήψεις χωρίς βελτίωση
    
    ΔΙΑΦΟΡΑ ΑΠΟ SA:
    - SA: Στοχαστική αποδοχή κινήσεων βάσει “θερμοκρασίας”
    - ILS: Ντετερμινιστική τοπική αναζήτηση + στοχευμένη διατάραξη (strategic perturbation)
    
    ΔΙΑΦΟΡΑ ΑΠΟ VND:
    - VND μόνο: Κολλάει σε local optimum
    - ILS: Ξεφεύγει μέσω perturbation → νέο local search
    
    EARLY STOPPING:
    - Αν patience=10 επαναλήψεις χωρίς βελτίωση → STOP
    - Αποφύγεται άσκοπη κατανάλωση χρόνου σε διαταραχές που δεν οδηγούν σε προόδους
    
    Παράμετροι:
        sol_init: Αρχική λύση (από heuristics H1-H4)
        P, N, E, CL, ...: (Κοινές παράμετροι)
        outer_iters: Αριθμός ILS iterations 
        perturb_swaps: Πόσα site swaps στην perturbation 
        vnd_iters: VND iterations ανά ILS iteration
        seed: Random seed
        patience: Early stopping - σταμάτα μετά από X iterations χωρίς βελτίωση 
        progress: Εκτύπωση προόδου 
    
    Επιστρέφει:
        dict: Καλύτερη λύση από όλες τις ILS επαναλήψεις
    """
    
    # Γεννήτρια τυχαίων αριθμών
    rng = random.Random(seed)

    # Εκκίνηση μόνο αν υπάρχει εφικτή αρχική λύση από τους ευρετικούς
    if (sol_init is None) or (not sol_init.get("feasible", False)):
        return _empty_sol()
    
    # Αρχικοποίηση τρέχουσας και καλύτερης λύσης
    best = sol_init
    cur = sol_init

    # ========================================================================
    # ΠΡΟΫΠΟΛΟΓΙΣΜΟΣ: ΑΝΟΙΞΙΜΕΣ ΘΕΣΕΙΣ (όπως SA)
    # ========================================================================
    openable_set = set(
        i for i in range(N)
        if len(list(_allowed_types(i, facility_types, E))) > 0
    )

    # ========================================================================
    # ΑΡΧΙΚΟΠΟΙΗΣΗ EARLY STOPPING
    # ========================================================================
    # Μετρητής: Πόσες iterations χωρίς βελτίωση του best
    no_improvement = 0

    # ========================================================================
    # ΚΥΡΙΟΣ ΕΞΩΤΕΡΙΚΟΣ ΒΡΟΧΟΣ ILS: 
    # =======================================================================
    for it in range(int(outer_iters)):

        # ====================================================================
        # ΒΗΜΑ 1: PERTURBATION - Διαταραχή της τρέχουσας λύσης
        # ====================================================================
        # Διατάραξη της τρέχουσας λύσης για escape από local optimum. Εκτελεί perturb_swaps διαδοχικές ανταλλαγές θέσεων (remove + add)
        # Αρχικοποίηση trial_sites
        cur_sites = list(cur["open_sites"])  # Θέσεις τρέχουσας λύσης
        trial_sites = cur_sites[:]           # Copy (θα τροποποιηθεί)
        trial_set = set(trial_sites)         # Set για γρήγορο έλεγχο
        
        # Flag: Πέτυχε η διαταραχή;
        perturb_success = True

        # Εκτέλεση swaps
        for _ in range(int(perturb_swaps)):
            # Επιλογή τυχαία θέσης (από trial_sites) προς αφαίρεση
            rem = rng.choice(trial_sites)
            # Υπολογισμός κλειστών θέσεων
            closed_set = openable_set - trial_set
            if not closed_set:
                perturb_success = False
                break
            # Επιλογή θέσης προς προσθήκη
            add = rng.choice(tuple(closed_set))  # Τυχαία κλειστή θέση

            # Εκτέλεση SWAP: Αφαίρεσε rem, Πρόσθεσε add
            trial_sites = [i for i in trial_sites if i != rem] + [add]
            # Sanitization: Αφαίρεση διπλότυπα
            trial_sites = list(dict.fromkeys(trial_sites))
            if len(trial_sites) != P:
                perturb_success = False
                break
            # Ενημέρωση set για επόμενο swap
            trial_set = set(trial_sites)

        # ====================================================================
        # ΒΗΜΑ 2: DECODE - Δημιουργία πλήρους λύσης από perturbed sites
        # ====================================================================
        # Χρήση decode_repair (όπως SA):
        # - Greedy assignment
        # - Swap repair αν χρειάζεται
        # - Minimal types
        # - Budget check
        trial = decode_repair_from_open_sites(trial_sites, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
            transport_mode, combined_cost_per_km)
        # ΕΛΕΓΧΟΣ: Feasible decode;
        if not trial.get("feasible", False):
            continue   # ΑΠΟΤΥΧΙΑ: Παράλειψη αυτής της επανάληψης

        # ====================================================================
        # ΒΗΜΑ 3: LOCAL SEARCH - VND για βελτίωση
        # ====================================================================
        # Βελτίωση της perturbed λύσης με reassignment moves
        # VND ΑΛΓΟΡΙΘΜΟΣ:
        # - Σάρωση πελατών σε τυχαία σειρά
        # - Για κάθε πελάτη, δοκιμή μετακίνησης σε πλησιέστερες θέσεις
        # - Αποδοχή βελτιώσεων (first improvement)
        # - Επανάληψη μέχρι local optimum
        trial2 = meta_vnd_assignments(trial, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
            combined_cost_per_km, max_iters=vnd_iters, seed=rng.randint(0, 10**9))

        # ΕΠΙΛΟΓΗ: Χρήση VND-improved ή fallback σε trial
        cur = trial2 if trial2.get("feasible", False) else trial

        # ====================================================================
        # ΒΗΜΑ 4: UPDATE BEST - Έλεγχος βελτίωσης
        # ====================================================================
        # Αν cur καλύτερη από best → ενημέρωση
        if float(cur["distance_obj"]) < float(best["distance_obj"]) - 1e-12:
            # ΒΕΛΤΙΩΣΗ BEST
            best = cur               # Ενημέρωση best
            no_improvement = 0       # Reset early stopping counter
        else:
            # ΚΑΜΙΑ ΒΕΛΤΙΩΣΗ 
            no_improvement += 1      # Αύξηση counter

        # ====================================================================
        # ΒΗΜΑ 5 (ΠΡΟΑΙΡΕΤΙΚΟ): PROGRESS TRACKING
        # ====================================================================
        # Εκτύπωση στατιστικών αν progress=True
        # it: Τρέχουσα ILS iteration,                  best: Καλύτερη objective ever
        # no_imp: Πόσες iterations χωρίς βελτίωση,     perturb_ok: Πέτυχε η perturbation;
        if progress:
            print(f"[ILS] it={it+1} best={best['distance_obj']:.4f} no_imp={no_improvement} perturb_ok={perturb_success}")

        # ====================================================================
        # ΒΗΜΑ 6: EARLY STOPPING - Έλεγχος patience
        # ====================================================================
        # Αν no_improvement ≥ patience → stop
        if no_improvement >= int(patience):
            break
    
    # Αιτίες τερματισμού:
    # 1. Τέλος outer_iters: Ολοκλήρωσε όλες τις iterations
    # 2. Early stopping: no_improvement ≥ patience

    # Επιστροφή καλύτερης λύσης
    return best


