def _allowed_types(i, facility_types, E):
    """
    Επιστρέφει τους επιτρεπόμενους τύπους εγκαταστάσεων για μια συγκεκριμένη θέση
    Παράμετροι:
        i: index της θέσης (0 έως N-1)
        facility_types: πίνακας με τον μέγιστο επιτρεπόμενο τύπο ανά θέση
        E: συνολικός αριθμός τύπων εγκαταστάσεων
    
    Επιστρέφει:
        range: range(0, max_allowed_type + 1)
        Παράδειγμα: αν facility_types[5] = 2, επιστρέφει range(0, 3) → [0, 1, 2]
    """
    return range(min(int(facility_types[i]), E-1) + 1)


def _build_xvec(open_types, assign, N, E, CL):
    """
    Κατασκευάζει το πλήρες διάνυσμα μεταβλητών απόφασης από μια λύση σε "compact" μορφή
    Παράμετροι:
        open_types: dict {θέση → τύπος} για τις ανοιχτές εγκαταστάσεις
        assign: array[CL] με την ανάθεση κάθε πελάτη σε θέση
        N: αριθμός υποψήφιων θέσεων
        E: αριθμός τύπων εγκαταστάσεων
        CL: αριθμός πελατών
    
    Επιστρέφει:
        numpy array: το πλήρες διάνυσμα μεταβλητών απόφασης (binary, 0/1)
    """
    # Συνολικός αριθμός μεταβλητών
    num_vars = N*E + CL*N + 1
    x = np.zeros(num_vars, dtype=float)

    # ΜΕΡΟΣ 1: Γέμισμα x[i,j] μεταβλητών (ανοίγματα εγκαταστάσεων)
    # Για κάθε ανοιχτή εγκατάσταση, θέτουμε το αντίστοιχο x[i,j] = 1
    for i, j in open_types.items():
        # Υπολογισμός flat index: θέση i, τύπος j → index = i×E + j
        x[int(i)*E + int(j)] = 1.0

    # ΜΕΡΟΣ 2: Γέμισμα y[k,i] μεταβλητών (αναθέσεις πελατών)
    # Οι y μεταβλητές ξεκινούν μετά τις x, στη θέση N×E
    base = N*E
    for k in range(CL):
        i = int(assign[k])  # Σε ποια θέση ανατέθηκε ο πελάτης k
        # Υπολογισμός flat index: πελάτης k, θέση i → index = base + k×N + i
        x[base + k*N + i] = 1.0

    # ΜΕΡΟΣ 3: Dummy μεταβλητή (τελευταία θέση, πάντα 0)
    x[-1] = 0.0
    return x


def _evaluate(open_types, assign, cl_sp_distances, transport_mode, combined_cost_per_km, cost_types):
    """
    Αξιολογεί μια λύση και υπολογίζει δύο μεγέθη: απόσταση (αντικειμενική συνάρτηση) και κόστος
    Παράμετροι:
        open_types: dict {θέση → τύπος εγκατάστασης}
        assign: array[CL] ανάθεση πελατών σε θέσεις
        cl_sp_distances: πίνακας αποστάσεων [CL × N]
        transport_mode: array[CL] μέσο μεταφοράς ανά πελάτη
        combined_cost_per_km: array κόστους/km ανά μέσο μεταφοράς (συνδυάζει CO₂ + λειτουργικό)
        cost_types: array κόστους εγκατάστασης ανά τύπο
    Επιστρέφει:
        tuple: (distance_obj, total_cost)
               - distance_obj: συνολική απόσταση (αντικειμενική συνάρτηση προς ελαχιστοποίηση)
               - total_cost: συνολικό κόστος (περιορισμός ≤ Cmax)
    """

    CL = len(assign)
    dist_obj = 0.0        # Συνολική απόσταση (αντικειμενική)
    transport_cost = 0.0  # Κόστος μεταφοράς
    # Υπολογισμός για κάθε πελάτη
    for k in range(CL):
        i = int(assign[k])               # Σε ποια θέση ανατέθηκε ο πελάτης k
        d = float(cl_sp_distances[k][i]) # Απόσταση από πελάτη k στη θέση i
        dist_obj += d                    # Προσθήκη στην αντικειμενική συνάρτηση (απόσταση)
        # Προσθήκη στο κόστος μεταφοράς (απόσταση × κόστος/km για το μέσο του πελάτη)
        transport_cost += d * float(combined_cost_per_km[int(transport_mode[k])]) 

    # Υπολογισμός κόστους εγκατάστασης (άθροισμα κόστους όλων των ανοιχτών εγκαταστάσεων)
    install_cost = 0.0
    for j in open_types.values():   # j = τύπος εγκατάστασης
        install_cost += float(cost_types[int(j)])

    # Συνολικό κόστος = κόστος εγκατάστασης + κόστος μεταφοράς
    total_cost = install_cost + transport_cost

    return dist_obj, total_cost


def _choose_min_type_for_load(i, load, facility_types, capacities, E):
    """
    Επιλέγει τον ελάχιστο (πιο οικονομικό) τύπο εγκατάστασης που μπορεί να εξυπηρετήσει το δοσμένο φορτίο (load) σε μια συγκεκριμένη θέση
    
    ΣΤΡΑΤΗΓΙΚΗ:
    -Επειδή οι μεγαλύτεροι τύποι εγκαταστάσεων κοστίζουν περισσότερο, θέλουμε να 
    επιλέξουμε τον μικρότερο τύπο που έχει επαρκή χωρητικότητα
    
    Παράμετροι:
        i: index της θέσης
        load: το συνολικό φορτίο (ζήτηση) που πρέπει να εξυπηρετηθεί σε αυτή τη θέση
        facility_types: πίνακας με τον μέγιστο επιτρεπόμενο τύπο ανά θέση
        capacities: πίνακας χωρητικότητας ανά τύπο εγκατάστασης
        E: συνολικός αριθμός τύπων
    
    Επιστρέφει:
        int: ο index του επιλεγμένου τύπου (0, 1, 2, ...) ή
        None: αν καμία εγκατάσταση δεν μπορεί να εξυπηρετήσει το φορτίο
    """
    # Σάρωση όλων των επιτρεπόμενων τύπων για αυτή τη θέση (από μικρότερο σε μεγαλύτερο - Small → Medium → Large)
    for j in _allowed_types(i, facility_types, E):
        # Έλεγχος αν αυτός ο τύπος έχει επαρκή χωρητικότητα
        if float(capacities[j]) >= load - 1e-9:
            return int(j)  # Επιστροφή του πρώτου (ελάχιστου) επαρκούς τύπου
        
    # Αν κανένας τύπος δεν επαρκεί, επιστροφή None (infeasible)
    return None


def _scarcity_order(dist_feasible, demand):
    """
    Ταξινομεί τους πελάτες κατά "σπανιότητα" (scarcity) - πόσο δύσκολο είναι να εξυπηρετηθούν
    
    ΟΡΙΣΜΟΣ ΣΠΑΝΙΟΤΗΤΑΣ:
    Ένας πελάτης είναι "σπάνιος" αν:
    1. Έχει ΛΙΓΕΣ εφικτές επιλογές θέσεων (λίγες θέσεις σε εφικτή απόσταση)
    2. Έχει ΜΕΓΑΛΗ ζήτηση (για tie-breaking)
    
    ΓΙΑΤΙ ΤΑΞΙΝΟΜΟΥΜΕ ΕΤΣΙ;
    Στην ανάθεση πελατών, οι "σπάνιοι" πελάτες πρέπει να εξυπηρετηθούν ΠΡΩΤΟΙ γιατί:
    - Έχουν λιγότερες εναλλακτικές → πιο πιθανό να μην βρεθεί λύση αν αφεθούν για τέλος
    - Αν εξαντληθεί η χωρητικότητα των λίγων εφικτών τους θέσεων, δεν έχουν fallback
    
    ΚΡΙΤΗΡΙΟ ΤΑΞΙΝΟΜΗΣΗΣ (αύξουσα σειρά):
    1ο: Αριθμός εφικτών θέσεων (options) - λιγότερες επιλογές = υψηλότερη προτεραιότητα
    2ο: Αρνητική ζήτηση (-demand) - μεγαλύτερη ζήτηση = υψηλότερη προτεραιότητα (tie-breaker)
    
    Παράμετροι:
        dist_feasible: boolean πίνακας [CL × N]
                      dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
        demand: array[CL] με τη ζήτηση κάθε πελάτη
    
    Επιστρέφει:
        list: Λίστα με τα indices των πελατών σε σειρά scarcity (πιο σπάνιοι πρώτοι)
    """
    # Υπολογισμός αριθμού εφικτών θέσεων ανά πελάτη
    # options[k] = πόσες θέσεις είναι σε εφικτή απόσταση για τον πελάτη k
    options = dist_feasible.sum(axis=1)
    # Μετατροπή demand σε numpy array
    demand = np.asarray(demand, float)

    # Ταξινόμηση πελατών με κλειδί (options, -demand)
    # Αν δύο στοιχεία έχουν ίδιο αριθμό εφικτών θέσεων τότε ταξινομούμε κατά μεγαλύτερη ζήτηση
    return sorted(range(dist_feasible.shape[0]), key=lambda k: (options[k], -demand[k]))


def _assign_greedy(open_sites, max_cap, dist_feasible, cl_sp_distances, demand):
    """
    Άπληστη (greedy) ανάθεση πελατών σε ανοιχτές εγκαταστάσεις με scarcity-first σειρά
    
    ΑΛΓΟΡΙΘΜΟΣ:
    1. Ταξινόμηση πελατών κατά σπανιότητα (scarcity_order) - οι δύσκολοι πρώτα
    2. Για κάθε πελάτη k (με αυτή τη σειρά):
       a) Βρες όλες τις εφικτές ανοιχτές θέσεις (σε εφικτή απόσταση)
       b) Ταξινόμησέ τις κατά αύξουσα απόσταση (πλησιέστερες πρώτες)
       c) Δοκίμασε να αναθέσεις τον πελάτη στην πλησιέστερη θέση με διαθέσιμη χωρητικότητα
       d) Μείωσε τη διαθέσιμη χωρητικότητα της θέσης
    3. Αν κάποιος πελάτης δεν μπορεί να εξυπηρετηθεί → ΑΠΟΤΥΧΙΑ
    
    Παράμετροι:
        open_sites: λίστα με τα indices των ανοιχτών θέσεων
        max_cap: array[N] με τη ΔΙΑΘΕΣΙΜΗ χωρητικότητα κάθε θέσης (τροποποιείται!)
        dist_feasible: boolean πίνακας [CL × N] για εφικτότητα αποστάσεων
        cl_sp_distances: πίνακας αποστάσεων [CL × N]
        demand: array[CL] με τη ζήτηση κάθε πελάτη
    
    Επιστρέφει:
        tuple: (assign, ok)
               - assign: array[CL] με την ανάθεση (assign[k] = θέση για πελάτη k)
                        ή None αν αποτυχία
               - ok: True αν επιτυχία, False αν κάποιος πελάτης δεν μπορεί να εξυπηρετηθεί
    """

    CL, N = dist_feasible.shape
    # Αρχικοποίηση: όλοι οι πελάτες μη ανατεθειμένοι (-1)
    assign = np.full(CL, -1, dtype=int)
    demand = np.asarray(demand, float)
    
    # ΒΗΜΑ 1: Ταξινόμηση πελατών κατά σπανιότητα (δύσκολοι πρώτα)
    order = _scarcity_order(dist_feasible, demand)

    # ΒΗΜΑ 2: Επεξεργασία κάθε πελάτη με τη σειρά σπανιότητας
    for k in order:
        # ΒΗΜΑ 2a: Εύρεση εφικτών ανοιχτών θέσεων για αυτόν τον πελάτη
        # Θέση i είναι υποψήφια αν: (1) είναι ανοιχτή ΚΑΙ (2) σε εφικτή απόσταση
        candidates = [i for i in open_sites if dist_feasible[k, i]]

        # Αν δεν υπάρχουν εφικτές θέσεις → Αποτυχία (πελάτης εκτός εμβέλειας)
        if not candidates:
            return None, False
        
        # ΒΗΜΑ 2b: Ταξινόμηση υποψηφίων θέσεων κατά απόσταση (πλησιέστερες πρώτες)
        # Αυτό ελαχιστοποιεί την αντικειμενική συνάρτηση (συνολική απόσταση)
        candidates.sort(key=lambda i: float(cl_sp_distances[k][i]))

        # ΒΗΜΑ 2c: Δοκιμή ανάθεσης στην πλησιέστερη θέση με επαρκή χωρητικότητα
        dk = float(demand[k])   # Ζήτηση αυτού του πελάτη
        placed = False          # Flag για επιτυχή τοποθέτηση

        for i in candidates:             # Δοκιμή θέσεων με αύξουσα απόσταση
            if max_cap[i] >= dk - 1e-9:  # Ανοχή για αριθμητικά σφάλματα
                # Επιτυχία: Ανάθεση πελάτη σε αυτή τη θέση
                assign[k] = int(i)
                max_cap[i] -= dk         # Μείωση διαθέσιμης χωρητικότητας
                placed = True
                break                    # Βρέθηκε λύση για αυτόν τον πελάτη → επόμενος πελάτης

        # Αν δεν βρέθηκε θέση με επαρκή χωρητικότητα → Αποτυχία
        if not placed:
            return None, False

    # Επιτυχία: Όλοι οι πελάτες ανατέθηκαν επιτυχώς
    return assign, True


def _greedy_open_sites_cover(P, dist_feasible, cl_sp_distances, facility_types, E, weights=None):
    """
    Άπληστη επιλογή P θέσεων που καλύπτουν όλους τους πελάτες με βάση weighted proximity
    
    ΑΛΓΟΡΙΘΜΟΣ GREEDY SET COVER:
    Σε κάθε επανάληψη:
    1. Υπολογίζει το "gain" κάθε υποψήφιας θέσης:
       gain[i] = Σ(k∈uncovered, feasible για i) weights[k] / (distance[k,i] + ε)
    2. Επιλέγει τη θέση με το μέγιστο gain
    3. Σημειώνει όλους τους νέο-καλυμμένους πελάτες ως covered
    4. Επαναλαμβάνει μέχρι να ανοίξει P θέσεις ή να καλυφθούν όλοι οι πελάτες
    
    GAIN METRIC:
    Το gain μιας θέσης i μετρά πόσο "πολύτιμους" πελάτες καλύπτει:
    - weights[k]: η σημαντικότητα του πελάτη (π.χ. demand, scarcity, cost)
    - 1/(distance+ε): προτιμά πιο κοντινούς πελάτες (μικρότερη απόσταση → μεγαλύτερο gain)
    - ε (epsilon): αποφυγή διαίρεσης με μηδέν για distance=0
    
    WEIGHTS:
    Διαφορετικά βάρη δίνουν διαφορετικές προτεραιότητες:
    - weights = demand: προτιμά θέσεις που καλύπτουν μεγάλη συνολική ζήτηση
    - weights = cost_per_km: προτιμά θέσεις που μειώνουν το κόστος μεταφοράς
    - weights = 1: ίση σημαντικότητα σε όλους (απλό coverage)
    
    Παράμετροι:
        P: αριθμός θέσεων προς άνοιγμα
        dist_feasible: boolean πίνακας [CL × N]
        cl_sp_distances: πίνακας αποστάσεων [CL × N]
        facility_types: επιτρεπόμενοι τύποι ανά θέση
        E: συνολικός αριθμός τύπων
        weights: array[CL] βάρη πελατών (None → ίσα βάρη)
    
    Επιστρέφει:
        tuple: (open_sites, uncovered)
               - open_sites: λίστα με τα indices των επιλεγμένων θέσεων (μήκος ≤ P)
               - uncovered: set με πελάτες που ΔΕΝ καλύπτονται από τις επιλεγμένες θέσεις
                           (ιδανικά κενό, αλλά μπορεί να έχει στοιχεία αν P πολύ μικρό)
    """
    CL, N = dist_feasible.shape
    # Αν δεν δοθούν βάρη, χρήση ίσων βαρών (όλοι οι πελάτες εξίσου σημαντικοί)
    if weights is None:
        weights = np.ones(CL, dtype=float)
    weights = np.asarray(weights, float)

    uncovered = set(range(CL)) # Σύνολο πελατών που δεν έχουν ακόμα καλυφθεί
    open_sites = []            # Λίστα επιλεγμένων θέσεων (αρχικά κενή)
    eps = 1e-6                 # Μικρή σταθερά για αποφυγή διαίρεσης με μηδέν

    # Προϋπολογισμός ποιες θέσεις μπορούν να "ανοίξουν" (έχουν επιτρεπόμενους τύπους)
    # openable[i] = True αν η θέση i μπορεί να φιλοξενήσει τουλάχιστον έναν τύπο
    openable = np.array([len(list(_allowed_types(i, facility_types, E))) > 0 for i in range(N)], dtype=bool)

    # ΒΡΟΓΧΟΣ GREEDY: Επιλογή P θέσεων
    for _ in range(P):
        best_i = None     # Καλύτερη θέση σε αυτή την επανάληψη
        best_gain = -1.0  # Καλύτερο gain (αρχικά πολύ μικρό)

        # ΑΝΑΖΗΤΗΣΗ: Εξέταση όλων των υποψήφιων θέσεων
        for i in range(N):
            # Παράλειψη θέσεων που:
            # (1) δεν μπορούν να ανοίξουν ή (2) έχουν ήδη επιλεγεί
            if (not openable[i]) or (i in open_sites):
                continue

            # ΥΠΟΛΟΓΙΣΜΟΣ GAIN: Άθροισμα συνεισφοράς από uncovered πελάτες
            gain = 0.0
            for k in uncovered:         # Μόνο μη-καλυμμένοι πελάτες συνεισφέρουν
                if dist_feasible[k, i]: # Αν ο πελάτης k μπορεί να εξυπηρετηθεί από θέση i
                    # Συνεισφορά = weight / distance
                    # Μεγαλύτερο weight ή μικρότερη απόσταση → μεγαλύτερη συνεισφορά
                    gain += float(weights[k]) / (float(cl_sp_distances[k][i]) + eps)

            # Ενημέρωση καλύτερης θέσης αν βρέθηκε μεγαλύτερο gain
            if gain > best_gain:
                best_gain = gain
                best_i = i

        # ΤΕΡΜΑΤΙΣΜΟΣ: Αν δεν βρέθηκε καμία υποψήφια θέση, σταμάτα πρόωρα
        if best_i is None:
            break

        # ΕΠΙΛΟΓΗ: Πρόσθεσε την καλύτερη θέση στη λίστα
        open_sites.append(int(best_i))

        # ΕΝΗΜΕΡΩΣΗ UNCOVERED: Σημείωσε τους νέους καλυμμένους πελάτες
        # newly = πελάτες που μόλις καλύφθηκαν από αυτή τη θέση
        newly = [k for k in uncovered if dist_feasible[k, best_i]]
        for k in newly:
            uncovered.discard(k)  # Αφαίρεση από το σύνολο uncovered

    return open_sites, uncovered


def _swap_repair(open_sites, dist_feasible, cl_sp_distances, demand, facility_types, capacities, E, P, max_tries=200):
    """
    Αποκατάσταση εφικτότητας μέσω ανταλλαγής θέσεων όταν η άπληστη ανάθεση αποτυγχάνει
    
    ΣΤΡΑΤΗΓΙΚΗ SWAP-REPAIR:
    Δοκιμάζει να αντικαταστήσει μια "κακή" ανοιχτή θέση με μια "καλύτερη" κλειστή θέση,
    επαναλαμβάνοντας μέχρι να επιτύχει εφικτή ανάθεση ή να εξαντληθούν οι προσπάθειες
    ΑΛΓΟΡΙΘΜΟΣ (σε κάθε επανάληψη):
    1. ΔΟΚΙΜΗ ΑΝΑΘΕΣΗΣ:
       - Υπολόγισε max χωρητικότητες για τις τρέχουσες ανοιχτές θέσεις
       - Δοκίμασε greedy assignment με scarcity-first
       - Αν επιτυχία → ΤΕΛΟΣ (εφικτή λύση)
    
    2. ΕΝΤΟΠΙΣΜΟΣ "ΔΥΣΚΟΛΟΥ" ΠΕΛΑΤΗ:
       - Βρες τον πιο σπάνιο πελάτη που ΔΕΝ καλύπτεται από τις ανοιχτές θέσεις
       - Ή τον πρώτο στη scarcity order (fallback)
    
    3. SWAP EXPLORATION:
       a) Βρες υποψήφιες θέσεις που μπορούν να εξυπηρετήσουν τον hard client
       b) Ταξινόμησέ τις κατά απόσταση (πλησιέστερες πρώτες)
       c) Για κάθε υποψήφια νέα θέση (top 30):
          - Αν ήδη ανοιχτή, παράλειψε
          - Βρες τις "χειρότερες" ανοιχτές θέσεις (worst_score)
          - Δοκίμασε να SWAP καθεμία (top 10) με τη νέα θέση
          - Έλεγχος coverage: όλοι οι πελάτες πρέπει να καλύπτονται
          - Αν βρεθεί εφικτό swap → ΕΦΑΡΜΟΓΗ και επόμενη επανάληψη
    
    4. ΤΕΡΜΑΤΙΣΜΟΣ:
       - Αν δεν βρέθηκε κανένα εφικτό swap → ΑΠΟΤΥΧΙΑ
       - Αν φτάσαμε max_tries → ΑΠΟΤΥΧΙΑ
    
    WORST_SCORE METRIC:
    Για να επιλέξουμε ποια θέση να αφαιρέσουμε, βαθμολογούμε κάθε θέση με:
    - 1ο κριτήριο: αριθμός πελατών που καλύπτει (λιγότεροι = χειρότερη)
    - 2ο κριτήριο: μέση απόσταση στους καλυμμένους πελάτες (μεγαλύτερη = χειρότερη)
    
    Θέσεις με λίγους πελάτες ή μεγάλες αποστάσεις είναι υποψήφιες για αφαίρεση
    
    Παράμετροι:
        open_sites: τρέχουσες ανοιχτές θέσεις (μπορεί μη εφικτές)
        dist_feasible: boolean πίνακας εφικτότητας αποστάσεων
        cl_sp_distances: πίνακας αποστάσεων
        demand: ζήτηση πελατών
        facility_types: επιτρεπόμενοι τύποι ανά θέση
        capacities: χωρητικότητες τύπων
        E: αριθμός τύπων
        P: επιθυμητός αριθμός ανοιχτών θέσεων
        max_tries: μέγιστες επαναλήψεις swap (προεπιλογή 200)
    
    Επιστρέφει:
        tuple: (open_sites, assign, ok)
               - open_sites: τροποποιημένες ανοιχτές θέσεις (εφικτές) ή None
               - assign: array ανάθεσης πελατών ή None
               - ok: True αν επιτυχία, False αν αποτυχία
    """

    CL, N = dist_feasible.shape
    open_sites = list(open_sites)   # Μετατροπή σε λίστα για ευκολία τροποποίησης
    demand = np.asarray(demand, float)

    # Σειρά scarcity: δύσκολοι πελάτες πρώτα
    order = _scarcity_order(dist_feasible, demand)

    # ΚΥΡΙΟΣ ΒΡΟΧΟΣ: Μέχρι max_tries προσπάθειες
    for _ in range(max_tries):
        # ============================================================
        # ΒΗΜΑ 1: ΔΟΚΙΜΗ ΑΝΑΘΕΣΗΣ ΜΕ ΤΡΕΧΟΥΣΕΣ ΘΕΣΕΙΣ
        # ============================================================
        # Υπολογισμός μέγιστων χωρητικοτήτων για κάθε ανοιχτή θέση
        # (χρησιμοποιώντας τον μεγαλύτερο επιτρεπόμενο τύπο)
        max_cap = np.zeros(N, dtype=float)
        for i in open_sites:
            jmax = min(int(facility_types[i]), E-1)  # Μέγιστος επιτρεπόμενος τύπος
            max_cap[i] = float(capacities[jmax])

        # Δοκιμή greedy assignment
        assign, ok = _assign_greedy(open_sites, max_cap, dist_feasible, cl_sp_distances, demand)
        # Αν επιτυχία → ΤΕΛΟΣ
        if ok:
            return open_sites, assign, True

        # ============================================================
        # ΒΗΜΑ 2: ΕΝΤΟΠΙΣΜΟΣ "ΔΥΣΚΟΛΟΥ" ΠΕΛΑΤΗ
        # ============================================================
        # Βρες τον πιο σπάνιο πελάτη που ΔΕΝ καλύπτεται από τις ανοιχτές θέσεις
        hard_k = None
        for k in order:  # Σάρωση με σειρά scarcity
            # Έλεγχος αν ο πελάτης k δεν καλύπτεται από καμία ανοιχτή θέση
            if not any(dist_feasible[k, i] for i in open_sites):
                hard_k = k
                break

        # Fallback: Αν όλοι καλύπτονται, επίλεξε τον πιο σπάνιο (πρώτος στο order)  
        if hard_k is None:
            hard_k = order[0]

        # ============================================================
        # ΒΗΜΑ 3: SWAP EXPLORATION
        # ============================================================
        # Βρες θέσεις που μπορούν να εξυπηρετήσουν τον δύσκολο πελάτη
        cand_sites = [i for i in range(N) if dist_feasible[hard_k, i]]
        # Αν δεν υπάρχουν → πρόβλημα είναι infeasible (πελάτης εκτός εμβέλειας)
        if not cand_sites:
            return None, None, False
        # Ταξινόμηση κατά απόσταση (πλησιέστερες πρώτες - καλύτερες για objective)
        cand_sites.sort(key=lambda i: float(cl_sp_distances[hard_k][i]))
        swapped = False # Flag για επιτυχή swap

        # Δοκιμή των top 30 πλησιέστερων υποψήφιων
        for new_i in cand_sites[:min(30, len(cand_sites))]:
             # Παράλειψη αν ήδη ανοιχτή
            if new_i in open_sites:
                continue

            # --------------------------------------------------------------
            # WORST_SCORE: Βαθμολόγηση υπαρχουσών θέσεων (χειρότερες πρώτες)
            # --------------------------------------------------------------
            def worst_score(r):
                """
                Επιστρέφει tuple (cover_count, -mean_distance) για θέση r
                Python's sort ταξινομεί αύξουσα, άρα:
                - Λιγότεροι πελάτες (μικρό cover) → πρώτοι (χειρότεροι)
                - Μεγαλύτερη μέση απόσταση (αρνητική) → πρώτοι (χειρότεροι)
                """
                cover = int(dist_feasible[:, r].sum())  # Πόσοι πελάτες καλύπτονται
                if cover == 0:
                    return (0, 0.0)                     # Καμία κάλυψη = χειρότερη θέση
                # Μέση απόσταση στους καλυμμένους πελάτες (με αρνητικό πρόσημο)
                return (cover, -float(np.mean(cl_sp_distances[dist_feasible[:, r], r])))

            # Ταξινόμηση ανοιχτών θέσεων: χειρότερες πρώτες
            remove_candidates = sorted(open_sites, key=worst_score)  

            # Δοκιμή swap με τις top 10 χειρότερες
            for rem in remove_candidates[:min(10, len(remove_candidates))]:
                # Δημιουργία trial set: αφαίρεση rem, προσθήκη new_i
                trial = [i for i in open_sites if i != rem] + [new_i]

                # Αφαίρεση duplicates διατηρώντας σειρά (dict.fromkeys trick)
                trial = list(dict.fromkeys(trial))

                # Έλεγχος μεγέθους: πρέπει να έχουμε ακριβώς P θέσεις
                if len(trial) != P:
                    continue

                # --------------------------------------------------------
                # COVERAGE CHECK: Όλοι οι πελάτες πρέπει να καλύπτονται
                # --------------------------------------------------------
                ok_cover = True
                for kk in range(CL):
                    # Έλεγχος αν ο πελάτης kk καλύπτεται από τουλάχιστον μία trial θέση
                    if not any(dist_feasible[kk, i] for i in trial):
                        ok_cover = False  # Uncovered πελάτης → κακό trial
                        break

                # Αν δεν καλύπτονται όλοι, δοκίμασε άλλο swap
                if not ok_cover:
                    continue

                # ΕΠΙΤΥΧΙΑ: Εφικτό swap βρέθηκε
                open_sites = [int(t) for t in trial]
                swapped = True
                break   # Έξοδος από τον remove loop

            # Αν βρέθηκε swap, έξοδος από τον new_i loop για να δοκιμάσουμε assignment
            if swapped:
                break

        # ============================================================
        # ΒΗΜΑ 4: ΕΛΕΓΧΟΣ ΠΡΟΟΔΟΥ
        # ============================================================
        # Αν δεν βρέθηκε κανένα εφικτό swap → αδιέξοδο, τερματισμός
        if not swapped:
            break

    # Αν βγήκαμε από τον βρόχο χωρίς επιτυχία → ΑΠΟΤΥΧΙΑ
    return None, None, False


def _empty_sol():
    """
    Δημιουργεί και επιστρέφει ένα "κενό" (infeasible) dictionary λύσης
    
    ΧΡΗΣΗ:
    Χρησιμοποιείται ως ΕΠΙΣΤΡΟΦΗ όταν μια ευρετική ή μεθευρετική μέθοδος
    αποτυγχάνει να βρει εφικτή λύση

    Επιστρέφει:
        dict: Infeasible λύση με όλα τα πεδία None εκτός από flags
    """

    return {
        "feasible": False,
        "distance_obj": np.inf,
        "total_cost": np.inf,
        "open_sites": None,
        "open_types": None,
        "assign": None,
        "xvec": None,
    }


# ====================================================================================================================
# ΕΥΡΕΤΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ H1: Distance Greedy Cover + Scarcity-first Assignment
# ====================================================================================================================
def heuristic_distance_greedy(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, combined_cost_per_km):
    """
    Ευρετικός αλγόριθμος Distance Greedy για κατασκευή αρχικής λύσης
    Επιλέγει θέσεις που είναι κοντά στους πελάτες και χρησιμοποιεί έξυπνη στρατηγική ανάθεσης

    ΣΤΡΑΤΗΓΙΚΗ ΕΠΙΛΟΓΗΣ ΘΕΣΕΩΝ:
    Greedy Set Cover με βάρη demand → προτιμά θέσεις που:
       1. Καλύπτουν πολλούς πελάτες (high coverage)
       2. Είναι κοντά σε πελάτες με μεγάλη ζήτηση (weighted proximity)
       3. Βοηθούν στην capacity feasibility (demand weights)

    ΣΤΡΑΤΗΓΙΚΗ ΑΝΑΘΕΣΗΣ:
    Scarcity-first → εξυπηρετεί πρώτα τους "δύσκολους" πελάτες (λίγες επιλογές)

    ΡΟΗ ΑΛΓΟΡΙΘΜΟΥ (ΚΟΙΝΗ ΔΟΜΗ 5 ΒΗΜΑΤΩΝ):
      1. Επιλογή P θέσεων (open_sites) - με demand weights
      2. Ανάθεση πελατών (assign) - greedy με scarcity-first, fallback σε swap repair
      3. Επιλογή ελάχιστου τύπου ανά θέση (open_types) - minimal type για οικονομία
      4. Έλεγχος προϋπολογισμού (Cmax) - total_cost ≤ Cmax
      5. Επιστροφή λύσης - πλήρες dictionary ή empty_sol()
  
    ΠΑΡΑΜΕΤΡΟΙ:
        P: Αριθμός εγκαταστάσεων προς άνοιγμα
        N: Συνολικός αριθμός υποψήφιων θέσεων
        E: Αριθμός διαθέσιμων τύπων εγκαταστάσεων (Small=0, Medium=1, Large=2)
        CL: Αριθμός πελατών
        cl_sp_distances: Πίνακας αποστάσεων [CL × N] (πελάτης k → θέση i)
        max_distance: Array[CL] μέγιστη επιτρεπόμενη απόσταση ανά πελάτη
        demand: Array[CL] ζήτηση κάθε πελάτη
        facility_types: Array[N] μέγιστος επιτρεπόμενος τύπος ανά θέση
        capacities: Array[E] χωρητικότητα ανά τύπο εγκατάστασης
        cost_types: Array[E] κόστος εγκατάστασης ανά τύπο
        Cmax: Μέγιστος προϋπολογισμός (installation + transport cost ≤ Cmax)
        transport_mode: Array[CL] μέσο μεταφοράς ανά πελάτη (0-4)
        combined_cost_per_km: Array[5] κόστος/km ανά μέσο (operational + CO₂)
    
    ΕΠΙΣΤΡΟΦΗ:
        dict: {
            "feasible": True/False,
            "distance_obj": συνολική απόσταση,
            "total_cost": συνολικό κόστος,
            "open_sites": λίστα ανοιχτών θέσεων,
            "open_types": dict {θέση → τύπος},
            "assign": array ανάθεσης πελατών,
            "xvec": πλήρες διάνυσμα λύσης
        }
    """
    
    # ====================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ: Μετατροπή δεδομένων σε numpy arrays
    # ====================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    demand = np.asarray(demand, float)

    # ===============================================
    # ΥΠΟΛΟΓΙΣΜΟΣ ΕΦΙΚΤΟΤΗΤΑΣ ΑΠΟΣΤΑΣΕΩΝ
    # ===============================================
    # Δημιουργία boolean πίνακα: dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
    # Broadcasting: cl_sp[CL×N] <= maxd[CL×1] → [CL×N] boolean array
    dist_feasible = cl_sp <= maxd[:, None]

    # ==========================================================
    # ΒΗΜΑ 1: ΕΠΙΛΟΓΗ P ΘΕΣΕΩΝ - Greedy Cover με Demand Weights
    # ==========================================================
    # Επιλέγει επαναληπτικά P θέσεις που μεγιστοποιούν το "gain". Προτιμά θέσεις κοντά σε πελάτες με μεγάλη ζήτηση  
    open_sites, uncovered = _greedy_open_sites_cover(P, dist_feasible, cl_sp, facility_types, E, weights=demand)
    if len(open_sites) != P: # Αν δεν μπορούν να βρεθούν P θέσεις να ανοίξουν επιστρέφεται ΑΠΟΤΥΧΙΑ
        return _empty_sol()

    # ===========================================================
    # ΒΗΜΑ 2: ΑΝΑΘΕΣΗ ΠΕΛΑΤΩΝ ΣΕ ΘΕΣΕΙΣ (assign)
    # ===========================================================
    # Δύο σενάρια:
    # A) Υπάρχουν uncovered πελάτες → Άμεσο swap repair
    # B) Όλοι οι πελάτες καλύπτονται → Δοκιμή greedy assignment, fallback σε swap
    if uncovered:
        # =====================================================================
        # ΣΕΝΑΡΙΟ A: ΥΠΑΡΧΟΥΝ UNCOVERED ΠΕΛΑΤΕΣ
        # =====================================================================
        # Αν κάποιοι πελάτες δεν καλύπτονται από τις επιλεγμένες θέσεις, αντικαθιστά "κακές" θέσεις με "καλύτερες" που καλύπτουν τους uncovered
        open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, demand, facility_types, capacities, E, P)
        if not ok:
            return _empty_sol()
    else:
        # =====================================================================
        # ΣΕΝΑΡΙΟ B: ΟΛΟΙ ΟΙ ΠΕΛΑΤΕΣ ΚΑΛΥΠΤΟΝΤΑΙ
        # =====================================================================
        # Όλοι οι πελάτες είναι σε εφικτή απόσταση από τουλάχιστον μία επιλεγμένη θέση. Τώρα πρέπει να τους αναθέσουμε συγκεκριμένα, λαμβάνοντας υπόψη capacity
        # Υπολογισμός μέγιστων διαθέσιμων χωρητικοτήτων. Για κάθε ανοιχτή θέση, υποθέτουμε τον μέγιστο επιτρεπόμενο τύπο
        # (για να δώσουμε τη μέγιστη ελευθερία στο greedy assignment)
        max_cap = np.zeros(N, dtype=float)
        for i in open_sites:
            jmax = min(int(facility_types[i]), E-1)
            max_cap[i] = float(capacities[jmax])
        # Χρησιμοποιεί scarcity-first σειρά: δύσκολοι πελάτες πρώτα. Για κάθε πελάτη, τον ανατίθει στην πλησιέστερη θέση με επαρκή χωρητικότητα
        assign, ok = _assign_greedy(open_sites, max_cap, dist_feasible, cl_sp, demand)
        if not ok:
            open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, demand, facility_types, capacities, E, P)
            if not ok:
                return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 3: ΕΠΙΛΟΓΗ ΕΛΑΧΙΣΤΟΥ ΤΥΠΟΥ ΕΓΚΑΤΑΣΤΑΣΗΣ ΑΝΑ ΘΕΣΗ (open_types)
    # ========================================================================
    # Υπολογισμός φορτίου ανά θέση. Τώρα που έχουμε αναθέσει τους πελάτες, υπολογίζουμε πόσο φορτίο (ζήτηση) έχει κάθε ανοιχτή θέση
    load = {i: 0.0 for i in open_sites} # Αρχικοποίηση: μηδενικό φορτίο
    for k in range(CL):                 # Για κάθε πελάτη
        load[int(assign[k])] += float(demand[k])

    
    # Για κάθε θέση, επιλέγουμε τον ΜΙΚΡΟΤΕΡΟ τύπο που μπορεί να χειριστεί το φορτίο της. Αυτό ελαχιστοποιεί το installation cost
    open_types = {}      # Dictionary: θέση → τύπος εγκατάστασης
    for i in open_sites: # Για κάθε ανοιχτή θέση
        # Βρες τον ελάχιστο τύπο που μπορεί να εξυπηρετήσει το φορτίο
        j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
        if j is None:
            return _empty_sol()
        open_types[int(i)] = int(j)

    # ========================================================================
    # ΒΗΜΑ 4: ΕΛΕΓΧΟΣ ΠΡΟΥΠΟΛΟΓΙΣΜΟΥ (Cmax)
    # ========================================================================
    # Υπολογισμός των δύο μεγεθών: συνολική απόσταση (αντικειμενική συνάρτηση), total_cost (installation + transport cost)
    dist_obj, total_cost = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

    # Η λύση πρέπει να σεβαστεί το budget constraint: total_cost ≤ Cmax. Χρησιμοποιούμε μικρή ανοχή (1e-9) για αριθμητικά σφάλματα
    if total_cost > float(Cmax) + 1e-9:
        return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 5: ΔΗΜΙΟΥΡΓΙΑ ΠΛΗΡΟΥΣ ΔΙΑΝΥΣΜΑΤΟΣ ΛΥΣΗΣ
    # ========================================================================
    xvec = _build_xvec(open_types, assign, N, E, CL)
    return {
        "feasible": True,
        "distance_obj": float(dist_obj),
        "total_cost": float(total_cost),
        "open_sites": list(open_sites),
        "open_types": open_types,
        "assign": assign,
        "xvec": xvec,
    }


# ============================================================================================================================
# ΕΥΡΕΤΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ Η2: Cost-Aware Greedy (Budget-Friendly)
# ============================================================================================================================
def heuristic_cost_aware(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, combined_cost_per_km):
    """
    Επιλέγει θέσεις που εξοικονομούν κόστος μεταφοράς για "ακριβούς" πελάτες

    ΚΡΙΤΗΡΙΟ ΕΠΙΛΟΓΗΣ ΘΕΣΕΩΝ:
    Greedy cover με βάρη cost_per_km → προτιμά θέσεις κοντά σε πελάτες με ακριβά μέσα

    ΣΤΡΑΤΗΓΙΚΗ:
    - Επιλογή θέσεων: Greedy cover με βάρη cost_per_km (ΟΧΙ demand)
    - Ανάθεση: ΠΑΝΤΑ swap repair (aggressive, χωρίς greedy)
    - Type selection: Minimal type που χωράει το φορτίο (ίδια με H1)
    
    ΔΙΑΦΟΡΑ ΑΠΟ H1:
    - WEIGHTS: cost_per_km αντί για demand
    - ASSIGNMENT: Άμεσο swap repair χωρίς greedy προσπάθεια
    
    Παράμετροι: (Ίδιες με H1 - βλ. heuristic_distance_greedy)

    """
    # ====================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ: Μετατροπή δεδομένων σε numpy arrays
    # ====================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    demand = np.asarray(demand, float)

    # ===============================================
    # ΥΠΟΛΟΓΙΣΜΟΣ ΕΦΙΚΤΟΤΗΤΑΣ ΑΠΟΣΤΑΣΕΩΝ
    # ===============================================
    # Δημιουργία boolean πίνακα: dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
    # Broadcasting: cl_sp[CL×N] <= maxd[CL×1] → [CL×N] boolean array
    dist_feasible = cl_sp <= maxd[:, None]

    # ========================================================================
    # ΒΗΜΑ 1: ΕΠΙΛΟΓΗ P ΘΕΣΕΩΝ (open_sites)
    # ========================================================================
    # Η ΚΡΙΣΙΜΗ ΔΙΑΦΟΡΑ ΑΠΟ H1 : Δημιουργία cost weights ( weight[k] = combined_cost_per_km[transport_mode[k]] )
    # ΛΟΓΙΚΗ: Κάθε πελάτης k χρησιμοποιεί συγκεκριμένο μέσο μεταφοράς με κόστος/km (Διαφορετικά μέσα έχουν ΠΟΛΥ διαφορετικό κόστος)
    weights = np.array([float(combined_cost_per_km[int(transport_mode[k])]) for k in range(CL)], dtype=float)

    # Υψηλό gain → Θέση i εξοικονομεί πολύ transport cost εξυπηρετώντας: Πελάτες με ΑΚΡΙΒΑ μέσα μεταφοράς και Σε ΚΟΝΤΙΝΕΣ αποστάσεις
    open_sites, _ = _greedy_open_sites_cover(P, dist_feasible, cl_sp, facility_types, E, weights=weights)
    if len(open_sites) != P:
        return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 2 από 5: ΑΝΑΘΕΣΗ ΠΕΛΑΤΩΝ ΣΕ ΘΕΣΕΙΣ (assign)
    # ========================================================================
    # ΚΡΙΣΙΜΗ ΔΙΑΦΟΡΑ ΑΠΟ H1:  H1: Δοκιμάζει greedy assignment πρώτα → fallback σε swap,  H2: ΑΠΕΥΘΕΙΑΣ swap repair (χωρίς greedy προσπάθεια)
    # Ίδια λογική swap repair με H1 
    # Η H2 συχνά διαλέγει θέσεις που είναι φτηνές αλλά ακατάλληλες ως προς capacity και κάλυψη, οπότε το greedy assignment αποτυγχάνει
    # το aggressive swap διορθώνει αυτό αντικαθιστώντας τις «λάθος» θέσεις με πιο ισορροπημένες, έστω και με λίγο μεγαλύτερο κόστος
    open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, demand, facility_types, capacities, E, P)
    if not ok:
        return _empty_sol()

    # ===============================================================================
    # ΒΗΜΑ 3: ΕΠΙΛΟΓΗ ΕΛΑΧΙΣΤΟΥ ΤΥΠΟΥ ΕΓΚΑΤΑΣΤΑΣΗΣ ΑΝΑ ΘΕΣΗ (open_types) 
    # ===============================================================================
    # Υπολογισμός φορτίου ανά θέση. Τώρα που έχουμε αναθέσει τους πελάτες, υπολογίζουμε πόσο φορτίο (ζήτηση) έχει κάθε ανοιχτή θέση
    load = {i: 0.0 for i in open_sites}  # Αρχικοποίηση: μηδενικό φορτίο
    for k in range(CL):                  # Για κάθε πελάτη
        load[int(assign[k])] += float(demand[k])

    # Για κάθε θέση, επιλέγουμε τον ΜΙΚΡΟΤΕΡΟ τύπο που μπορεί να χειριστεί το φορτίο της. Αυτό ελαχιστοποιεί το installation cost
    open_types = {}      # Dictionary: θέση → τύπος εγκατάστασης
    for i in open_sites: # Για κάθε ανοιχτή θέση
        # Βρες τον ελάχιστο τύπο που μπορεί να εξυπηρετήσει το φορτίο
        j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
        if j is None:
            return _empty_sol()
        open_types[int(i)] = int(j)

    # ========================================================================
    # ΒΗΜΑ 4 : ΕΛΕΓΧΟΣ ΠΡΟΥΠΟΛΟΓΙΣΜΟΥ (Cmax) 
    # ========================================================================
    # Υπολογισμός των δύο μεγεθών: συνολική απόσταση (αντικειμενική συνάρτηση), total_cost (installation + transport cost)
    dist_obj, total_cost = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

    # Η λύση πρέπει να σεβαστεί το budget constraint: total_cost ≤ Cmax. Χρησιμοποιούμε μικρή ανοχή (1e-9) για αριθμητικά σφάλματα
    # Η H2 ΕΙΝΑΙ ΚΑΛΥΤΕΡΗ ΓΙΑ BUDGET: Επιλέγει θέσεις για να ΜΕΙΩΣΕΙ transport cost, Χρησιμοποιεί minimal types (μείωση installation cost)
    # και Trade-off: Αποδέχεται μεγαλύτερη απόσταση για μικρότερο κόστος
    if total_cost > float(Cmax) + 1e-9:
        return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 5: ΔΗΜΙΟΥΡΓΙΑ ΠΛΗΡΟΥΣ ΔΙΑΝΥΣΜΑΤΟΣ ΛΥΣΗΣ
    # ========================================================================
    xvec = _build_xvec(open_types, assign, N, E, CL)
    return {
        "feasible": True,
        "distance_obj": float(dist_obj),
        "total_cost": float(total_cost),
        "open_sites": list(open_sites),
        "open_types": open_types,
        "assign": assign,
        "xvec": xvec,
    }


# ============================================================================================================================
# ΕΥΡΕΙΚΟΣ ΑΛΓΟΡΙΘΜΟΣ Η3: Feasibility-First (Capacity × Coverage Priority)
# ============================================================================================================================
def heuristic_feasibility_first(P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, combined_cost_per_km):
    """
    Επιλέγει θέσεις που συνδυάζουν ΜΕΓΑΛΗ ΧΩΡΗΤΙΚΟΤΗΤΑ με ΚΑΛΗ ΚΑΛΥΨΗ πελατών

    ΚΡΙΤΗΡΙΟ ΕΠΙΛΟΓΗΣ ΘΕΣΕΩΝ:
    Ταξινόμηση θέσεων κατά: max_capacity × coverage (ΦΘΙΝΟΥΣΑ) → Προτιμά θέσεις που μπορούν να εξυπηρετήσουν πολλούς πελάτες με μεγάλη χωρητικότητα

    ΣΤΡΑΤΗΓΙΚΗ:
    - Επιλογή θέσεων: Ταξινόμηση κατά (max_capacity × coverage)
    - Ανάθεση: Απευθείας swap repair
    - Type selection: Minimal type (ίδια με H1/H2)
    
    ΔΙΑΦΟΡΑ ΑΠΟ H1/H2:
    - Δεν χρησιμοποιεί greedy cover (iterative)
    - Απλή ταξινόμηση όλων των θέσεων και επιλογή top-P
    - Focus σε feasibility, όχι σε objective optimization
    
    Παράμετροι: (Ίδιες με H1/H2)
    """
    # ====================================================
    # ΠΡΟΕΤΟΙΜΑΣΙΑ: Μετατροπή δεδομένων σε numpy arrays
    # ====================================================
    cl_sp = np.asarray(cl_sp_distances, float)
    maxd = np.asarray(max_distance, float)
    demand = np.asarray(demand, float)

    # ===============================================
    # ΥΠΟΛΟΓΙΣΜΟΣ ΕΦΙΚΤΟΤΗΤΑΣ ΑΠΟΣΤΑΣΕΩΝ
    # ===============================================
    # Δημιουργία boolean πίνακα: dist_feasible[k,i] = True αν distance(k,i) ≤ max_distance[k]
    # Broadcasting: cl_sp[CL×N] <= maxd[CL×1] → [CL×N] boolean array
    dist_feasible = cl_sp <= maxd[:, None]

    
    # ========================================================================
    # ΒΗΜΑ 1: ΕΠΙΛΟΓΗ P ΘΕΣΕΩΝ (open_sites)
    # ========================================================================
    # ΣΤΡΑΤΗΓΙΚΗ: Ταξινόμηση θέσεων κατά (max_capacity × coverage) -  ο πολλαπλασιασμός ισορροπεί τα δύο κριτήρια
    # ΔΙΑΦΟΡΑ ΑΠΟ H1/H2:  H1/H2: Iterative greedy cover (επιλέγει μία-μία θέση),  H3: Ταξινομεί ΟΛΕΣ τις θέσεις εκ των προτέρων και παίρνει top-P
    # Θέλουμε θέσεις που: Μπορούν να χωρέσουν ΠΟΛΥ φορτίο (μεγάλη capacity) και καλύπτουν ΠΟΛΛΟΥΣ πελάτες (μεγάλο coverage)
    site_list = []                              # Λίστα (score, θέση) tuples
    for i in range(N):                          # Για κάθε υποψήφια θέση
        jmax = min(int(facility_types[i]), E-1) # Μέγιστος επιτρεπόμενος τύπος
        if jmax < 0:
            continue
        cover = int(dist_feasible[:, i].sum())  # Πόσοι πελάτες σε εφικτή απόσταση
        if cover == 0:
            continue
        maxcap = float(capacities[jmax])        # Χωρητικότητα του μεγαλύτερου τύπου
        # Score = max_capacity × coverage
        # ΣΗΜΕΙΩΣΗ: Αποθηκεύουμε -score για να ταξινομήσουμε κατά φθίνουσα σειρά
        site_list.append((-(maxcap * cover), int(i)))

    site_list.sort()
    # Επιλογή των P θέσεων με το υψηλότερο score
    open_sites = [i for _, i in site_list[:P]]
    if len(open_sites) != P:
        return _empty_sol()
    
    # ========================================================================
    # ΒΗΜΑ 2: ΑΝΑΘΕΣΗ ΠΕΛΑΤΩΝ ΣΕ ΘΕΣΕΙΣ (assign)
    # ========================================================================
    # ΣΤΡΑΤΗΓΙΚΗ: ΠΑΝΤΑ Swap Repair (όπως H2)
    # ΟΧΙ GREEDY διότι η H3 επιλέγει θέσεις για CAPACITY, όχι για proximity. Οι θέσεις μπορεί να είναι γεωγραφικά διασκορπισμένες 
    # και το greedy assignment πιθανώς θα αποτύχει (κακή coverage balance)
    # Swap repair απευθείας για καλύτερη κατανομή
    open_sites, assign, ok = _swap_repair(open_sites, dist_feasible, cl_sp, demand, facility_types, capacities, E, P)
    if not ok:
        return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 3: ΕΠΙΛΟΓΗ ΕΛΑΧΙΣΤΟΥ ΤΥΠΟΥ ΕΓΚΑΤΑΣΤΑΣΗΣ ΑΝΑ ΘΕΣΗ (open_types)
    # ========================================================================
    # Υπολογισμός φορτίου ανά θέση. Τώρα που έχουμε αναθέσει τους πελάτες, υπολογίζουμε πόσο φορτίο (ζήτηση) έχει κάθε ανοιχτή θέση
    load = {i: 0.0 for i in open_sites}  # Αρχικοποίηση: μηδενικό φορτίο
    for k in range(CL):                  # Για κάθε πελάτη
        load[int(assign[k])] += float(demand[k])

    # Για κάθε θέση, επιλέγουμε τον ΜΙΚΡΟΤΕΡΟ τύπο που μπορεί να χειριστεί το φορτίο της. Αυτό ελαχιστοποιεί το installation cost
    open_types = {}      # Dictionary: θέση → τύπος εγκατάστασης
    for i in open_sites: # Για κάθε ανοιχτή θέση
        # Βρες τον ελάχιστο τύπο που μπορεί να εξυπηρετήσει το φορτίο
        j = _choose_min_type_for_load(i, load[i], facility_types, capacities, E)
        if j is None:
            return _empty_sol()
        open_types[int(i)] = int(j)

    # ========================================================================
    # ΒΗΜΑ 4 : ΕΛΕΓΧΟΣ ΠΡΟΥΠΟΛΟΓΙΣΜΟΥ (Cmax) 
    # ========================================================================
    # Υπολογισμός των δύο μεγεθών: συνολική απόσταση (αντικειμενική συνάρτηση), total_cost (installation + transport cost)
    dist_obj, total_cost = _evaluate(open_types, assign, cl_sp, transport_mode, combined_cost_per_km, cost_types)

    # Η λύση πρέπει να σεβαστεί το budget constraint: total_cost ≤ Cmax. Χρησιμοποιούμε μικρή ανοχή (1e-9) για αριθμητικά σφάλματα
    if total_cost > float(Cmax) + 1e-9:
        return _empty_sol()

    # ========================================================================
    # ΒΗΜΑ 5: ΔΗΜΙΟΥΡΓΙΑ ΠΛΗΡΟΥΣ ΔΙΑΝΥΣΜΑΤΟΣ ΛΥΣΗΣ
    # ========================================================================
    xvec = _build_xvec(open_types, assign, N, E, CL)
    return {
        "feasible": True,
        "distance_obj": float(dist_obj),
        "total_cost": float(total_cost),
        "open_sites": list(open_sites),
        "open_types": open_types,
        "assign": assign,
        "xvec": xvec,
    }


