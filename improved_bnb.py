class Node:
    """
    Η κλάση Node αναπαριστά έναν κόμβο στο δέντρο αναζήτησης του Branch and Bound 
    Χρησιμοποιεί __slots__ για μείωση της κατανάλωσης μνήμης (σημαντικό για μεγάλα δέντρα) και ταχύτερη πρόσβαση στα πεδία
    Δεν χρησιμοποιεί dictionary για την αποθήκευση πεδίων, μειώνοντας έτσι την κατανάλωση μνήμης ανά κόμβο
    Κάθε κόμβος περιέχει:
       - Όρια μεταβλητών (ub, lb): Τα άνω και κάτω φράγματα που ορίζουν τον χώρο αναζήτησης
       - Βάθος (depth): Το επίπεδο στο δέντρο (root = 0)
       - Βάσεις simplex (vbasis, cbasis): Για warm start του simplex
       - Μεταβλητή διακλάδωσης (branching_var): Ποια μεταβλητή επιλέχθηκε για branching
       - Τιμή διακλάδωσης (branch_val): Η κλασματική τιμή της μεταβλητής στο LP
       - Αντικειμενική τιμή (obj_val): Η τιμή του LP relaxation σε αυτόν τον κόμβο
       - Μοναδικό αναγνωριστικό (node_id): Σε περίπτωση tie-breaking στην ουρά προτεραιότητας
    """

    __slots__ = ("ub", "lb", "depth", "vbasis", "cbasis", "branching_var", "branch_val", "label", "obj_val", "node_id")
    _counter = 0  # Καθολικός μετρητής για μοναδικά IDs κόμβων 
    def __init__(self, ub, lb, depth, vbasis, cbasis, branching_var, branch_val, obj_val, label=""):
        self.ub = ub                       # Άνω όρια (upper bounds) των μεταβλητών
        self.lb = lb                       # Κάτω όρια (lower bounds) των μεταβλητών
        self.depth = depth                 # Βάθος κόμβου στο δέντρο B&B
        self.vbasis = vbasis               # Variable basis για warm start του simplex
        self.cbasis = cbasis               # Constraint basis για warm start του simplex
        self.branching_var = branching_var # Index της μεταβλητής που επιλέχθηκε για branching
        self.branch_val = branch_val       # Η κλασματική τιμή της μεταβλητής στη LP λύση
        self.obj_val = obj_val             # Τιμή αντικειμενικής συνάρτησης του LP relaxation
        self.label = label                 # Ετικέτα κόμβου (π.χ. "Left", "Right", "Root")
        self.node_id = Node._counter       # Μοναδικό ID για αυτόν τον κόμβο
        Node._counter += 1                 # Αύξηση του καθολικού μετρητή

    def __lt__(self, other):
        """
        Τελεστής σύγκρισης για χρήση στο heapq (ουρά προτεραιότητας)
        Συγκρίνει πρώτα με βάση την αντικειμενική τιμή, και σε περίπτωση
        ισοπαλίας με βάση το node_id (για deterministic tie-breaking)
        """
        return (self.obj_val, self.node_id) < (other.obj_val, other.node_id)
    


