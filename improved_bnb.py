def precompute_branching_data(cl_sp_distances, max_distance, demand, eps=0.05):
    """
    Προϋπολογίζει στατιστικά που είναι σταθερά για το instance και χρησιμοποιούνται
    στην επιλογή μεταβλητής διακλάδωσης. Αυτός ο προϋπολογισμός γίνεται ΜΙΑ ΦΟΡΑ
    πριν ξεκινήσει το Branch and Bound, εξοικονομώντας σημαντικό υπολογιστικό χρόνο
    Υπολογίζει τις παρακάτω μετρικές:
    1. dist_feasible (CL x N bool πίνακας):
       - Ποιες αναθέσεις (πελάτης k → θέση i) είναι εφικτές βάσει απόστασης
       - dist_feasible[k,i] = True αν distance[k,i] <= max_distance[k]

    2. cust_scar (CL,) - Customer Scarcity:
       - Μέτρο σπανιότητας πελάτη = 1 / (αριθμός διαθέσιμων θέσεων)
       - Υψηλότερη τιμή → λιγότερες επιλογές → πιο "σπάνιος" πελάτης
       - Χρησιμοποιείται για να δώσουμε προτεραιότητα σε πελάτες με λίγες επιλογές
    
    3. feas_cnt (N,) - Feasible Count per Site:
       - Πόσοι πελάτες μπορούν να εξυπηρετηθούν από κάθε θέση
       - Υψηλότερη τιμή → πιο σημαντική θέση (εξυπηρετεί περισσότερους)
    
    4. feas_dem (N,) - Feasible Demand per Site:
       - Συνολική ζήτηση πελατών που μπορούν να εξυπηρετηθούν από κάθε θέση
       - Κανονικοποιημένη ως % της συνολικής ζήτησης
    
    5. invd (N,) - Inverse Distance Score:
       - Άθροισμα των demand[k] / (distance[k,i] + eps) για εφικτούς πελάτες
       - Υψηλότερη τιμή → θέση με μεγάλη ζήτηση σε κοντινές αποστάσεις
       - Μετρά την "προσπελασιμότητα" της θέσης
    
    6. scarcity_site (N,) - Site Scarcity Score:
       - Άθροισμα (demand[k] / options[k]) για πελάτες που μπορούν να εξυπηρετηθούν
       - Υψηλότερη τιμή → θέση που εξυπηρετεί πολλούς "σπάνιους" πελάτες
       - Μετρά πόσο κρίσιμη είναι η θέση για πελάτες με λίγες επιλογές
    
    Όλα τα scores κανονικοποιούνται στο [0,1] για ομοιόμορφη συνεισφορά στην τελική βαθμολογία
    
    Παράμετροι:
        cl_sp_distances: πίνακας αποστάσεων (CL x N)
        max_distance: μέγιστες αποδεκτές αποστάσεις ανά πελάτη (CL)
        demand: ζήτηση ανά πελάτη (CL)
        eps: μικρή σταθερά για αποφυγή διαίρεσης με μηδέν
    
    Επιστρέφει:
        tuple: (dist_feasible, cust_scar, feas_cnt, feas_dem, invd, scarcity_site)
    """

    # Μετατροπή εισόδων σε numpy arrays για αποδοτικότερη επεξεργασία
    cl_sp = np.asarray(cl_sp_distances, dtype=float)   
    maxd  = np.asarray(max_distance, dtype=float)      
    dem   = np.asarray(demand, dtype=float)            

    CL, N = cl_sp.shape  # CL: αριθμός πελατών, N: αριθμός υποψήφιων θέσεων

    # ===========================================================
    # ΒΗΜΑ 1: Υπολογισμός εφικτότητας αναθέσεων βάσει απόστασης
    # ===========================================================
    # Κάθε στοιχείο [k,i] είναι True αν ο πελάτης k μπορεί να εξυπηρετηθεί από τη θέση i
    dist_feasible = (cl_sp <= maxd[:, None])           
    feas_f = dist_feasible.astype(float)     # Μετατροπή σε float για αριθμητικές πράξεις

    # ============================================================
    # ΒΗΜΑ 2: Υπολογισμός σπανιότητας πελατών (Customer Scarcity)
    # ============================================================
    # Για κάθε πελάτη, μετράμε πόσες εφικτές θέσεις έχει
    options = dist_feasible.sum(axis=1).astype(float)  
    options = np.maximum(options, 1.0)      # Αποφυγή διαίρεσης με μηδέν
    cust_scar = 1.0 / options               # Όσο λιγότερες επιλογές, τόσο υψηλότερη η σπανιότητα             
    cust_scar /= (cust_scar.max() + 1e-12)  # Κανονικοποίηση στο [0,1]

    # =========================================================
    # ΒΗΜΑ 3: Υπολογισμός μετρικών σημαντικότητας για κάθε θέση
    # ==========================================================
    # 3α. Πλήθος εφικτών πελατών ανά θέση
    feas_cnt = feas_f.sum(axis=0)         # Πόσοι πελάτες μπορούν να εξυπηρετηθούν από κάθε θέση
    # 3β. Συνολική ζήτηση εφικτών πελατών ανά θέση (ως ποσοστό της συνολικής ζήτησης)            
    total_dem = dem.sum() + 1e-9          # Συνολική ζήτηση όλων των πελατών
    feas_dem = (feas_f * dem[:, None]).sum(axis=0) / total_dem
    # 3γ. Inverse Distance Score - βαρύτητα ζήτησης με αντίστροφη απόσταση
    # Υψηλότερη τιμή για θέσεις με μεγάλη ζήτηση σε κοντινές αποστάσεις
    invd = (feas_f * (dem[:, None] / (cl_sp + eps))).sum(axis=0)
    # 3δ. Site Scarcity Score - πόσο κρίσιμη είναι η θέση για "σπάνιους" πελάτες
    # Αθροίζει τη σταθμισμένη ζήτηση πελατών με λίγες επιλογές
    scarcity_site = (feas_f * (dem / options)[:, None]).sum(axis=0)

    # =============================================================
    # ΒΗΜΑ 4: Κανονικοποίηση όλων των μετρικών στο [0,1]
    # =============================================================
    # Ορίζουμε βοηθητική συνάρτηση για κανονικοποίηση
    def norm01(a):
        """Κανονικοποιεί τον πίνακα στο [0,1] διαιρώντας με τη μέγιστη τιμή"""
        a = np.asarray(a, dtype=float)
        m = a.max()
        return a / m if m > 1e-12 else a
    
    # Εφαρμόζουμε κανονικοποίηση σε όλες τις μετρικές θέσεων
    feas_cnt      = norm01(feas_cnt)
    feas_dem      = norm01(feas_dem)
    invd          = norm01(invd)
    scarcity_site = norm01(scarcity_site)

    # Επιστρέφουμε όλα τα προϋπολογισμένα δεδομένα
    return dist_feasible, cust_scar, feas_cnt, feas_dem, invd, scarcity_site


def select_branching_var_hybrid_simple(x_candidate, integer_var, N, E, CL, facility_types, capacities, demand, dist_feasible, cust_scar, feas_cnt=None,
    feas_dem=None, invd=None, scarcity_site=None, ub=None, phase="dive", eps_int=1e-6, frac_threshold=0.05, w_cnt=0.3, w_dem=0.6, w_invd=0.4, w_scar_site=1.0,
    w_cap_dive=1.6, w_cap_best=1.0, pressure_scale=0.25, pressure_max=2.0):
    """
    Υβριδική στρατηγική επιλογής μεταβλητής διακλάδωσης για τον αλγόριθμο Branch and Bound
    
    Η συνάρτηση υλοποιεί μια δύο-επίπεδη στρατηγική επιλογής:
    1. Προτεραιότητα σε μεταβλητές X (ανοίγματα θέσεων): Επιλέγει θέσεις με υψηλή 
       επιρροή (πολλοί εφικτοί πελάτες, μεγάλη ζήτηση) και στη συνέχεια τον τύπο 
       εγκατάστασης που μεγιστοποιεί τη χωρητικότητα υπό πίεση
    
    2. Εναλλακτικά σε μεταβλητές Y (αναθέσεις πελατών): Επιλέγει αναθέσεις πελατών
       με λίγες διαθέσιμες επιλογές (υψηλή σπανιότητα) για γρηγορότερη σύγκλιση
    
    Η στρατηγική προσαρμόζεται δυναμικά στη φάση του αλγορίθμου (dive vs best-first)
    και στην πίεση χωρητικότητας που προκύπτει από την κλασματική λύση
    
    Παράμετροι:
        x_candidate    : Κλασματική λύση LP χαλάρωσης (όλες οι μεταβλητές)
        integer_var    : Boolean mask - True για ακέραιες μεταβλητές
        N              : Αριθμός υποψήφιων θέσεων
        E              : Αριθμός τύπων εγκαταστάσεων
        CL             : Αριθμός πελατών
        facility_types : Μέγιστος τύπος εγκατάστασης ανά θέση 
        capacities     : Χωρητικότητα κάθε τύπου εγκατάστασης 
        demand         : Ζήτηση κάθε πελάτη 
        dist_feasible  : Boolean πίνακας εφικτότητας αναθέσεων 
        cust_scar      : Σπανιότητα πελατών - προϋπολογισμένη 
        feas_cnt       : Πλήθος εφικτών πελατών ανά θέση 
        feas_dem       : Εφικτή ζήτηση ανά θέση 
        invd           : Inverse distance score ανά θέση 
        scarcity_site  : Σπανιότητα θέσης - κρισιμότητα 
        ub             : Upper bounds μεταβλητών στον τρέχοντα κόμβο
        phase          : Φάση αλγορίθμου - "dive" ή "best" 
        eps_int        : Ανοχή ακεραιότητας 
        frac_threshold : Ελάχιστη κλασματικότητα για επιλογή 
        w_cnt          : Βάρος μετρικής πλήθους πελατών 
        w_dem          : Βάρος μετρικής ζήτησης 
        w_invd         : Βάρος inverse distance score 
        w_scar_site    : Βάρος σπανιότητας θέσης 
        w_cap_dive     : Βάρος χωρητικότητας σε dive φάση 
        w_cap_best     : Βάρος χωρητικότητας σε best φάση 
        pressure_scale : Παράμετρος κλιμάκωσης πίεσης χωρητικότητας 
        pressure_max   : Μέγιστη επιτρεπτή πίεση χωρητικότητας 
    
    Επιστρέφει:
        tuple: (var_idx, var_value) - Δείκτης και τιμή της επιλεγμένης μεταβλητής
               Επιστρέφει (None, None) αν δεν βρεθεί κατάλληλη μεταβλητή
    """
    # Μετατροπή της υποψήφιας λύσης σε numpy array
    x = np.asarray(x_candidate, dtype=float)

    def frac(v):
        """Υπολογίζει την κλασματικότητα μιας μεταβλητής: min(v, 1-v)"""
        v = float(v)
        if v < 0.0: v = 0.0
        if v > 1.0: v = 1.0
        return min(v, 1.0 - v)

    # ============================================================
    # ΥΠΟΛΟΓΙΣΜΟΣ ΠΙΕΣΗΣ ΧΩΡΗΤΙΚΟΤΗΤΑΣ (Capacity Pressure)
    # ============================================================
    # Η πίεση χωρητικότητας μετράει πόσο επείγουσα είναι η ανάγκη για περισσότερη χωρητικότητα
    cap = np.asarray(capacities, dtype=float)
    cap_max = float(cap.max() + 1e-9)    # Μέγιστη χωρητικότητα για κανονικοποίηση
    dem = np.asarray(demand, dtype=float)
    dem_total = float(dem.sum())         # Συνολική ζήτηση προς κάλυψη

    # Αναμενόμενη χωρητικότητα από την τρέχουσα κλασματική λύση
    x_open = x[:N * E].reshape(N, E)  # Μεταβλητές ανοίγματος θέσεων
    expected_capacity = float((x_open * cap[None, :]).sum())
    # Υπολογισμός ελλείμματος χωρητικότητας
    gap = max(0.0, dem_total - expected_capacity)
    # Υπολογισμός συντελεστή πίεσης: όσο μεγαλύτερο το έλλειμμα, τόσο υψηλότερη η πίεση
    denom = pressure_scale * dem_total + 1e-9
    cap_pressure = 1.0 + gap / denom
    cap_pressure = min(pressure_max, cap_pressure)  # Περιορισμός στη μέγιστη επιτρεπτή τιμή

    # Επιλογή βάρους χωρητικότητας ανάλογα με τη φάση του αλγορίθμου
    w_cap = w_cap_dive if phase == "dive" else w_cap_best

    # ========================================================================
    # ΜΕΡΟΣ 1: ΔΙΑΚΛΑΔΩΣΗ ΣΕ ΜΕΤΑΒΛΗΤΕΣ X (Επιλογή Θέσης - Site Selection)
    # ========================================================================
    # Υπολογίζουμε τη συνολική κλασματικότητα για κάθε θέση (άθροισμα όλων των τύπων)
    site_frac = np.zeros(N, dtype=float)
    for i in range(N):
        s = 0.0
        for j in range(facility_types[i] + 1):
            s += frac(x_open[i, j])
        site_frac[i] = s

    # Αναζήτηση της καλύτερης θέσης για διακλάδωση
    best_site = None
    best_site_score = -1.0
    for i in range(N):
        # Αγνοούμε θέσεις με πολύ μικρή κλασματικότητα
        if site_frac[i] < frac_threshold:
            continue

        # Υπολογισμός επιρροής (impact) της θέσης βάσει των προϋπολογισμένων μετρικών
        impact = 1.0
        if feas_cnt is not None:      impact += w_cnt * float(feas_cnt[i])
        if feas_dem is not None:      impact += w_dem * float(feas_dem[i])
        if invd is not None:          impact += w_invd * float(invd[i])
        if scarcity_site is not None: impact += w_scar_site * float(scarcity_site[i])

        # Συνολική βαθμολογία: κλασματικότητα × επιρροή
        score_i = site_frac[i] * impact

        if score_i > best_site_score:
            best_site_score = score_i
            best_site = i

    # Αν βρέθηκε καλή θέση, επιλέγουμε τον καλύτερο τύπο εγκατάστασης για αυτή
    if best_site is not None:
        i = best_site
        best_idx = None
        best_val = None
        best_score = -1.0

        # Εξετάζουμε όλους τους διαθέσιμους τύπους για τη θέση i
        for j in range(facility_types[i] + 1):
            idx = i * E + j
            # Ελέγχουμε αν η μεταβλητή είναι ακέραια
            if not integer_var[idx]:
                continue

            v = float(x[idx])
            # Αγνοούμε μεταβλητές που είναι ήδη ακέραιες
            if abs(v - round(v)) <= eps_int:
                continue

            f = frac(v)
            # Αγνοούμε πολύ μικρές κλασματικές τιμές
            if f < frac_threshold:
                continue

            # Bonus για μεγαλύτερες χωρητικότητες (κανονικοποιημένο στο [0,1])
            cap_bonus = float(cap[j] / cap_max)  

            # Τελική βαθμολογία: κλασματικότητα × (1 + βάρος_χωρητικότητας × πίεση × bonus)
            score = f * (1.0 + w_cap * cap_pressure * cap_bonus)

            if score > best_score:
                best_score = score
                best_idx = idx
                best_val = v

        # Αν βρέθηκε κατάλληλη μεταβλητή x, την επιστρέφουμε
        if best_idx is not None:
            return best_idx, best_val

    # ========================================================================
    # ΜΕΡΟΣ 2: ΔΙΑΚΛΑΔΩΣΗ ΣΕ ΜΕΤΑΒΛΗΤΕΣ Y (Ανάθεση Πελατών - Assignment)
    # ========================================================================
    y_start = N * E         # Αρχή των μεταβλητών ανάθεσης
    y_end = N * E + CL * N  # Τέλος των μεταβλητών ανάθεσης

    # Fallback: αν δεν υπάρχει upper bound, επιλέγουμε απλά τη μεγαλύτερη κλασματικότητα
    if ub is None:
        best_idx = None
        best_val = None
        best_f = -1.0
        for idx in range(y_start, y_end):
            if not integer_var[idx]:
                continue
            v = float(x[idx])
            # Αγνοούμε ακέραιες τιμές
            if abs(v - round(v)) <= eps_int:
                continue
            f = frac(v)
            # Επιλέγουμε τη μεγαλύτερη κλασματικότητα
            if f >= frac_threshold and f > best_f:
                best_f = f
                best_idx = idx
                best_val = v
        return best_idx, best_val

    # Μετατροπή upper bounds σε numpy array
    ub = np.asarray(ub, dtype=float)

    # ========================================================================
    # Υπολογισμός ποιες θέσεις μπορούν ακόμα να ανοίξουν σε αυτόν τον κόμβο
    # ========================================================================
    can_open = np.zeros(N, dtype=bool)
    for i in range(N):
        ok = False
        # Ελέγχουμε αν κάποιος τύπος εγκατάστασης έχει upper bound > 0
        for j in range(facility_types[i] + 1):
            if ub[i * E + j] > eps_int:
                ok = True
                break
        can_open[i] = ok

    # Αναζήτηση της καλύτερης μεταβλητής y για διακλάδωση
    best_idx = None
    best_val = None
    best_score = -1.0

    # ==========================================================================
    # Για κάθε πελάτη, υπολογίζουμε τις διαθέσιμες επιλογές στον τρέχοντα κόμβο
    # ==========================================================================
    for k in range(CL):
        # Μετράμε πόσες θέσεις είναι εφικτές ΚΑΙ μπορούν ακόμα να ανοίξουν
        options_node = float(np.sum(dist_feasible[k, :] & can_open))
        options_node = max(1.0, options_node)   # Αποφυγή διαίρεσης με μηδέν

        # Σπανιότητα σε επίπεδο κόμβου: λιγότερες επιλογές → υψηλότερη σπανιότητα
        node_scar = 1.0 / options_node

        base = y_start + k * N
        # Εξετάζουμε όλες τις εφικτές αναθέσεις για τον πελάτη k
        for i in range(N):
            # Αγνοούμε μη εφικτές αναθέσεις λόγω απόστασης
            if not dist_feasible[k, i]:
                continue
            # Αγνοούμε θέσεις που δεν μπορούν να ανοίξουν
            if not can_open[i]:
                continue

            idx = base + i
            # Ελέγχουμε αν η μεταβλητή είναι ακέραια
            if not integer_var[idx]:
                continue

            v = float(x[idx])
            # Αγνοούμε ακέραιες τιμές
            if abs(v - round(v)) <= eps_int:
                continue

            f = frac(v)
            # Αγνοούμε πολύ μικρές κλασματικές τιμές
            if f < frac_threshold:
                continue

            # Βαθμολογία: προτεραιότητα σε σπάνιους πελάτες × κλασματικότητα
            # Συνδυάζουμε τη σπανιότητα σε επίπεδο κόμβου και τη γενική σπανιότητα
            score = (1.0 + 2.0 * node_scar + 1.0 * float(cust_scar[k])) * f

            if score > best_score:
                best_score = score
                best_idx = idx
                best_val = v

    # Επιστροφή της καλύτερης μεταβλητής y (ή None αν δεν βρέθηκε)
    return best_idx, best_val



def make_select_branching_fn(integer_var, N, E, CL, facility_types, capacities, demand, cust_scar, dist_feasible=None, feas_cnt=None, feas_dem=None,
    invd=None, scarcity_site=None, w_cap_dive=1.6, w_cap_best=1.0, frac_threshold=0.05):
    """
    Δημιουργεί closure function για την επιλογή μεταβλητής διακλάδωσης
    Αυτή η συνάρτηση δημιουργεί μια εξειδικευμένη συνάρτηση επιλογής διακλάδωσης που "κλειδώνει" όλες τις σταθερές παραμέτρους 
    του προβλήματος (δομή δικτύου, χωρητικότητες, ζήτηση, προϋπολογισμένα στατιστικά).
    
    Η επιστρεφόμενη συνάρτηση δέχεται μόνο τις δυναμικές παραμέτρους που αλλάζουν
    σε κάθε κόμβο του Branch and Bound (κλασματική λύση, upper bounds, φάση)
    
    Αυτή η προσέγγιση εξοικονομεί χρόνο και μνήμη καθώς:
    - Αποφεύγει την επανάληψη των ίδιων ορισμάτων σε κάθε κλήση
    - Κάνει τον κώδικα του B&B πιο καθαρό και ευανάγνωστο
    - Επιτρέπει εύκολη αλλαγή στρατηγικής χωρίς τροποποίηση του κύριου αλγορίθμου
    
    Επιστρέφει:
        function: Συνάρτηση με υπογραφή select_fn(x, ub, phase) που επιστρέφει
                  (var_idx, var_value) για τη μεταβλητή διακλάδωσης  
    """
    
    def select_fn(x, ub, phase):
        """
        Εσωτερική συνάρτηση επιλογής - καλείται σε κάθε κόμβο του Branch and Bound
        
        Παράμετροι:
            x (array): Κλασματική λύση LP χαλάρωσης
            ub (array): Upper bounds μεταβλητών στον τρέχοντα κόμβο
            phase (str): Φάση αλγορίθμου - "dive" ή "best"
        
        Επιστρέφει:
            tuple: (var_idx, var_value) - Δείκτης και τιμή επιλεγμένης μεταβλητής
        """
        return select_branching_var_hybrid_simple(x_candidate=x, integer_var=integer_var, N=N, E=E, CL=CL, facility_types=facility_types, capacities=capacities,
            demand=demand, dist_feasible=dist_feasible, cust_scar=cust_scar, feas_cnt=feas_cnt, feas_dem=feas_dem, invd=invd, scarcity_site=scarcity_site,
            ub=ub, phase=phase, w_cap_dive=w_cap_dive, frac_threshold=frac_threshold, w_cap_best=w_cap_best)
    
    return select_fn


