# Εκτέλεση ΟΛΩΝ των Μεθευρετικών και Επιλογή Καλύτερης
# ============================================================================================================================
def run_vnd_sa_ils_vnd(sol_init, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, combined_cost_per_km,
    # VND params
    vnd1_iters=200,
    vnd2_iters=200,
    vnd_first_improvement=True,
    vnd_top_scan_per_client=100,
    # SA params
    sa_iters=2000,
    sa_T0=1.0,
    sa_alpha=0.995,
    sa_candidate_pool=200,
    sa_progress_every=0,
    # ILS params
    ils_outer_iters=50,
    ils_perturb_swaps=2,
    ils_vnd_iters=200,
    ils_patience=10,
    ils_progress=False,
    # others
    seed=0,
    verbose=False
):
    """
    Ολοκληρωμένη μεΘευρετική διαδικασία που συνδυάζει 4 μεθόδους σε σειρά για μέγιστη βελτίωση λύσης
    Αυτή η λύση θα χρησιμοποιηθεί από τον αλγόριθμο Branch and Bound ως upper_bound για αποδοτικότερο pruning
    Παίρνει feasible sol_init (από τα heuristics Η1-Η4)
    PIPELINE FLOW:   sol_init → VND₁ → SA → ILS → VND₂ → best
    Επιστρέφει την καλύτερη feasible λύση που βρήκε

    Παράμετροι:
        sol_init: Αρχική feasible λύση (από heuristics H1-H4)
        P, N, E, CL, ...: Παράμετροι προβλήματος
        vnd1_iters, vnd2_iters: VND iterations 
        vnd_first_improvement: First vs Best improvement (προεπιλογή first)
        vnd_top_scan_per_client: Scan limit ανά πελάτη 
        sa_iters, sa_T0, sa_alpha, sa_candidate_pool: SA παράμετροι
        ils_outer_iters, ils_perturb_swaps, ils_vnd_iters, ils_patience: ILS παράμετροι
        seed: Master seed 
        verbose: Εκτύπωση προόδου (προεπιλογή False)
    
    Επιστρέφει:
        dict: Η ΚΑΛΥΤΕΡΗ λύση από όλα τα stages με επιπλέον metadata:
              - "pipeline_used": "VND->SA->ILS->VND"
              - "seed": Το χρησιμοποιημένο seed
    """
    
    # Δέχεται μόνο feasible λύσεις. Αν infeasible → Δεν μπορεί να ξεκινήσει
    if (sol_init is None) or (not sol_init.get("feasible", False)):
        return _empty_sol()

    # Αρχικοποίηση best solution. Το best θα ενημερώνεται μετά από κάθε στάδιο αν υπάρχει βελτίωση
    best = sol_init

    # Εκτύπωση αρχικής κατάστασης
    if verbose:
        print(f"[PIPE] start obj={best['distance_obj']:.4f} cost={best['total_cost']:.2f}")

    # ========================================================================
    # ΣΤΑΔΙΟ 1: VND₁ - INITIAL LOCAL SEARCH
    # ========================================================================
    sol1 = meta_vnd_assignments(best, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
        combined_cost_per_km, max_iters=vnd1_iters, top_scan_per_client=vnd_top_scan_per_client, first_improvement=vnd_first_improvement, seed=seed)
    # Έλεγχος και ενημέρωση best
    # Αν το VND₁ βρήκε καλύτερη λύση → Ενημέρωση best. Αν όχι (ή infeasible) → Το best παραμένει sol_init
    if sol1.get("feasible", False) and sol1["distance_obj"] < best["distance_obj"] - 1e-12:
        best = sol1
    if verbose:
        print(f"[PIPE] after VND1 obj={best['distance_obj']:.4f}")

    # ========================================================================
    # ΣΤΑΔΙΟ 2: SA - SIMULATED ANNEALING (SITE SWAPS)
    # ========================================================================
    sol2 = meta_simulated_annealing_site_swap(best, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax,
        transport_mode, combined_cost_per_km, iters=sa_iters, T0=sa_T0, alpha=sa_alpha, candidate_pool=sa_candidate_pool, seed=seed + 1, progress_every=sa_progress_every
    )
    # Έλεγχος και ενημέρωση best
    if sol2.get("feasible", False) and sol2["distance_obj"] < best["distance_obj"] - 1e-12:
        best = sol2
    if verbose:
        print(f"[PIPE] after SA   obj={best['distance_obj']:.4f}")

    # ========================================================================
    # ΣΤΑΔΙΟ 3: ILS - ITERATED LOCAL SEARCH (PERTURB + VND)
    # ========================================================================
    sol3 = meta_ils_perturb_vnd(best, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode,
        combined_cost_per_km, outer_iters=ils_outer_iters, perturb_swaps=ils_perturb_swaps, vnd_iters=ils_vnd_iters, seed=seed + 2, patience=ils_patience,
        progress=ils_progress)
    # Έλεγχος και ενημέρωση best
    if sol3.get("feasible", False) and sol3["distance_obj"] < best["distance_obj"] - 1e-12:
        best = sol3
    if verbose:
        print(f"[PIPE] after ILS  obj={best['distance_obj']:.4f}")

    # ========================================================================
    # ΣΤΑΔΙΟ 4: VND₂ - FINAL POLISH
    # ========================================================================
    sol4 = meta_vnd_assignments(best, P, N, E, CL, cl_sp_distances, max_distance, demand, facility_types, capacities, cost_types, Cmax, transport_mode, 
        combined_cost_per_km, max_iters=vnd2_iters, top_scan_per_client=vnd_top_scan_per_client, first_improvement=vnd_first_improvement, seed=seed + 3)
    # Έλεγχος και ενημέρωση best
    if sol4.get("feasible", False) and sol4["distance_obj"] < best["distance_obj"] - 1e-12:
        best = sol4
    if verbose:
        print(f"[PIPE] after VND2 obj={best['distance_obj']:.4f}")

    # Δημιουργία αντιγράφου για να μην τροποποιήσουμε το original
    best = dict(best)

    # Προσθήκη πληροφοριών για tracking
    best["pipeline_used"] = "VND->SA->ILS->VND"  # Όνομα pipeline
    best["seed"] = seed                          # Master seed που χρησιμοποιήθηκε

    # Επιστροφή καλύτερης λύσης
    return best




# ========================================================================================================================================
#                                                     ΣΥΝΑΡΤΗΣΗ BRANCH AND BOUND
# ========================================================================================================================================
def branch_and_bound(model, ub, lb, integer_var, start,select_branching_fn, dive_time_limit=None, dive_node_limit=5000, heap_seed_limit=None, verbose_transfer=True, 
                    heuristic_solution=None):
    """
    Υλοποίηση υβριδικού Branch and Bound, το οποίο συνδυάζει DFS diving για γρήγορη εύρεση αρχικής εφικτής λύσης (incumbent)
    με best‑first search για βελτιστότητα

    ΣΤΡΑΤΗΓΙΚΗ:
    1. PHASE 1 (DIVING): DFS dive για γρήγορη εύρεση feasible solution (incumbent)
       - Preferred child → stack (DFS path)
       - Sibling → deferred list (ΌΧΙ heap - αποφυγή περιττού υπολογιστικού κόστους)
       - Τερματισμός: Βρέθηκε incumbent ή time/node limit
    2. TRANSITION: Μεταφορά των deferred nodes → heap (με φιλτράρισμα)
       - Prune μέσω cutoff (βασισμένο στο τρέχον incumbent)
       - Ταξινόμηση κατά bound (best‑first)
       - Προαιρετικός περιορισμός μεγέθους (heap_seed_limit)
    3. PHASE 2 (BEST-FIRST): Εξερεύνηση για εύρεση optimal solution
       - Εξερεύνηση μέσω min‑heap (κόμβος με το καλύτερο bound πρώτος)
       - Επιθετικό pruning χρησιμοποιώντας το cutoff του incumbent
       - Τερματισμός: Απόδειξη βέλτιστου ή time limit
    
    Βελτιώσεις από Aπλό B&B:
    - Υβριδική στρατηγική εξερεύνησης (DFS + best‑first)
    - Έξυπνη επιλογή μεταβλητής για διακλάδωση και προϋπολογισμός δεδομένων
    - Constraint propagation Α, Β, C (προώθηση περιορισμών)
    - Ισχυρό warm start:  (α) Simplex bases (VBasis/CBasis)  (β) Incumbent warm start από ευρετικές/μεθευρετικές
    - Ενσωμάτωση ευρετικών για αρχικές λύσεις και μεθευρετικών για βελτίωση incumbent πριν ξεκινήσει το B&B 
    - Filtered transfer και πιο επιθετικό pruning
    - Χρήση slots για μικρότερη χρήση μνήμης ανά κόμβο και πιο αποδοτική διαχείριση κόμβων
    
    Παράμετροι:
        model: Gurobi LP model (relaxation του MILP)
        ub, lb: Upper/Lower bounds μεταβλητών
        integer_var: Boolean mask για ακέραιες μεταβλητές
        start: Χρόνος εκκίνησης (για time limit)
        select_branching_fn: Function(x, ub, phase) → (var_idx, var_value)
        dive_time_limit: Χρόνος diving (None=auto 5% του TIME_LIMIT)
        dive_node_limit: Μέγιστοι κόμβοι diving 
        heap_seed_limit: Μέγιστα deferred → heap (None=όλα)
        verbose_transfer: Εκτύπωση transfer stats
        heuristic_solution: Dict με metaheuristic λύση (None=καμία)
    
    Επιστρέφει:
        tuple: (
            solutions: List[List[x, obj, depth]] - Όλες οι integer λύσεις,
            best_sol_idx: Index καλύτερης λύσης στο solutions,
            num_solutions: Συνολικός αριθμός λύσεων,
            heuristic_loaded: Boolean - Φορτώθηκε heuristic;
        )
    
    """

    # ========================================================================
    # ΚΑΘΟΛΙΚΕΣ ΜΕΤΑΒΛΗΤΕΣ ΚΑΙ ΣΤΑΘΕΡΕΣ
    # ========================================================================
    global nodes, lower_bound, upper_bound, isMax

    EPS = 1e-6          # Αριθμητική ανοχή
    Node._counter = 0   # Επαναφορά μετρητή node ID

    # ========================================================================
    # ΠΡΟΣΑΡΜΟΣΤΙΚΟ ΧΡΟΝΙΚΟ ΟΡΙΟ DIVE
    # ========================================================================
    # ΣΤΡΑΤΗΓΙΚΗ:
    # Αν δεν δοθεί dive_time_limit → Υπολογίζεται αυτόματα ως 5% του TIME_LIMIT
    # Περιορίζεται σε [10, 180] δευτερόλεπτα για λογική συμπεριφορά
    if dive_time_limit is None:
        dive_time_limit = max(10.0, min(180.0, TIME_LIMIT * 0.05))

    # ========================================================================
    # INITIALIZATION: INCUMBENT & CUTOFF
    # ========================================================================
    # INCUMBENT: Η καλύτερη integer λύση που βρέθηκε μέχρι στιγμής
    # CUTOFF: Bound για pruning (κόμβοι με bound χειρότερο από cutoff → prune)
    # ΑΡΧΙΚΟΠΟΙΗΣΗ:  MAX problem: incumbent=-∞, cutoff=lower_bound (από global) ,  MIN problem: incumbent=+∞, cutoff=upper_bound (από global)
    if isMax:
        cutoff = lower_bound   # Αρχικό lower bound (από global)
        incumbent = -np.inf    # Χειρότερη δυνατή objective
    else:
        cutoff = upper_bound   # Αρχικό upper bound (από global)
        incumbent = np.inf     # Χειρότερη δυνατή objective

    # SOLUTIONS TRACKING
    solutions = []       # List[List[x_vector, objective, depth]] - λίστα όλων των integer λύσεων που βρέθηκαν
    best_sol_idx = None  # Index καλύτερης λύσης στο solutions list

    # ========================================================================
    # HEURISTIC SOLUTION INTEGRATION
    # ========================================================================
    # Αν υπάρχει λύση από metaheuristics (VND→SA→ILS→VND), φορτώνεται ως incumbent για επιθετικό pruning από την αρχή
    # ΠΛΕΟΝΕΚΤΗΜΑ:
    # - Cutoff ενημερώνεται άμεσα
    # - SKIP diving phase (έχουμε ήδη incumbent)
    # - Επιθετικό pruning σε best-first
    # - Ταχύτερη σύγκλιση στο βέλτιστο

    heuristic_loaded = False   # Flag: Φορτώθηκε heuristic λύση;
    if heuristic_solution is not None:
        # Εξαγωγή heuristic solution
        heur_x = heuristic_solution["x"]           # διάνυσμα λύσης
        heur_obj = heuristic_solution["obj"]       # τιμή αντικειμενικής συνάρτησης
        
        # Αποθήκευση ως πρώτη λύση
        # Depth=-1 σημαίνει "heuristic" (όχι από B&B κόμβο)
        solutions.append([heur_x, heur_obj, -1])    
        best_sol_idx = 0      # Πρώτη λύση = καλύτερη (μέχρι στιγμής)
        incumbent = heur_obj  # Ενημέρωση incumbent
        
        # Ενημέρωση cutoff
        # MAX: cutoff = max(cutoff, incumbent) → Prune κόμβους με bound < cutoff
        # MIN: cutoff = min(cutoff, incumbent) → Prune κόμβους με bound > cutoff
        if isMax:
            cutoff = max(cutoff, incumbent)
        else:
            cutoff = min(cutoff, incumbent)
        
        heuristic_loaded = True    # Σημείωσε ότι φορτώθηκε
        print(f"[B&B] Heuristic solution loaded: obj={heur_obj:.6f}")


    # ========================================================================
    # ΔΟΜΕΣ ΔΕΔΟΜΕΝΩΝ
    # ========================================================================
    heap = []               # Min-heap για αναζήτηση βέλτιστου-πρώτα (PHASE 2)
                            # Μορφή: [(priority, node_id, Node), ...]
                            # Priority = -obj (MAX) ή obj (MIN)
    
    dive_stack = []         # Στοίβα για DFS diving (PHASE 1)
                            # Μορφή: [Node, Node, ...]
                            # LIFO: Τελευταίο Μέσα Πρώτο Έξω
    
    deferred_nodes = []     # Αδελφοί κόμβοι που δεν ακολουθήθηκαν στο dive
                            # Μορφή: [Node, Node, ...]
                            # ΔΕΝ μπαίνουν στο heap κατά το dive (χωρίς overhead)


    # ========================================================================
    # ΒΟΗΘΗΤΙΚΕΣ ΣΥΝΑΡΤΗΣΕΙΣ
    # ========================================================================
    def domain_infeasible(LB, UB, tol=1e-12):
        """
        Ελέγχει αν κάποια μεταβλητή έχει lower_bound > upper_bound
        
        ΧΡΗΣΗ:
        Μετά από branching ή propagations, αν LB[i] > UB[i] → Infeasible κόμβος
        
        Παράμετροι:
            LB: Lower bounds [array]
            UB: Upper bounds [array]
            tol: Numerical tolerance
        
        Επιστρέφει:
            bool: True αν υπάρχει infeasibility
        """
        return np.any(np.asarray(LB) > np.asarray(UB) + tol)


    def check_integrality(x):
        """
        Ελέγχει αν η λύση x είναι ακέραια σε όλες τις integer μεταβλητές
        
        ΛΟΓΙΚΗ:
        Για κάθε integer μεταβλητή, ελέγχει αν |x[i] - round(x[i])| < EPS
        
        Παράμετροι:
            x: Διάνυσμα λύσης [array]
        
        Επιστρέφει:
            tuple: (
                is_integer: bool - Όλες οι integer vars είναι ακέραιες;
                frac_idx: int|None - Index πρώτης fractional (αν υπάρχει)
                frac_val: float|None - Value της fractional (αν υπάρχει)
            )
        """

        for idx, is_int_var in enumerate(integer_var):
            if is_int_var and not is_nearly_integer(x[idx], tolerance=EPS):
                # Βρέθηκε fractional μεταβλητή
                return False, idx, float(x[idx])
            
        # Όλες οι integer μεταβλητές είναι ακέραιες
        return True, None, None


    def push_heap(node):
        """
        Προσθέτει κόμβο στο min-heap με σωστή προτεραιότητα
        
        ΠΡΟΤΕΡΑΙΟΤΗΤΑ:
        - MAX problem: -obj (μεγαλύτερο obj → μικρότερο priority)
        - MIN problem: obj (μικρότερο obj → μικρότερο priority)
        
        ΛΟΓΙΚΗ:
        Python's heapq είναι min-heap → Μικρότερο priority βγαίνει πρώτο
        
        Παράμετροι:
            node: Node object
        """

        pr = -node.obj_val if isMax else node.obj_val
        heapq.heappush(heap, (pr, node.node_id, node))


    def prune_by_cutoff(bound):
        """
        Ελέγχει αν ένας κόμβος μπορεί να αποκοπεί με βάση το cutoff
        
        ΚΡΙΤΗΡΙΑ PRUNING:
        - MAX: bound < cutoff → Prune (δεν μπορεί να βελτιώσει incumbent)
        - MIN: bound > cutoff → Prune (δεν μπορεί να βελτιώσει incumbent)
        
        Παράμετροι:
            bound: LP relaxation objective του κόμβου
        
        Επιστρέφει:
            bool: True αν πρέπει να αποκοπεί
        """
        if isMax:
            # MAX: Prune αν bound < cutoff (δεν μπορεί να φτάσει το incumbent)
            return (cutoff > -np.inf) and (bound < cutoff - EPS)
        else:
            # MIN: Prune αν bound > cutoff (δεν μπορεί να βελτιώσει το incumbent)
            pr = (cutoff < np.inf) and (bound > cutoff + EPS)
            return pr

    # =======================================================================================
    # PROPAGATIONS: ΜΕΙΩΣΗ ΧΩΡΟΥ ΑΝΑΖΗΤΗΣΗΣ
    # ========================================================================================
    # ΣΚΟΠΟΣ:  Μείωση του πεδίου τιμών (domain) των μεταβλητών με βάση τη δομή του προβλήματος
    # Αξιοποιεί λογικούς περιορισμούς για αυστηρότερα (tighter) όρια

    def apply_propagations(selected_var_idx, left_lb, left_ub, right_lb, right_ub):
        """
        Εφαρμόζει προωθήσεις περιορισμού σε left και right παιδιά
        
        (A) x[i,j] BRANCHING:
            RIGHT (x[i,j]=1): Θέση i ανοίγει με τύπο j
            Άλλοι τύποι στη θέση i πρέπει x[i,j']=0 για j'≠j
            (Μόνο ΕΝΑΣ τύπος ανά θέση)
        
        (B) y[k,i] BRANCHING:
            RIGHT (y[k,i]=1): Πελάτης k ανατίθεται σε θέση i
            Άλλες θέσεις για πελάτη k πρέπει y[k,i']=0 για i'≠i
            (Μόνο ΜΙΑ θέση ανά πελάτη)
        
        (C) x[i,j] BRANCHING:
            LEFT (x[i,j]=0): Τύπος j στη θέση i κλείνει
            Αν ΟΛΟΙ οι τύποι στη θέση i είναι 0 → Θέση i κλειστή
            Κανένας πελάτης δεν μπορεί να ανατεθεί: y[k,i]=0 για όλα k
              
            Σημείωση: Αν κάποιος πελάτης k έχει ήδη y[k,i]=1 (forced) τότε INFEASIBILITY (αντίφαση)
        
        Παράμετροι:
            selected_var_idx: Index της branching μεταβλητής
            left_lb, left_ub: Bounds για LEFT child
            right_lb, right_ub: Bounds για RIGHT child
        
        Επιστρέφει:
            tuple: (
                left_lb, left_ub, right_lb, right_ub: Ενημερωμένα bounds
                infeas_left: bool - LEFT child infeasible;
                infeas_right: bool - RIGHT child infeasible;
            )
        """
        infeas_left = False
        infeas_right = False

        # ΕΛΕΓΧΟΣ: x[i,j] ή y[k,i] branching;
        if selected_var_idx < N * E:

            # x[i,j] BRANCHING
            # Decode: selected_var_idx = i*E + j
            i = selected_var_idx // E  # Θέση
            j = selected_var_idx % E   # Τύπος

            # PROPAGATION (A): Aν x[i,j]=1 → x[i,j']=0 για j'≠j
            if right_lb[selected_var_idx] >= 1.0 - EPS:
                for jp in range(facility_types[i] + 1):  # Επιτρεπόμενοι τύποι για θέση i
                    if jp != j:
                        # Άλλος τύπος → Κλείσιμο
                        right_ub[i * E + jp] = 0.0

            # PROPAGATION (C): Όλα x[i,*]=0 → y[k,i]=0 για όλα k
            if left_ub[selected_var_idx] <= 0.0 + EPS:
                all_closed = True
                for j_check in range(facility_types[i] + 1):
                    idx_x = i * E + j_check
                    if left_ub[idx_x] > 0.0 + EPS:  # Υπάρχει ανοιχτός τύπος
                        all_closed = False
                        break

                if all_closed:
                    # GUARD: Έλεγχος infeasibility
                    # Αν κάποιος πελάτης k έχει ήδη forced y[k,i]=1  → ΑΝΤΙΦΑΣΗ: Πρέπει y[k,i]=1 αλλά θέση i κλειστή
                    for k in range(CL):
                        idx_y = N * E + k * N + i
                        if left_lb[idx_y] >= 1.0 - EPS:   # y[k,i] forced to 1
                            infeas_left = True            # INFEASIBILITY
                            break
                    # Αν δεν υπάρχει infeasibility → Zero όλα τα y[k,i]
                    if not infeas_left:
                        for k in range(CL):
                            idx_y = N * E + k * N + i
                            left_ub[idx_y] = 0.0    # Κανένας πελάτης σε θέση i
        else:
            # y[k,i] BRANCHING
            # Decode: selected_var_idx - N*E = k*N + i
            y_flat = selected_var_idx - N * E
            k = y_flat // N  # Πελάτης
            i = y_flat % N   # Θέση

            # PROPAGATION (B): Αν y[k,i]=1 → y[k,i']=0 για i'≠i
            if right_lb[selected_var_idx] >= 1.0 - EPS:
                base = N * E + k * N
                for ii in range(N):
                    if ii != i:
                        # Άλλη θέση → Κλείσιμο
                        right_ub[base + ii] = 0.0

        return left_lb, left_ub, right_lb, right_ub, infeas_left, infeas_right

   
    # ========================================================================
    # ΕΠΙΛΥΣΗ ΠΑΙΔΙΚΟΥ ΚΟΜΒΟΥ
    # ========================================================================
    # Λύνει το LP relaxation του child node και:
    # - Αν integer → Αποθήκευση/Ενημέρωση incumbent → Return None
    # - Αν fractional → Δημιουργία Node object → Return Node
    # - Αν infeasible/pruned → Return None
    def solve_child_build_node(child_lb, child_ub, parent_node, label, phase):
        """
        Λύνει child node και επιστρέφει Node αν fractional, None αν integer/pruned
        
        Παράμετροι:
            child_lb, child_ub: Bounds του child
            parent_node: Parent Node (για basis)
            label: "Left" ή "Right" (για debugging)
            phase: "dive" ή "best" (για branching selection)
        
        Επιστρέφει:
            Node|None: Node αν fractional, None αλλιώς
        
        Ενημερώνει: 
            cutoff, incumbent, best_sol_idx, solutions
        """

        nonlocal cutoff, incumbent, best_sol_idx

        # ======================================================
        # ΕΛΕΓΧΟΣ 1: Domain Feasibility
        # ======================================================
        # Αν κάποια μεταβλητή έχει LB > UB → Infeasible
        if domain_infeasible(child_lb, child_ub):
            return None  # Prune: Domain infeasibility

        # ======================================================
        # WARM START: Basis από parent
        # ======================================================
        # Επιτάχυνση LP solve με καλό starting point
        # Gurobi Basis: VBasis: Variable basis status [array] και CBasis: Constraint basis status [array]
        # Αν το parent έχει valid basis → Χρησιμοποιείται 
        if (len(parent_node.vbasis) == model.numVars) and (len(parent_node.cbasis) == model.numConstrs):
            model.setAttr("VBasis", model.getVars(), parent_node.vbasis)
            model.setAttr("CBasis", model.getConstrs(), parent_node.cbasis)

        # Ενημέρωση bounds και solve
        model.setAttr("LB", model.getVars(), child_lb)
        model.setAttr("UB", model.getVars(), child_ub)
        model.update()
        model.optimize()

        # ========================================================
        # ΕΛΕΓΧΟΣ 2: LP Status
        # ========================================================
        if model.status != GRB.OPTIMAL:
            return None  # Prune: LP infeasible ή unbounded

        # Εξαγωγή λύσης
        bound = float(model.ObjVal)                                    # LP relaxation objective
        x = np.array(model.getAttr("X", model.getVars()), dtype=float) # Solution
        
        # ========================================================
        # ΕΛΕΓΧΟΣ 3: Prune by Cutoff
        # ========================================================
        # Αν το bound δεν μπορεί να βελτιώσει το incumbent → Prune
        if prune_by_cutoff(bound):
            return None  # Prune: Bound-based pruning

        # =======================================================
        # ΕΛΕΓΧΟΣ 4: Integrality
        # =======================================================
        is_int, frac_idx, frac_val = check_integrality(x)

        if is_int:
            # Βρέθηκε ακέραια λύση
            # Αποθήκευση λύσης
            solutions.append([x, bound, parent_node.depth + 1])
            this_idx = len(solutions) - 1

            # Ενημέρωση incumbent 
            # ΠΡΩΤΗ ακέραια λύση
            if best_sol_idx is None:
                best_sol_idx = this_idx
                incumbent = bound
                # Ενημέρωση cutoff
                cutoff = max(cutoff, incumbent) if isMax else min(cutoff, incumbent)
            
            # ΒΕΛΤΙΩΣΗ υπάρχοντος incumbent
            else:
                # MAX: Νέα λύση καλύτερη αν bound > incumbent
                if isMax and bound > incumbent + EPS:
                    incumbent = bound
                    best_sol_idx = this_idx
                    cutoff = incumbent  # Ενημέρωση cutoff

                # MIN: Νέα λύση καλύτερη αν bound < incumbent
                if (not isMax) and bound < incumbent - EPS:
                    incumbent = bound
                    best_sol_idx = this_idx
                    cutoff = incumbent  # Ενημέρωση cutoff

            return None  # Integer node δεν χρειάζεται branching

        # ===============================================================
        # ΚΛΑΣΜΑΤΙΚΗ ΛΥΣΗ → ΔΗΜΙΟΥΡΓΙΑ ΚΟΜΒΟΥ
        # ===============================================================
        

        # Επιλογή μεταβλητής για διακλάδωση
        # Χρήση της select_branching_fn για έξυπνη επιλογή
        # Αν επιστρέψει None → Fallback σε πρώτη fractional
        b_idx, b_val = select_branching_fn(x, child_ub, phase)
        if b_idx is None:
            # Fallback: Πρώτη fractional μεταβλητή
            b_idx, b_val = frac_idx, frac_val

        # Αποθήκευση BASIS (για warm start των παιδιών)
        vb = model.getAttr("VBasis", model.getVars())
        cb = model.getAttr("CBasis", model.getConstrs())

        # Δημιουργία κόμβου
        return Node(child_ub.copy(), child_lb.copy(), parent_node.depth + 1, vb, cb, b_idx, b_val, bound, label)

    # ========================================================================
    # ΕΠΙΛΥΣΗ ΡΙΖΙΚΟΥ ΚΟΜΒΟΥ
    # ========================================================================
    # Λύνει το αρχικό LP relaxation και:
    # - Root bound (lower/upper bound για το πρόβλημα)
    # - Έλεγχος αν είναι ήδη integer
    # - Αρχικοποίηση του δέντρου αναζήτησης

    # Ρύθμιση και επίλυση
    model.setAttr("LB", model.getVars(), lb)
    model.setAttr("UB", model.getVars(), ub)
    model.update()
    model.optimize()

    # Έλεγχος status
    if model.status != GRB.OPTIMAL:
        # Root LP infeasible → Το πρόβλημα είναι infeasible
        return [], None, 0, False

    # Εξαγωγή λύσης του root κόμβου
    x_root = np.array(model.getAttr("X", model.getVars()), dtype=float)
    root_bound = float(model.ObjVal)

    # Basis για warm start
    vb_root = model.getAttr("VBasis", model.getVars())
    cb_root = model.getAttr("CBasis", model.getConstrs())

    # Έλεγχος ακεραιότητας
    root_is_int, frac_idx, frac_val = check_integrality(x_root)
    if root_is_int:
        # ROOT ακέραιος
        # Αποθήκευση λύσης
        solutions.append([x_root, root_bound, 0]) # Depth=0
        this_idx = len(solutions) - 1

        # Ενημέρωση incumbent
        if best_sol_idx is None:
            best_sol_idx = this_idx
            incumbent = root_bound
        else:
            # Σύγκριση με heuristic (αν υπάρχει)
            if isMax and root_bound > incumbent + EPS:
                best_sol_idx = this_idx
                incumbent = root_bound
            elif (not isMax) and root_bound < incumbent - EPS:
                best_sol_idx = this_idx
                incumbent = root_bound

        # Ενημέρωση bounds
        cutoff = max(cutoff, incumbent) if isMax else min(cutoff, incumbent)
        lower_bound = root_bound
        upper_bound = root_bound

        # ΤΕΡΜΑΤΙΣΜΟΣ: Βρέθηκε βέλτιστη
        return solutions, best_sol_idx, len(solutions), heuristic_loaded

    # =============================================================================
    # ROOT ΜΗ ΑΚΕΡΑΙΟΣ → ΔΙΑΚΛΑΔΩΣΗ
    # =============================================================================
    # Επιλογή μεταβλητής διακλάδωσης (με phase="dive")
    root_bi, root_bv = select_branching_fn(x_root, ub, "dive")
    if root_bi is None:
        # Fallback σε πρώτη fractional
        root_bi, root_bv = frac_idx, frac_val

    # Δημιουργία root Node
    root_node = Node(np.array(ub, dtype=float), np.array(lb, dtype=float), 0, vb_root, cb_root, root_bi, root_bv, root_bound, "Root")

    # ============================================================================
    # ΚΡΙΣΙΜΗ ΑΠΟΦΑΣΗ: ΔΙΑΔΡΟΜΗ ΑΝΑΖΗΤΗΣΗΣ
    # ============================================================================
    # ΕΡΩΤΗΣΗ: Έχουμε ήδη incumbent από heuristics;
    # ΝΑΙ → SKIP diving, άμεση best-first αναζήτηση
    # ΟΧΙ  → DFS diving για γρήγορο incumbent και μετά best-first αναζήτηση
    HAVE_INCUMBENT_FROM_HEURISTICS = (cutoff > -np.inf) if isMax else (cutoff < np.inf)

    
    # ========================================================================
    # ΦΑΣΗ 1: DFS DIVING 
    # ========================================================================
    """
    Γρήγορη εύρεση εφικτής ακέραιης λύσης (incumbent) μέσω DFS diving
    ΣΤΡΑΤΗΓΙΚΗ:
      - Preferred child → Stack (συνέχιση διαδρομής DFS)
      - Sibling → Deferred list (όχι heap, αποφυγή overhead)
      - Προτίμηση με βάση στρογγυλοποίηση: x=0.7 → RIGHT (x ≥ 1)
    ΤΕΡΜΑΤΙΣΜΟΣ:
      - Βρέθηκε incumbent
      - Time limit
      - dive_deadline
      - dive_node_limit
    ΣΥΝΘΗΚΗ:
    Εκτελείται μόνο αν ΔΕΝ υπάρχει heuristic incumbent
    """

    dive_nodes = 0                           # Counter: Κόμβοι στο dive
    dive_deadline = start + dive_time_limit  # Χρόνος τερματισμού dive

    if not HAVE_INCUMBENT_FROM_HEURISTICS:
        # Αρχικοποίηση stack με root
        dive_stack = [root_node]

        # ===========================================
        # ΚΥΡΙΟΣ ΒΡΟΧΟΣ DIVING
        # ===========================================
        while dive_stack and best_sol_idx is None:
            # Έλεγχος 1: Global time limit
            if time.time() - start >= TIME_LIMIT:
                break
            # Έλεγχος 2: Dive time limit
            if time.time() >= dive_deadline:
                break
            # Έλεγχος 3: Dive node limit
            if dive_nodes >= dive_node_limit:
                break

            # ========================================
            # POP CURRENT NODE (LIFO - DFS)
            # ========================================
            current_node = dive_stack.pop()
            dive_nodes += 1  # Counter
            nodes += 1       # Global counter

            # Αν bound χειρότερο από cutoff → Prune (skip)
            if prune_by_cutoff(current_node.obj_val):
                continue

            # ===============================================
            # BRANCHING: CREATE LEFT & RIGHT CHILDREN
            # ===============================================
            selected_var_idx = current_node.branching_var
            selected_val = current_node.branch_val

            # Αρχικοποίηση ορίων των παιδιών (copies από parent)
            left_lb = current_node.lb.copy()
            left_ub = current_node.ub.copy()
            right_lb = current_node.lb.copy()
            right_ub = current_node.ub.copy()

            # Branching constraints
            # LEFT:  x ≤ floor(value)  → UB = floor
            # RIGHT: x ≥ ceil(value)   → LB = ceil
            left_ub[selected_var_idx] = np.floor(selected_val)
            right_lb[selected_var_idx] = np.ceil(selected_val)

            # Εφαρμογή propagations
            left_lb, left_ub, right_lb, right_ub, infeas_left, infeas_right = apply_propagations(
                selected_var_idx, left_lb, left_ub, right_lb, right_ub
            )

            # ========================================
            # SOLVE LEFT CHILD
            # ========================================
            if infeas_left:
                left_node = None   # Propagation infeasibility
            else:
                left_node = solve_child_build_node(left_lb, left_ub, current_node, "Left", "dive")

            # =======================================
            # SOLVE RIGHT CHILD
            # =======================================
            if infeas_right:
                right_node = None  # Propagation infeasibility
            else:
                right_node = solve_child_build_node(right_lb, right_ub, current_node, "Right", "dive")

            # ========================================
            # PREFERRED DIRECTION: Rounding-based
            # ========================================
            # Αν x=0.7 → Prefer ceil (x≥1) → RIGHT
            # Αν x=0.3 → Prefer floor (x≤0) → LEFT
            prefer_right = (selected_val >= 0.5)

            # Τοποθέτηση παιδιού
            if prefer_right:
                # Prefer RIGHT → DFS path
                if right_node is not None:
                    dive_stack.append(right_node)        # Stack: συνέχεια της DFS αναζήτησης
                if left_node is not None:
                    deferred_nodes.append(left_node)     # sibling -> deferred (NO heap)
            else:
                # Prefer LEFT → DFS path
                if left_node is not None:
                    dive_stack.append(left_node)         # Stack: συνέχεια της DFS αναζήτησης
                if right_node is not None:
                    deferred_nodes.append(right_node)    # sibling -> deferred (NO heap)

    # --------------------------------- ΤΕΛΟΣ DIVING -------------------------------------------------------

    else:
        # ================================================================
        # SKIP DIVING: Heuristic incumbent exists
        # ================================================================
        # Root node → Deferred (θα μπει στο heap)
        deferred_nodes.append(root_node)

    """
    ΜΕΤΑΒΑΣΗ: DEFERRED NODES → HEAP (FILTERED TRANSFER)
    Μεταφορά deferred nodes (+ leftover dive_stack) στο heap για best-first
    ΣΤΡΑΤΗΓΙΚΗ:
       1. Συγκέντρωση: dive_stack + deferred_nodes
       2. Sorting: Κατά bound (best first)
       3. Filtering: Prune by cutoff (incumbent-based)
       4. Limiting: Optional heap_seed_limit
       5. Transfer: push_heap
    
    ΠΛΕΟΝΕΚΤΗΜΑ:
       - Pruning ΠΡΙΝ μπουν στο heap (όχι κατά το pop)
       - Μειωμένο heap size
       - Ταχύτερη best-first phase
    """

    # ======================================================================
    # ΒΗΜΑ 1: Συγκέντρωση leftover dive_stack
    # ======================================================================
    # Αν το diving σταμάτησε πριν αδειάσει το stack → Μεταφορά στα deferred
    while dive_stack:
        deferred_nodes.append(dive_stack.pop())

    # ====================================================================
    # ΒΗΜΑ 2: Sorting κατά bound
    # ====================================================================
    # MAX: Φθίνουσα (καλύτερα bounds πρώτα)
    # MIN: Αύξουσα (καλύτερα bounds πρώτα)
    deferred_nodes.sort(key=lambda n: n.obj_val, reverse=isMax)

    # ====================================================================
    # ΒΗΜΑ 3: Filtered transfer με pruning & limiting
    # ====================================================================
    moved = 0    # Counter: Πόσοι μεταφέρθηκαν
    pruned = 0   # Counter: Πόσοι pruned

    # Limit: Μέγιστος αριθμός nodes στο heap
    limit = len(deferred_nodes) if heap_seed_limit is None else min(heap_seed_limit, len(deferred_nodes))

    for n in deferred_nodes:
        # LIMIT CHECK
        if moved >= limit:
            break  # Φτάσαμε το όριο

        # PRUNE CHECK
        if prune_by_cutoff(n.obj_val):
            pruned += 1  # Count pruned
            continue     # Skip αυτόν τον κόμβο
        
        # ΜΕΤΑΦΟΡΑ ΣΤΟ HEAP
        push_heap(n)
        moved += 1

    # ========================================================================
    # ΦΑΣΗ 2: ΑΝΑΖΗΤΗΣΗ ΒΕΛΤΙΣΤΟΥ-ΠΡΩΤΑ
    # ========================================================================
    """
    Εύρεση βέλτιστης λύσης με best-first αναζήτηση. Eξερεύνηση κόμβων με καλύτερο LP bound πρώτα
    ΣΤΡΑΤΗΓΙΚΗ:
       - Pop best bound από heap (min-heap)
       - Branch σε left/right children
       - Push children στο heap
       - Επιθετικό pruning με incumbent cutoff
    
    OPTIMALITY ΣΥΝΘΗΚΗ:
    best_open_bound = incumbent → Optimality proven
    ΤΕΡΜΑΤΙΣΜΟΣ:
       - Optimality
       - Heap άδειο (όλοι pruned)
       - Time limit
    """
    while heap:
        # Έλεγχος τερματισμού: Time limit
        if time.time() - start >= TIME_LIMIT:
            break

        # ====================================================
        # CURRENT BOUNDS UPDATE
        # ====================================================
        # Best open bound = objective του top heap node
        best_open_bound = heap[0][2].obj_val

        # Ενημέρωση global bounds
        if isMax:
            upper_bound = best_open_bound   
            lower_bound = incumbent         
        else:
            lower_bound = best_open_bound
            upper_bound = incumbent if best_sol_idx is not None else cutoff

        # ===============================================================
        # OPTIMALITY CHECK
        # ===============================================================
        # ΚΡΙΤΗΡΙΟ: best_open_bound = incumbent → ΒΕΛΤΙΣΤΗ ΛΥΣΗ!
        # ΛΟΓΙΚΗ (MIN):
        #   - best_open_bound = καλύτερο bound ανοιχτών κόμβων
        #   - LP bound ≤ οποιαδήποτε ακέραια λύση από αυτόν τον κόμβο
        #   - Αν best_open_bound ≥ incumbent → Κανένας κόμβος δεν μπορεί να δώσει obj < incumbent
        #   - Επομένως: incumbent είναι ΒΕΛΤΙΣΤΗ
        if best_sol_idx is not None:
            if isMax:
                # MAX: Optimal αν best_open_bound ≤ incumbent
                if best_open_bound <= incumbent + EPS:
                    break
            else:
                # MIN: Optimal αν best_open_bound ≥ incumbent
                if best_open_bound >= incumbent - EPS:
                    break

        # ================================================================
        # POP BEST NODE
        # ================================================================
        _, _, current_node = heapq.heappop(heap)
        nodes += 1

        # ================================================================
        # PRUNING CHECK (re-check κατά το pop)
        # ================================================================
        # Cutoff μπορεί να έχει ενημερωθεί μετά το push
        if prune_by_cutoff(current_node.obj_val):
            continue

        # ================================================================
        # BRANCHING: CREATE LEFT & RIGHT CHILDREN
        # ================================================================
        selected_var_idx = current_node.branching_var
        selected_val = current_node.branch_val

        # Αρχικοποίηση ορίων των παιδιών (copies από τον parent)
        left_lb = current_node.lb.copy()
        left_ub = current_node.ub.copy()
        right_lb = current_node.lb.copy()
        right_ub = current_node.ub.copy()

        # Branching constraints
        # LEFT:  x ≤ floor(value)  → UB = floor(value)
        # RIGHT: x ≥ ceil(value)   → LB = ceil(value)
        left_ub[selected_var_idx] = np.floor(selected_val)
        right_lb[selected_var_idx] = np.ceil(selected_val)

        # Εφαρμογή propagations
        left_lb, left_ub, right_lb, right_ub, infeas_left, infeas_right = apply_propagations(
            selected_var_idx, left_lb, left_ub, right_lb, right_ub
        )

        # ================================================================
        # SOLVE & PUSH LEFT CHILD
        # ================================================================
        if not infeas_left:
            ln = solve_child_build_node(left_lb, left_ub, current_node, "Left", "best")
            if ln is not None:
                push_heap(ln)   # Fractional → Add to heap

        # ================================================================
        # SOLVE & PUSH RIGHT CHILD
        # ================================================================
        if not infeas_right:
            rn = solve_child_build_node(right_lb, right_ub, current_node, "Right", "best")
            if rn is not None:
                push_heap(rn)   # Fractional → Add to heap

    # ---------------------------------------- ΤΕΛΟΣ BEST-FIRST ------------------------------------------------------

    # Eπιστροφή αποτελεσμάτων
    return solutions, best_sol_idx, len(solutions), heuristic_loaded




